                emitNameScopeRegistration,
                nameScopeReference,
                existingVariableName is null,
                topDownAttachmentTemplate,
                completeNameScopeOnNodeCompletion,
                serviceProviderReference,
                baseUriExpression,
                nodeParentStackReferences,
                intermediateRootReference,
                emitDebugLineDirectives,
                lineDirectiveFilePath))
        {
            return variableName;
        }

        if (existingVariableName is null)
        {
            nodeCounter++;
            EmitNodeStatement($"var {variableName} = {BuildObjectCreationExpression(node, serviceProviderReference, baseUriExpression)};");
        }

        if (emitNameScopeRegistration &&
            !string.IsNullOrWhiteSpace(nameScopeReference))
        {
            var scopedElementVariable = "__scopedStyledElement" + nodeCounter.ToString();
            EmitNodeStatement($"if ((object){variableName} is global::Avalonia.StyledElement {scopedElementVariable})");
            EmitNodeStatement("{");
            EmitNodeStatement($"    __TrySetNameScope({scopedElementVariable}, {nameScopeReference});");
            EmitNodeStatement("}");
        }

        EmitNodeStatement($"__BeginInit({variableName});");
        if (node.ChildAttachmentMode == ResolvedChildAttachmentMode.DictionaryAdd &&
            (node.Children.Length > 0 || existingVariableName is not null))
        {
            EmitNodeStatement($"if (__replaceExistingCollections) __TryClearDictionaryEntries({variableName});");
        }

        string? pendingTopDownAttachmentStatement = null;
        if (node.UseTopDownInitialization &&
            !string.IsNullOrWhiteSpace(topDownAttachmentTemplate))
        {
            var attachmentValueExpression = BuildAttachedNodeValueExpression(
                node,
                variableName,
                serviceProviderReference,
                rootReference,
                intermediateRootReference,
                baseUriExpression,
                nodeParentStackExpression);
            pendingTopDownAttachmentStatement = ExpandTopDownAttachmentTemplate(
                topDownAttachmentTemplate!,
                attachmentValueExpression);
        }

        if (!string.IsNullOrWhiteSpace(node.Name))
        {
            EmitNodeStatement(
                $"if ((object){variableName} is global::Avalonia.StyledElement) ((global::Avalonia.StyledElement)(object){variableName}).Name = \"{Escape(node.Name!)}\";");

            if (namedFieldMap.TryGetValue(node.Name!, out var fieldName))
            {
                EmitNodeStatement($"{rootReference}.{fieldName} = {variableName};");
            }

            if (emitNameScopeRegistration &&
                !string.IsNullOrWhiteSpace(nameScopeReference))
            {
                EmitNodeStatement(
                    $"{nameScopeReference}.Register(\"{Escape(node.Name!)}\", {variableName});");
            }
        }

        if (!string.IsNullOrWhiteSpace(pendingTopDownAttachmentStatement))
        {
            EmitNodeStatement(pendingTopDownAttachmentStatement);
        }

        foreach (var assignment in node.PropertyAssignments)
        {
            var avaloniaPropertyExpression = BuildAvaloniaPropertyExpression(assignment);
            var avaloniaPriorityExpression = BuildAvaloniaPriorityExpression(assignment);
            var targetPropertyExpression = BuildTargetPropertyExpression(assignment);
            var bindingAnchorExpression =
                $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ResolveBindingAnchor({variableName}, {nodeParentStackExpression})";
            var valueExpression = ExpandMarkupContextExpression(
                assignment.ValueExpression,
                serviceProviderReference,
                rootReference,
                intermediateRootReference,
                variableName,
                targetPropertyExpression,
                baseUriExpression,
                nodeParentStackExpression);

            if (string.IsNullOrWhiteSpace(avaloniaPropertyExpression))
            {
                var clrAssignmentValueExpression = AttachBindingNameScope(valueExpression, assignment.ValueKind);

                if (TryBuildSpecialClrSetterInvocation(
                        variableName,
                        assignment.PropertyName,
                        assignment.ClrPropertyOwnerTypeName,
                        assignment.ClrPropertyTypeName,
                        clrAssignmentValueExpression,
                        out var specialClrSetterInvocation))
                {
                    EmitStatementAt(specialClrSetterInvocation, assignment.Line, assignment.Column);
                }
                else if (TryBuildDirectClrPropertyAssignment(
                        variableName,
                        assignment.PropertyName,
                        assignment.ClrPropertyTypeName,
                        clrAssignmentValueExpression,
                        out var directClrAssignment))
                {
                    EmitStatementAt(directClrAssignment, assignment.Line, assignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.{assignment.PropertyName} = {clrAssignmentValueExpression};",
                        assignment.Line,
                        assignment.Column);
                }
            }
            else
            {
                if (IsBindingValueKind(assignment.ValueKind))
                {
                    var bindingExpression = AttachBindingNameScope(valueExpression, assignment.ValueKind);
                    EmitStatementAt(
                        $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ApplyBinding({variableName}, {avaloniaPropertyExpression}, {bindingExpression}, {bindingAnchorExpression});",
                        assignment.Line,
                        assignment.Column);
                    continue;
                }

                if (string.IsNullOrWhiteSpace(avaloniaPriorityExpression))
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpression}, {WrapSetValueExpression(valueExpression, variableName, avaloniaPropertyExpression)});",
                        assignment.Line,
                        assignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpression}, {WrapSetValueExpression(valueExpression, variableName, avaloniaPropertyExpression)}, {avaloniaPriorityExpression});",
                        assignment.Line,
                        assignment.Column);
                }
            }
        }

        foreach (var eventSubscription in node.EventSubscriptions)
        {
            EmitEventSubscription(
                sourceBuilder,
                indent,
                variableName,
                rootReference,
                eventSubscription,
                emitDebugLineDirectives,
                lineDirectiveFilePath);
        }

        foreach (var propertyElementAssignment in node.PropertyElementAssignments)
        {
            if (propertyElementAssignment.IsCollectionAdd)
            {
                EmitStatementAt(
                    $"if (__replaceExistingCollections) __TryClearCollection({variableName}.{propertyElementAssignment.PropertyName});",
                    propertyElementAssignment.Line,
                    propertyElementAssignment.Column);
                var isMergedDictionariesProperty = string.Equals(
                    propertyElementAssignment.PropertyName,
                    "MergedDictionaries",
                    StringComparison.Ordinal);
                var collectionExpression = $"{variableName}.{propertyElementAssignment.PropertyName}";
                for (var valueIndex = 0; valueIndex < propertyElementAssignment.ObjectValues.Length; valueIndex++)
                {
                    var value = propertyElementAssignment.ObjectValues[valueIndex];
                    var addInstruction = ResolveCollectionAddInstruction(
                        propertyElementAssignment.CollectionAddInstructions,
                        valueIndex,
                        node.TypeName + "." + propertyElementAssignment.PropertyName);
                    var isMergeResourceIncludeValue =
                        isMergedDictionariesProperty &&
                        IsMergeResourceIncludeNode(value);
                    var isStyleIncludeValue = IsStyleIncludeNode(value);
                    var requiresDeferredCollectionAttach =
                        isMergeResourceIncludeValue ||
                        isStyleIncludeValue;
                    var inlineAddValueToken = AttachBindingNodeNameScope(value, TopDownAttachValueToken);
                    var inlineAddTemplate = BuildCollectionAddStatement(
                        collectionExpression,
                        inlineAddValueToken,
                        addInstruction);
                    var valueVariable = EmitNode(
                        value,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: requiresDeferredCollectionAttach ? null : inlineAddTemplate,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference,
                        emitDebugLineDirectives: emitDebugLineDirectives,
                        lineDirectiveFilePath: lineDirectiveFilePath);
                    if (isMergeResourceIncludeValue)
                    {
                        var mergeIncludeExpression = BuildAttachedNodeValueExpression(
                            value,
                            valueVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, valueVariable)));
                        EmitStatementAt(
                            $"if (!__TryApplyMergedResourceInclude({variableName}, {mergeIncludeExpression})) {BuildCollectionAddStatement(collectionExpression, mergeIncludeExpression, addInstruction)}",
                            value.Line,
                            value.Column);
                        continue;
                    }

                    if (isStyleIncludeValue)
                    {
                        var styleIncludeExpression = BuildAttachedNodeValueExpression(
                            value,
                            valueVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, valueVariable)));
                        EmitStatementAt(
                            $"if (!__TryApplyStyleInclude({collectionExpression}, {variableName}, {styleIncludeExpression})) {BuildCollectionAddStatement(collectionExpression, styleIncludeExpression, addInstruction)}",
                            value.Line,
                            value.Column);
                        continue;
                    }

                    if (!value.UseTopDownInitialization)
                    {
                        var valueExpression = BuildAttachedNodeValueExpression(
                            value,
                            valueVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, valueVariable)));
                        valueExpression = AttachBindingNodeNameScope(value, valueExpression);
                        EmitStatementAt(
                            BuildCollectionAddStatement(collectionExpression, valueExpression, addInstruction),
                            value.Line,
                            value.Column);
                    }
                }

                continue;
            }

            if (propertyElementAssignment.IsDictionaryMerge)
            {
                if (TryEmitDictionaryMergePropertyElement(
                        propertyElementAssignment,
                        variableName,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference,
                        baseUriExpression,
                        nodeParentStackReferences,
                        intermediateRootReference,
                        emitDebugLineDirectives,
                        lineDirectiveFilePath))
                {
                    continue;
                }
            }

            if (propertyElementAssignment.ObjectValues.Length == 0)
            {
                continue;
            }

            var avaloniaPropertyExpression = BuildAvaloniaPropertyExpression(propertyElementAssignment);
            var avaloniaPriorityExpression = BuildAvaloniaPriorityExpression(propertyElementAssignment);
            string assignmentTemplate;
            if (string.IsNullOrWhiteSpace(avaloniaPropertyExpression))
            {
                if (TryBuildSpecialClrSetterInvocation(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyOwnerTypeName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        TopDownAttachValueToken,
                        out var specialTopDownSetterInvocation))
                {
                    assignmentTemplate = specialTopDownSetterInvocation;
                }
                else if (TryBuildDirectClrPropertyAssignment(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        TopDownAttachValueToken,
                        out var topDownClrAssignment))
                {
                    assignmentTemplate = topDownClrAssignment;
                }
                else
                {
                    assignmentTemplate = $"{variableName}.{propertyElementAssignment.PropertyName} = {TopDownAttachValueToken};";
                }
            }
            else if (propertyElementAssignment.ObjectValues[0].IsBindingObjectNode)
            {
                var bindingValueExpression = string.IsNullOrWhiteSpace(nameScopeReference)
                    ? TopDownAttachValueToken
                    : $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.AttachBindingNameScope({TopDownAttachValueToken}, {nameScopeReference})";
                assignmentTemplate =
                    $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ApplyBinding({variableName}, {avaloniaPropertyExpression}, {bindingValueExpression}, global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ResolveBindingAnchor({variableName}, {nodeParentStackExpression}));";
            }
            else if (string.IsNullOrWhiteSpace(avaloniaPriorityExpression))
            {
                assignmentTemplate =
                    $"{variableName}.SetValue({avaloniaPropertyExpression}, {WrapSetValueExpression(TopDownAttachValueToken, variableName, avaloniaPropertyExpression)});";
            }
            else
            {
                assignmentTemplate =
                    $"{variableName}.SetValue({avaloniaPropertyExpression}, {WrapSetValueExpression(TopDownAttachValueToken, variableName, avaloniaPropertyExpression)}, {avaloniaPriorityExpression});";
            }

            var assignedNode = propertyElementAssignment.ObjectValues[0];
            var assignedValue = EmitNode(
                assignedNode,
                sourceBuilder,
                ref nodeCounter,
                indent,
                rootReference,
                namedFieldMap,
                emitNameScopeRegistration,
                nameScopeReference,
                serviceProviderReference: serviceProviderReference,
                topDownAttachmentTemplate: assignmentTemplate,
                baseUriExpression: baseUriExpression,
                parentStackReferences: nodeParentStackReferences,
                intermediateRootReference: intermediateRootReference,
                emitDebugLineDirectives: emitDebugLineDirectives,
                lineDirectiveFilePath: lineDirectiveFilePath);
            if (assignedNode.UseTopDownInitialization)
            {
                continue;
            }

            assignedValue = BuildAttachedNodeValueExpression(
                assignedNode,
                assignedValue,
                serviceProviderReference,
                rootReference,
                intermediateRootReference,
                baseUriExpression,
                BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, assignedValue)));

            if (string.IsNullOrWhiteSpace(avaloniaPropertyExpression))
            {
                if (TryBuildSpecialClrSetterInvocation(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyOwnerTypeName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        assignedValue,
                        out var specialClrSetterInvocation))
                {
                    EmitStatementAt(
                        specialClrSetterInvocation,
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else if (TryBuildDirectClrPropertyAssignment(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        assignedValue,
                        out var directClrAssignment))
                {
                    EmitStatementAt(
                        directClrAssignment,
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.{propertyElementAssignment.PropertyName} = {assignedValue};",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
            }
            else
            {
                if (assignedNode.IsBindingObjectNode)
                {
                    var bindingValueExpression = string.IsNullOrWhiteSpace(nameScopeReference)
                        ? assignedValue
                        : $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.AttachBindingNameScope({assignedValue}, {nameScopeReference})";
                    EmitStatementAt(
                        $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ApplyBinding({variableName}, {avaloniaPropertyExpression}, {bindingValueExpression}, global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ResolveBindingAnchor({variableName}, {nodeParentStackExpression}));",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else if (string.IsNullOrWhiteSpace(avaloniaPriorityExpression))
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpression}, {WrapSetValueExpression(assignedValue, variableName, avaloniaPropertyExpression)});",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpression}, {WrapSetValueExpression(assignedValue, variableName, avaloniaPropertyExpression)}, {avaloniaPriorityExpression});",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
            }
        }

        switch (node.ChildAttachmentMode)
        {
            case ResolvedChildAttachmentMode.Content:
            {
                var contentPropertyName = string.IsNullOrWhiteSpace(node.ContentPropertyName)
                    ? "Content"
                    : node.ContentPropertyName!;
                TryBuildDirectClrPropertyAssignment(
                    variableName,
                    contentPropertyName,
                    null,
                    TopDownAttachValueToken,
                    out var topDownContentAssignment);
                var shouldResetImplicitContent =
                    existingVariableName is not null &&
                    string.IsNullOrWhiteSpace(topDownAttachmentTemplate);
                if (node.Children.Length > 0)
                {
                    var firstChildNode = node.Children[0];
                    var firstChild = EmitNode(
                        firstChildNode,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: topDownContentAssignment,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference,
                        emitDebugLineDirectives: emitDebugLineDirectives,
                        lineDirectiveFilePath: lineDirectiveFilePath);
                    if (!firstChildNode.UseTopDownInitialization)
                    {
                        var firstChildValueExpression = BuildAttachedNodeValueExpression(
                            firstChildNode,
                            firstChild,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, firstChild)));
                        TryBuildDirectClrPropertyAssignment(
                            variableName,
                            contentPropertyName,
                            null,
                            firstChildValueExpression,
                            out var directContentAssignment);
                        EmitStatementAt(
                            directContentAssignment,
                            firstChildNode.Line,
                            firstChildNode.Column);
                    }
                }
                else if (shouldResetImplicitContent && !HasExplicitContentValue(node, contentPropertyName))
                {
                    TryBuildDirectClrPropertyAssignment(
                        variableName,
                        contentPropertyName,
                        null,
                        "default",
                        out var resetContentAssignment);
                    EmitNodeStatement(resetContentAssignment);
                }

                break;
            }
            case ResolvedChildAttachmentMode.ChildrenCollection:
            {
                if (node.Children.Length > 0 || existingVariableName is not null)
                {
                    EmitNodeStatement($"if (__replaceExistingCollections) __TryClearCollection({variableName}.Children);");
                }
                for (var childIndex = 0; childIndex < node.Children.Length; childIndex++)
                {
                    var child = node.Children[childIndex];
                    var addInstruction = ResolveCollectionAddInstruction(
                        node.ChildAddInstructions,
                        childIndex,
                        node.TypeName + ".Children");
                    var inlineAddValueToken = AttachBindingNodeNameScope(child, TopDownAttachValueToken);
                    var inlineAddTemplate = BuildCollectionAddStatement(
                        $"{variableName}.Children",
                        inlineAddValueToken,
                        addInstruction);
                    var childVariable = EmitNode(
                        child,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: inlineAddTemplate,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference,
                        emitDebugLineDirectives: emitDebugLineDirectives,
                        lineDirectiveFilePath: lineDirectiveFilePath);
                    if (!child.UseTopDownInitialization)
                    {
                        var childValueExpression = BuildAttachedNodeValueExpression(
                            child,
                            childVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, childVariable)));
                        childValueExpression = AttachBindingNodeNameScope(child, childValueExpression);
                        EmitStatementAt(
                            BuildCollectionAddStatement($"{variableName}.Children", childValueExpression, addInstruction),
                            child.Line,
                            child.Column);
                    }
                }

                break;
            }
            case ResolvedChildAttachmentMode.ItemsCollection:
            {
                EmitNodeStatement($"if (__replaceExistingCollections) __TryClearCollection({variableName}.Items);");
                for (var childIndex = 0; childIndex < node.Children.Length; childIndex++)
                {
                    var child = node.Children[childIndex];
                    var addInstruction = ResolveCollectionAddInstruction(
                        node.ChildAddInstructions,
                        childIndex,
                        node.TypeName + ".Items");
                    var inlineAddValueToken = AttachBindingNodeNameScope(child, TopDownAttachValueToken);
                    var inlineAddTemplate = BuildCollectionAddStatement(
                        $"{variableName}.Items",
                        inlineAddValueToken,
                        addInstruction);
                    var childVariable = EmitNode(
                        child,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: inlineAddTemplate,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference,
                        emitDebugLineDirectives: emitDebugLineDirectives,
                        lineDirectiveFilePath: lineDirectiveFilePath);
                    if (!child.UseTopDownInitialization)
                    {
                        var childValueExpression = BuildAttachedNodeValueExpression(
                            child,
                            childVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, childVariable)));
                        childValueExpression = AttachBindingNodeNameScope(child, childValueExpression);
                        EmitStatementAt(
                            BuildCollectionAddStatement($"{variableName}.Items", childValueExpression, addInstruction),
                            child.Line,
                            child.Column);
                    }
                }

                break;
            }
            case ResolvedChildAttachmentMode.DirectAdd:
            {
                if (node.Children.Length > 0 || existingVariableName is not null)
                {
                    EmitNodeStatement($"if (__replaceExistingCollections) __TryClearCollection({variableName});");
                }
                for (var childIndex = 0; childIndex < node.Children.Length; childIndex++)
                {
                    var child = node.Children[childIndex];
                    var addInstruction = ResolveCollectionAddInstruction(
                        node.ChildAddInstructions,
                        childIndex,
                        node.TypeName);
                    var isStyleIncludeChild = IsStyleIncludeNode(child);
                    var inlineAddValueToken = AttachBindingNodeNameScope(child, TopDownAttachValueToken);
                    var inlineAddTemplate = BuildCollectionAddStatement(
                        variableName,
                        inlineAddValueToken,
                        addInstruction);
                    var childVariable = EmitNode(
                        child,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: isStyleIncludeChild ? null : inlineAddTemplate,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference,
                        emitDebugLineDirectives: emitDebugLineDirectives,
                        lineDirectiveFilePath: lineDirectiveFilePath);
                    if (isStyleIncludeChild)
                    {
                        var styleIncludeExpression = BuildAttachedNodeValueExpression(
                            child,
                            childVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, childVariable)));
                        EmitStatementAt(
                            $"if (!__TryApplyStyleInclude({variableName}, {variableName}, {styleIncludeExpression})) {BuildCollectionAddStatement(variableName, styleIncludeExpression, addInstruction)}",
                            child.Line,
                            child.Column);
                        continue;
                    }

                    if (!child.UseTopDownInitialization)
                    {
                        var childValueExpression = BuildAttachedNodeValueExpression(
                            child,
                            childVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, childVariable)));
                        childValueExpression = AttachBindingNodeNameScope(child, childValueExpression);
                        EmitStatementAt(
                            BuildCollectionAddStatement(variableName, childValueExpression, addInstruction),
                            child.Line,
                            child.Column);
                    }
                }

                break;
            }
            case ResolvedChildAttachmentMode.DictionaryAdd:
            {
                foreach (var child in node.Children)
                {
                    if (string.IsNullOrWhiteSpace(child.KeyExpression))
                    {
                        continue;
                    }

                    var childVariable = EmitNode(
                        child,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: $"__TryAddToDictionary({variableName}, {child.KeyExpression}, {TopDownAttachValueToken});",
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference,
                        emitDebugLineDirectives: emitDebugLineDirectives,
                        lineDirectiveFilePath: lineDirectiveFilePath);
                    if (!child.UseTopDownInitialization)
                    {
                        var childValueExpression = BuildAttachedNodeValueExpression(
                            child,
                            childVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, childVariable)));
                        EmitStatementAt(
                            $"__TryAddToDictionary({variableName}, {child.KeyExpression}, {childValueExpression});",
                            child.Line,
                            child.Column);
                    }
                }

                break;
            }
        }

        if (completeNameScopeOnNodeCompletion &&
            !string.IsNullOrWhiteSpace(nameScopeReference))
        {
            EmitNodeStatement($"__TryCompleteNameScope({nameScopeReference});");
        }

        EmitNodeStatement($"__EndInit({variableName});");
        return variableName;
    }

    private static bool TryEmitDeferredTemplateNode(
        ResolvedObjectNode node,
        StringBuilder sourceBuilder,
        ref int nodeCounter,
        string indent,
        string rootReference,
        string variableName,
        IReadOnlyDictionary<string, string> namedFieldMap,
        bool emitNameScopeRegistration,
        string? nameScopeReference,
        bool shouldCreateVariable,
        string? topDownAttachmentTemplate,
        bool completeNameScopeOnNodeCompletion,
        string serviceProviderReference,
        string baseUriExpression,
        ImmutableArray<string> parentStackReferences,
        string intermediateRootReference,
        bool emitDebugLineDirectives,
        string lineDirectiveFilePath)
    {
        if (!IsDeferredTemplateNode(node))
        {
            return false;
        }

        var nodeParentStackReferences = ExtendParentStack(parentStackReferences, variableName);
        var nodeParentStackExpression = BuildParentStackExpression(nodeParentStackReferences);
        void EmitNodeStatement(string statement)
        {
            AppendSourceMappedLine(
                sourceBuilder,
                indent + statement,
                emitDebugLineDirectives,
                lineDirectiveFilePath,
                node.Line,
                node.Column);
        }

        void EmitStatementAt(string statement, int line, int column)
        {
            AppendSourceMappedLine(
                sourceBuilder,
                indent + statement,
                emitDebugLineDirectives,
                lineDirectiveFilePath,
                line,
                column);
        }

        string AttachBindingNameScope(string expression, ResolvedValueKind valueKind)
        {
            if (string.IsNullOrWhiteSpace(nameScopeReference) || !IsBindingValueKind(valueKind))
            {
                return expression;
            }

            return $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.AttachBindingNameScope({expression}, {nameScopeReference})";
        }

        string AttachBindingNodeNameScope(ResolvedObjectNode nodeValue, string expression)
        {
            if (string.IsNullOrWhiteSpace(nameScopeReference) || !nodeValue.IsBindingObjectNode)
            {
                return expression;
            }

            return $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.AttachBindingNameScope({expression}, {nameScopeReference})";
        }

        string WrapSetValueExpression(string expression, string targetObjectExpression, string targetPropertyExpression)
        {
            return "global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ProvideMarkupExtensionValue(" +
                   expression +
                   ", " + serviceProviderReference +
                   ", " + rootReference +
                   ", " + intermediateRootReference +
                   ", " + targetObjectExpression +
                   ", " + targetPropertyExpression +
                   ", " + baseUriExpression +
                   ", " + nodeParentStackExpression +
                   ")";
        }

        if (shouldCreateVariable)
        {
            nodeCounter++;
            EmitNodeStatement($"var {variableName} = {BuildObjectCreationExpression(node, serviceProviderReference, baseUriExpression)};");
        }

        if (emitNameScopeRegistration &&
            !string.IsNullOrWhiteSpace(nameScopeReference))
        {
            var scopedElementVariable = "__scopedStyledElement" + nodeCounter.ToString();
            EmitNodeStatement($"if ((object){variableName} is global::Avalonia.StyledElement {scopedElementVariable})");
            EmitNodeStatement("{");
            EmitNodeStatement($"    __TrySetNameScope({scopedElementVariable}, {nameScopeReference});");
            EmitNodeStatement("}");
        }

        EmitNodeStatement($"__BeginInit({variableName});");
        string? pendingTopDownAttachmentStatement = null;
        if (node.UseTopDownInitialization &&
            !string.IsNullOrWhiteSpace(topDownAttachmentTemplate))
        {
            var attachmentValueExpression = BuildAttachedNodeValueExpression(
                node,
                variableName,
                serviceProviderReference,
                rootReference,
                intermediateRootReference,
                baseUriExpression,
                nodeParentStackExpression);
            pendingTopDownAttachmentStatement = ExpandTopDownAttachmentTemplate(
                topDownAttachmentTemplate!,
                attachmentValueExpression);
        }

        if (!string.IsNullOrWhiteSpace(node.Name))
        {
            EmitNodeStatement(
                $"if ((object){variableName} is global::Avalonia.StyledElement) ((global::Avalonia.StyledElement)(object){variableName}).Name = \"{Escape(node.Name!)}\";");

            if (namedFieldMap.TryGetValue(node.Name!, out var fieldName))
            {
                EmitNodeStatement($"{rootReference}.{fieldName} = {variableName};");
            }

            if (emitNameScopeRegistration &&
                !string.IsNullOrWhiteSpace(nameScopeReference))
            {
                EmitNodeStatement(
                    $"{nameScopeReference}.Register(\"{Escape(node.Name!)}\", {variableName});");
            }
        }

        if (!string.IsNullOrWhiteSpace(pendingTopDownAttachmentStatement))
        {
            EmitNodeStatement(pendingTopDownAttachmentStatement);
        }

        foreach (var assignment in node.PropertyAssignments)
        {
            if (IsContentPropertyName(assignment.PropertyName))
            {
                continue;
            }

            var avaloniaPropertyExpression = BuildAvaloniaPropertyExpression(assignment);
            var avaloniaPriorityExpression = BuildAvaloniaPriorityExpression(assignment);
            var targetPropertyExpression = BuildTargetPropertyExpression(assignment);
            var bindingAnchorExpression =
                $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ResolveBindingAnchor({variableName}, {nodeParentStackExpression})";
            var valueExpression = ExpandMarkupContextExpression(
                assignment.ValueExpression,
                serviceProviderReference,
                rootReference,
                intermediateRootReference,
                variableName,
                targetPropertyExpression,
                baseUriExpression,
                nodeParentStackExpression);

            if (string.IsNullOrWhiteSpace(avaloniaPropertyExpression))
            {
                var clrAssignmentValueExpression = AttachBindingNameScope(valueExpression, assignment.ValueKind);

                if (TryBuildSpecialClrSetterInvocation(
                        variableName,
                        assignment.PropertyName,
                        assignment.ClrPropertyOwnerTypeName,
                        assignment.ClrPropertyTypeName,
                        clrAssignmentValueExpression,
                        out var specialClrSetterInvocation))
                {
                    EmitStatementAt(specialClrSetterInvocation, assignment.Line, assignment.Column);
                }
                else if (TryBuildDirectClrPropertyAssignment(
                        variableName,
                        assignment.PropertyName,
                        assignment.ClrPropertyTypeName,
                        clrAssignmentValueExpression,
                        out var directClrAssignment))
                {
                    EmitStatementAt(directClrAssignment, assignment.Line, assignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.{assignment.PropertyName} = {clrAssignmentValueExpression};",
                        assignment.Line,
                        assignment.Column);
                }
            }
            else
            {
                if (IsBindingValueKind(assignment.ValueKind))
                {
                    var bindingExpression = AttachBindingNameScope(valueExpression, assignment.ValueKind);
                    EmitStatementAt(
                        $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ApplyBinding({variableName}, {avaloniaPropertyExpression}, {bindingExpression}, {bindingAnchorExpression});",
                        assignment.Line,
                        assignment.Column);
                    continue;
                }

                if (string.IsNullOrWhiteSpace(avaloniaPriorityExpression))
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpression}, {WrapSetValueExpression(valueExpression, variableName, avaloniaPropertyExpression)});",
                        assignment.Line,
                        assignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpression}, {WrapSetValueExpression(valueExpression, variableName, avaloniaPropertyExpression)}, {avaloniaPriorityExpression});",
                        assignment.Line,
                        assignment.Column);
                }
            }
        }

        foreach (var eventSubscription in node.EventSubscriptions)
        {
            EmitEventSubscription(
                sourceBuilder,
                indent,
                variableName,
                rootReference,
                eventSubscription,
                emitDebugLineDirectives,
                lineDirectiveFilePath);
        }

        foreach (var propertyElementAssignment in node.PropertyElementAssignments)
        {
            if (IsContentPropertyName(propertyElementAssignment.PropertyName))
            {
                continue;
            }

            if (propertyElementAssignment.IsCollectionAdd)
            {
                EmitStatementAt(
                    $"if (__replaceExistingCollections) __TryClearCollection({variableName}.{propertyElementAssignment.PropertyName});",
                    propertyElementAssignment.Line,
                    propertyElementAssignment.Column);
                var isMergedDictionariesProperty = string.Equals(
                    propertyElementAssignment.PropertyName,
                    "MergedDictionaries",
                    StringComparison.Ordinal);
                var collectionExpression = $"{variableName}.{propertyElementAssignment.PropertyName}";
                for (var valueIndex = 0; valueIndex < propertyElementAssignment.ObjectValues.Length; valueIndex++)
                {
                    var value = propertyElementAssignment.ObjectValues[valueIndex];
                    var addInstruction = ResolveCollectionAddInstruction(
                        propertyElementAssignment.CollectionAddInstructions,
                        valueIndex,
                        node.TypeName + "." + propertyElementAssignment.PropertyName);
                    var isMergeResourceIncludeValue =
                        isMergedDictionariesProperty &&
                        IsMergeResourceIncludeNode(value);
                    var isStyleIncludeValue = IsStyleIncludeNode(value);
                    var requiresDeferredCollectionAttach =
                        isMergeResourceIncludeValue ||
                        isStyleIncludeValue;
                    var inlineAddValueToken = AttachBindingNodeNameScope(value, TopDownAttachValueToken);
                    var inlineAddTemplate = BuildCollectionAddStatement(
                        collectionExpression,
                        inlineAddValueToken,
                        addInstruction);
                    var valueVariable = EmitNode(
                        value,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: requiresDeferredCollectionAttach ? null : inlineAddTemplate,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference,
                        emitDebugLineDirectives: emitDebugLineDirectives,
                        lineDirectiveFilePath: lineDirectiveFilePath);
                    if (isMergeResourceIncludeValue)
                    {
                        var mergeIncludeExpression = BuildAttachedNodeValueExpression(
                            value,
                            valueVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, valueVariable)));
                        EmitStatementAt(
                            $"if (!__TryApplyMergedResourceInclude({variableName}, {mergeIncludeExpression})) {BuildCollectionAddStatement(collectionExpression, mergeIncludeExpression, addInstruction)}",
                            value.Line,
                            value.Column);
                        continue;
                    }

                    if (isStyleIncludeValue)
                    {
                        var styleIncludeExpression = BuildAttachedNodeValueExpression(
                            value,
                            valueVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, valueVariable)));
                        EmitStatementAt(
                            $"if (!__TryApplyStyleInclude({collectionExpression}, {variableName}, {styleIncludeExpression})) {BuildCollectionAddStatement(collectionExpression, styleIncludeExpression, addInstruction)}",
                            value.Line,
                            value.Column);
                        continue;
                    }

                    if (!value.UseTopDownInitialization)
                    {
                        var valueExpression = BuildAttachedNodeValueExpression(
                            value,
                            valueVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, valueVariable)));
                        valueExpression = AttachBindingNodeNameScope(value, valueExpression);
                        EmitStatementAt(
                            BuildCollectionAddStatement(collectionExpression, valueExpression, addInstruction),
                            value.Line,
                            value.Column);
                    }
                }

                continue;
            }

            if (propertyElementAssignment.IsDictionaryMerge)
            {
                if (TryEmitDictionaryMergePropertyElement(
                        propertyElementAssignment,
                        variableName,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference,
                        baseUriExpression,
                        nodeParentStackReferences,
                        intermediateRootReference,
                        emitDebugLineDirectives,
                        lineDirectiveFilePath))
                {
                    continue;
                }
            }

            if (propertyElementAssignment.ObjectValues.Length == 0)
            {
                continue;
            }

            var avaloniaPropertyExpression = BuildAvaloniaPropertyExpression(propertyElementAssignment);
            var avaloniaPriorityExpression = BuildAvaloniaPriorityExpression(propertyElementAssignment);
            string assignmentTemplate;
            if (string.IsNullOrWhiteSpace(avaloniaPropertyExpression))
            {
                if (TryBuildSpecialClrSetterInvocation(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyOwnerTypeName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        TopDownAttachValueToken,
                        out var specialTopDownSetterInvocation))
                {
                    assignmentTemplate = specialTopDownSetterInvocation;
                }
                else if (TryBuildDirectClrPropertyAssignment(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        TopDownAttachValueToken,
                        out var topDownClrAssignment))
                {
                    assignmentTemplate = topDownClrAssignment;
                }
                else
                {
                    assignmentTemplate = $"{variableName}.{propertyElementAssignment.PropertyName} = {TopDownAttachValueToken};";
                }
            }
            else if (propertyElementAssignment.ObjectValues[0].IsBindingObjectNode)
            {
                var bindingValueExpression = string.IsNullOrWhiteSpace(nameScopeReference)
                    ? TopDownAttachValueToken
                    : $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.AttachBindingNameScope({TopDownAttachValueToken}, {nameScopeReference})";
                assignmentTemplate =
                    $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ApplyBinding({variableName}, {avaloniaPropertyExpression}, {bindingValueExpression}, global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ResolveBindingAnchor({variableName}, {nodeParentStackExpression}));";
            }
            else if (string.IsNullOrWhiteSpace(avaloniaPriorityExpression))
            {
                assignmentTemplate =
                    $"{variableName}.SetValue({avaloniaPropertyExpression}, {WrapSetValueExpression(TopDownAttachValueToken, variableName, avaloniaPropertyExpression)});";
            }
            else
            {
                assignmentTemplate =
                    $"{variableName}.SetValue({avaloniaPropertyExpression}, {WrapSetValueExpression(TopDownAttachValueToken, variableName, avaloniaPropertyExpression)}, {avaloniaPriorityExpression});";
            }

            var assignedNode = propertyElementAssignment.ObjectValues[0];
            var assignedValue = EmitNode(
                assignedNode,
                sourceBuilder,
                ref nodeCounter,
                indent,
                rootReference,
                namedFieldMap,
                emitNameScopeRegistration,
                nameScopeReference,
                serviceProviderReference: serviceProviderReference,
                topDownAttachmentTemplate: assignmentTemplate,
                baseUriExpression: baseUriExpression,
                parentStackReferences: nodeParentStackReferences,
                intermediateRootReference: intermediateRootReference,
                emitDebugLineDirectives: emitDebugLineDirectives,
                lineDirectiveFilePath: lineDirectiveFilePath);
            if (assignedNode.UseTopDownInitialization)
            {
                continue;
            }

            assignedValue = BuildAttachedNodeValueExpression(
                assignedNode,
                assignedValue,
                serviceProviderReference,
                rootReference,
                intermediateRootReference,
                baseUriExpression,
                BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, assignedValue)));

            if (string.IsNullOrWhiteSpace(avaloniaPropertyExpression))
            {
                if (TryBuildSpecialClrSetterInvocation(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyOwnerTypeName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        assignedValue,
                        out var specialClrSetterInvocation))
                {
                    EmitStatementAt(
                        specialClrSetterInvocation,
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else if (TryBuildDirectClrPropertyAssignment(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        assignedValue,
                        out var directClrAssignment))
                {
                    EmitStatementAt(
                        directClrAssignment,
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.{propertyElementAssignment.PropertyName} = {assignedValue};",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
            }
            else
            {
                if (assignedNode.IsBindingObjectNode)
                {
                    var bindingValueExpression = string.IsNullOrWhiteSpace(nameScopeReference)
                        ? assignedValue
                        : $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.AttachBindingNameScope({assignedValue}, {nameScopeReference})";
                    EmitStatementAt(
                        $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ApplyBinding({variableName}, {avaloniaPropertyExpression}, {bindingValueExpression}, global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ResolveBindingAnchor({variableName}, {nodeParentStackExpression}));",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else if (string.IsNullOrWhiteSpace(avaloniaPriorityExpression))
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpression}, {WrapSetValueExpression(assignedValue, variableName, avaloniaPropertyExpression)});",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpression}, {WrapSetValueExpression(assignedValue, variableName, avaloniaPropertyExpression)}, {avaloniaPriorityExpression});",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
            }
        }

        var contentNode = TryGetTemplateContentNode(node);
        var templateContentPropertyName = string.IsNullOrWhiteSpace(node.ContentPropertyName)
            ? "Content"
            : node.ContentPropertyName!;
        if (contentNode is not null)
        {
            var factoryIndent = indent + "    ";
            var templateScopeName = "__templateScope" + nodeCounter.ToString();
            var templateProviderName = "__templateProvider" + nodeCounter.ToString();
            sourceBuilder.AppendLine(
                $"{indent}{variableName}.{templateContentPropertyName} = (global::System.Func<global::System.IServiceProvider?, object?>)(__templateServiceProvider =>");
            sourceBuilder.AppendLine($"{indent}{{");
            sourceBuilder.AppendLine(
                $"{factoryIndent}var {templateScopeName} = global::XamlToCSharpGenerator.Runtime.SourceGenDeferredServiceProviderFactory.CreateTemplateNameScope(__templateServiceProvider);");
            sourceBuilder.AppendLine(
                $"{factoryIndent}var {templateProviderName} = global::XamlToCSharpGenerator.Runtime.SourceGenDeferredServiceProviderFactory.CreateDeferredTemplateServiceProvider(__templateServiceProvider, {rootReference}, {templateScopeName});");
            var templateRoot = EmitNode(
                contentNode,
                sourceBuilder,
                ref nodeCounter,
                factoryIndent,
                rootReference,
                EmptyNamedFieldMap,
                emitNameScopeRegistration: true,
                nameScopeReference: templateScopeName,
                completeNameScopeOnNodeCompletion: true,
                serviceProviderReference: templateProviderName,
                baseUriExpression: baseUriExpression,
                parentStackReferences: nodeParentStackReferences,
                intermediateRootReference: variableName,
                emitDebugLineDirectives: emitDebugLineDirectives,
                lineDirectiveFilePath: lineDirectiveFilePath);
            sourceBuilder.AppendLine($"{factoryIndent}if ({templateRoot} is global::Avalonia.StyledElement __templateStyledElement)");
            sourceBuilder.AppendLine($"{factoryIndent}{{");
            sourceBuilder.AppendLine(
                $"{factoryIndent}    __TrySetNameScope(__templateStyledElement, {templateScopeName});");
            sourceBuilder.AppendLine($"{factoryIndent}}}");
            sourceBuilder.AppendLine(
                $"{factoryIndent}return new global::Avalonia.Controls.Templates.TemplateResult<global::Avalonia.Controls.Control>((global::Avalonia.Controls.Control){templateRoot}, {templateScopeName});");
            sourceBuilder.AppendLine($"{indent}}});");
        }

        if (completeNameScopeOnNodeCompletion &&
            !string.IsNullOrWhiteSpace(nameScopeReference))
        {
            EmitNodeStatement($"__TryCompleteNameScope({nameScopeReference});");
        }

        EmitNodeStatement($"__EndInit({variableName});");
        return true;
    }

    private static void EmitEventSubscription(
        StringBuilder sourceBuilder,
        string indent,
        string variableName,
        string rootReference,
        ResolvedEventSubscription eventSubscription,
        bool emitDebugLineDirectives,
        string lineDirectiveFilePath)
    {
        if (eventSubscription.Kind == ResolvedEventSubscriptionKind.RoutedEvent &&
            !string.IsNullOrWhiteSpace(eventSubscription.RoutedEventOwnerTypeName) &&
            !string.IsNullOrWhiteSpace(eventSubscription.RoutedEventFieldName))
        {
            var routedEventReference = eventSubscription.RoutedEventOwnerTypeName + "." + eventSubscription.RoutedEventFieldName;
            var handlerExpression = string.IsNullOrWhiteSpace(eventSubscription.RoutedEventHandlerTypeName)
                ? rootReference + "." + eventSubscription.HandlerMethodName
                : "(" + eventSubscription.RoutedEventHandlerTypeName + ")" + rootReference + "." + eventSubscription.HandlerMethodName;
            AppendSourceMappedLine(
                sourceBuilder,
                $"{indent}{variableName}.RemoveHandler({routedEventReference}, {handlerExpression});",
                emitDebugLineDirectives,
                lineDirectiveFilePath,
                eventSubscription.Line,
                eventSubscription.Column);
            AppendSourceMappedLine(
                sourceBuilder,
                $"{indent}{variableName}.AddHandler({routedEventReference}, {handlerExpression});",
                emitDebugLineDirectives,
                lineDirectiveFilePath,
                eventSubscription.Line,
                eventSubscription.Column);
            return;
        }

        AppendSourceMappedLine(
            sourceBuilder,
            $"{indent}{variableName}.{eventSubscription.EventName} -= {rootReference}.{eventSubscription.HandlerMethodName};",
            emitDebugLineDirectives,
            lineDirectiveFilePath,
            eventSubscription.Line,
            eventSubscription.Column);
        AppendSourceMappedLine(
            sourceBuilder,
            $"{indent}{variableName}.{eventSubscription.EventName} += {rootReference}.{eventSubscription.HandlerMethodName};",
            emitDebugLineDirectives,
            lineDirectiveFilePath,
            eventSubscription.Line,
            eventSubscription.Column);
    }

    private static bool TryEmitDictionaryMergePropertyElement(
        ResolvedPropertyElementAssignment propertyElementAssignment,
        string variableName,
        StringBuilder sourceBuilder,
        ref int nodeCounter,
        string indent,
        string rootReference,
        IReadOnlyDictionary<string, string> namedFieldMap,
        bool emitNameScopeRegistration,
        string? nameScopeReference,
        string serviceProviderReference,
        string baseUriExpression,
        ImmutableArray<string> parentStackReferences,
        string intermediateRootReference,
        bool emitDebugLineDirectives,
        string lineDirectiveFilePath)
    {
        AppendSourceMappedLine(
            sourceBuilder,
            $"{indent}if (__replaceExistingCollections) __TryClearDictionaryEntries({variableName}.{propertyElementAssignment.PropertyName});",
            emitDebugLineDirectives,
            lineDirectiveFilePath,
            propertyElementAssignment.Line,
            propertyElementAssignment.Column);

        if (propertyElementAssignment.ObjectValues.Length == 0)
        {
            return true;
        }

        var hasKeyedObjectValues = true;
        foreach (var objectValue in propertyElementAssignment.ObjectValues)
        {
            if (string.IsNullOrWhiteSpace(objectValue.KeyExpression))
            {
                hasKeyedObjectValues = false;
                break;
            }
        }

        if (hasKeyedObjectValues)
        {
            foreach (var keyedObjectValue in propertyElementAssignment.ObjectValues)
            {
                var dictionaryKeyExpression = BuildDictionaryKeyExpression(
                    propertyElementAssignment.PropertyName,
                    keyedObjectValue.KeyExpression!);
                var keyedChildVariable = EmitNode(
                    keyedObjectValue,
                    sourceBuilder,
                    ref nodeCounter,
                    indent,
                    rootReference,
                    namedFieldMap,
                    emitNameScopeRegistration,
                    nameScopeReference,
                    serviceProviderReference: serviceProviderReference,
                    topDownAttachmentTemplate: $"__TryAddToDictionary({variableName}.{propertyElementAssignment.PropertyName}, {dictionaryKeyExpression}, {TopDownAttachValueToken});",
                    baseUriExpression: baseUriExpression,
                    parentStackReferences: parentStackReferences,
                    intermediateRootReference: intermediateRootReference,
                    emitDebugLineDirectives: emitDebugLineDirectives,
                    lineDirectiveFilePath: lineDirectiveFilePath);
                if (!keyedObjectValue.UseTopDownInitialization)
                {
                    var keyedChildValueExpression = BuildAttachedNodeValueExpression(
                        keyedObjectValue,
                        keyedChildVariable,
                        serviceProviderReference,
                        rootReference,
                        intermediateRootReference,
                        baseUriExpression,
                        BuildParentStackExpression(ExtendParentStack(parentStackReferences, keyedChildVariable)));
                    AppendSourceMappedLine(
                        sourceBuilder,
                        $"{indent}__TryAddToDictionary({variableName}.{propertyElementAssignment.PropertyName}, {dictionaryKeyExpression}, {keyedChildValueExpression});",
                        emitDebugLineDirectives,
                        lineDirectiveFilePath,
                        keyedObjectValue.Line,
                        keyedObjectValue.Column);
                }
            }

            return true;
        }

        var valueNode = propertyElementAssignment.ObjectValues[0];
        if (valueNode.ChildAttachmentMode == ResolvedChildAttachmentMode.DictionaryAdd)
        {
            foreach (var child in valueNode.Children)
            {
                if (string.IsNullOrWhiteSpace(child.KeyExpression))
                {
                    continue;
                }

                var dictionaryKeyExpression = BuildDictionaryKeyExpression(
                    propertyElementAssignment.PropertyName,
                    child.KeyExpression!);
                var childVariable = EmitNode(
                    child,
                    sourceBuilder,
                    ref nodeCounter,
                    indent,
                    rootReference,
                    namedFieldMap,
                    emitNameScopeRegistration,
                    nameScopeReference,
                    serviceProviderReference: serviceProviderReference,
                    topDownAttachmentTemplate: $"__TryAddToDictionary({variableName}.{propertyElementAssignment.PropertyName}, {dictionaryKeyExpression}, {TopDownAttachValueToken});",
                    baseUriExpression: baseUriExpression,
                    parentStackReferences: parentStackReferences,
                    intermediateRootReference: intermediateRootReference,
                    emitDebugLineDirectives: emitDebugLineDirectives,
                    lineDirectiveFilePath: lineDirectiveFilePath);
                if (!child.UseTopDownInitialization)
                {
                    var childValueExpression = BuildAttachedNodeValueExpression(
                        child,
                        childVariable,
                        serviceProviderReference,
                        rootReference,
                        intermediateRootReference,
                        baseUriExpression,
                        BuildParentStackExpression(ExtendParentStack(parentStackReferences, childVariable)));
                    AppendSourceMappedLine(
                        sourceBuilder,
                        $"{indent}__TryAddToDictionary({variableName}.{propertyElementAssignment.PropertyName}, {dictionaryKeyExpression}, {childValueExpression});",
                        emitDebugLineDirectives,
                        lineDirectiveFilePath,
                        child.Line,
                        child.Column);
                }
            }

            return true;
        }

        var assignedValue = EmitNode(
            valueNode,
            sourceBuilder,
            ref nodeCounter,
            indent,
            rootReference,
            namedFieldMap,
            emitNameScopeRegistration,
            nameScopeReference,
            serviceProviderReference: serviceProviderReference,
            baseUriExpression: baseUriExpression,
            parentStackReferences: parentStackReferences,
            intermediateRootReference: intermediateRootReference,
            emitDebugLineDirectives: emitDebugLineDirectives,
            lineDirectiveFilePath: lineDirectiveFilePath);
        AppendSourceMappedLine(
            sourceBuilder,
            $"{indent}__TryMergeDictionary({variableName}.{propertyElementAssignment.PropertyName}, {assignedValue});",
            emitDebugLineDirectives,
            lineDirectiveFilePath,
            propertyElementAssignment.Line,
            propertyElementAssignment.Column);
        return true;
    }

    private static void AppendSourceMappedLine(
        StringBuilder sourceBuilder,
        string lineText,
        bool emitDebugLineDirectives,
        string lineDirectiveFilePath,
        int line,
        int column)
    {
        if (!emitDebugLineDirectives ||
            string.IsNullOrWhiteSpace(lineDirectiveFilePath))
        {
            sourceBuilder.AppendLine(lineText);
            return;
        }

        var normalizedLine = Math.Max(1, line);
        var normalizedColumn = Math.Max(1, column);
        var indent = ExtractLeadingIndent(lineText);
        sourceBuilder.AppendLine($"{indent}// AXSG:XAML {normalizedLine}:{normalizedColumn}");
        sourceBuilder.AppendLine($"{indent}#line {normalizedLine} \"{EscapeLineDirectivePath(lineDirectiveFilePath)}\"");
        sourceBuilder.AppendLine(lineText);
        sourceBuilder.AppendLine($"{indent}#line default");
        sourceBuilder.AppendLine($"{indent}#line hidden");
    }

    private static string NormalizeLineDirectivePath(string? filePath)
    {
        if (string.IsNullOrWhiteSpace(filePath))
        {
            return string.Empty;
        }

        return filePath!.Replace('\\', '/');
    }

    private static string EscapeLineDirectivePath(string filePath)
    {
        return filePath.Replace("\"", "\\\"");
    }

    private static string ExtractLeadingIndent(string text)
    {
        var index = 0;
        while (index < text.Length &&
               (text[index] == ' ' || text[index] == '\t'))
        {
            index++;
        }

        return text.Substring(0, index);
    }

    private static ResolvedObjectNode? TryGetTemplateContentNode(ResolvedObjectNode node)
    {
        foreach (var assignment in node.PropertyElementAssignments)
        {
            if (IsContentPropertyName(assignment.PropertyName) &&
                assignment.ObjectValues.Length > 0)
            {
                return assignment.ObjectValues[0];
            }
        }

        if (node.Children.Length > 0)
        {
            return node.Children[0];
        }

        return null;
    }

    private static bool IsDeferredTemplateNode(ResolvedObjectNode node)
    {
        return node.TypeName is "global::Avalonia.Markup.Xaml.Templates.Template"
            or "global::Avalonia.Markup.Xaml.Templates.DataTemplate"
            or "global::Avalonia.Markup.Xaml.Templates.TreeDataTemplate"
            or "global::Avalonia.Markup.Xaml.Templates.ControlTemplate"
            or "global::Avalonia.Markup.Xaml.Templates.FocusAdornerTemplate"
            or "global::Avalonia.Markup.Xaml.Templates.ItemsPanelTemplate";
    }

