            hotReloadClrPropertyMembers = BuildRootHotReloadClrPropertyMembers(viewModel.RootObject, namedFieldMap);
            hotReloadAvaloniaPropertyMembers = BuildRootHotReloadAvaloniaPropertyMembers(viewModel.RootObject);
            hotReloadRootEventSubscriptions = BuildRootHotReloadEventSubscriptions(viewModel.RootObject);
