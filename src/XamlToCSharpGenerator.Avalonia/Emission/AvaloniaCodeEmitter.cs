public sealed class AvaloniaCodeEmitter : IXamlCodeEmitter
{
    private const string TopDownAttachValueToken = "__AXSG_VALUE__";
    private const string MarkupContextServiceProviderToken = "__AXSG_CTX_SERVICE_PROVIDER__";
    private const string MarkupContextRootObjectToken = "__AXSG_CTX_ROOT_OBJECT__";
    private const string MarkupContextIntermediateRootObjectToken = "__AXSG_CTX_INTERMEDIATE_ROOT_OBJECT__";
    private const string MarkupContextTargetObjectToken = "__AXSG_CTX_TARGET_OBJECT__";
    private const string MarkupContextTargetPropertyToken = "__AXSG_CTX_TARGET_PROPERTY__";
    private const string MarkupContextBaseUriToken = "__AXSG_CTX_BASE_URI__";
    private const string MarkupContextParentStackToken = "__AXSG_CTX_PARENT_STACK__";

    private static readonly IReadOnlyDictionary<string, string> EmptyNamedFieldMap =
        new Dictionary<string, string>(StringComparer.Ordinal);
    private static readonly IReadOnlyDictionary<string, string> EmptyGeneratedMethodNameMap =
        new Dictionary<string, string>(StringComparer.Ordinal);

    public (string HintName, string Source) Emit(ResolvedViewModel viewModel)
    {
        var document = viewModel.Document;
        var namespaceName = document.ClassNamespace;
        var className = document.ClassName;
        var escapedUri = Escape(viewModel.BuildUri);
        var hotDesignDocumentRoleExpression = BuildHotDesignDocumentRoleExpression(viewModel);
        var hotDesignArtifactKindExpression = BuildHotDesignArtifactKindExpression(viewModel);
        var hotDesignScopeHintsExpression = BuildHotDesignScopeHintsExpression(viewModel);
        var emitReloadStateTracking = viewModel.Document.IsClassBacked && (viewModel.EnableHotReload || viewModel.EnableHotDesign);
        var emitDebugLineDirectives = viewModel.CreateSourceInfo;
        var lineDirectiveFilePath = NormalizeLineDirectivePath(viewModel.Document.FilePath);
        var namedFieldMap = BuildNamedFieldMap(viewModel);
        var knownTypeNames = CollectKnownTypeNames(viewModel);
        var hotReloadCollectionMembers = ImmutableArray<string>.Empty;
        var hotReloadClrPropertyMembers = ImmutableArray<string>.Empty;
        var hotReloadAvaloniaPropertyMembers = ImmutableArray<string>.Empty;
        var hotReloadRootEventSubscriptions = ImmutableArray<ResolvedEventSubscription>.Empty;
        var hotReloadClearsRootCollection = false;
        var eventBindingDefinitions = viewModel.Document.IsClassBacked
            ? BuildEventBindingDefinitions(viewModel.RootObject)
            : ImmutableArray<ResolvedEventBindingDefinition>.Empty;
        var emittedEventBindingMethodNames = eventBindingDefinitions.IsDefaultOrEmpty
            ? EmptyGeneratedMethodNameMap
            : BuildStableEventBindingMethodNameMap(eventBindingDefinitions);

        if (emitReloadStateTracking)
        {
            hotReloadCollectionMembers = BuildRootHotReloadCollectionMembers(viewModel.RootObject);
            hotReloadClrPropertyMembers = BuildRootHotReloadClrPropertyMembers(viewModel.RootObject, namedFieldMap);
            hotReloadAvaloniaPropertyMembers = BuildRootHotReloadAvaloniaPropertyMembers(viewModel.RootObject);
            hotReloadRootEventSubscriptions = BuildRootHotReloadEventSubscriptions(viewModel.RootObject);
            hotReloadClearsRootCollection = ShouldClearRootSelfCollection(viewModel.RootObject);
        }

        var sourceBuilder = new StringBuilder(EstimateSourceCapacity(viewModel));
        sourceBuilder.AppendLine("// <auto-generated />");
        if (viewModel.PassExecutionTrace.Length > 0)
        {
            foreach (var traceEntry in viewModel.PassExecutionTrace)
            {
                sourceBuilder.AppendLine("// pass: " + EscapeComment(traceEntry));
            }
        }
        sourceBuilder.AppendLine("#nullable enable annotations");
        sourceBuilder.AppendLine("#nullable disable warnings");
        sourceBuilder.AppendLine("using global::Avalonia.Controls;");
        sourceBuilder.AppendLine("using global::Avalonia.Markup.Xaml;");
        sourceBuilder.AppendLine("using global::System.Runtime.CompilerServices;");
        sourceBuilder.AppendLine("using __AXSGObjectGraph = global::XamlToCSharpGenerator.Runtime.SourceGenObjectGraphRuntimeHelpers;");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine($"namespace {namespaceName}");
        sourceBuilder.AppendLine("{");
        var classKeyword = viewModel.Document.IsClassBacked ? "partial class" : "static class";
        sourceBuilder.AppendLine($"    {viewModel.ClassModifier} {classKeyword} {className}");
        sourceBuilder.AppendLine("    {");

        if (viewModel.Document.IsClassBacked)
        {
            foreach (var namedElement in viewModel.NamedElements)
            {
                sourceBuilder.AppendLine($"        {namedElement.FieldModifier} {namedElement.TypeName}? {SanitizeIdentifier(namedElement.Name)};");
            }
        }

        sourceBuilder.AppendLine($"        private const string __SourceGenDocumentUri = \"{escapedUri}\";");

        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine("        [ModuleInitializer]");
        sourceBuilder.AppendLine("        internal static void __RegisterXamlSourceGenArtifacts()");
        sourceBuilder.AppendLine("        {");
        sourceBuilder.AppendLine(
            $"            global::XamlToCSharpGenerator.Runtime.SourceGenArtifactRegistryRuntime.ResetDocumentRegistries(\"{escapedUri}\");");
        if (knownTypeNames.Length > 0)
        {
            sourceBuilder.AppendLine(
                $"            global::XamlToCSharpGenerator.Runtime.SourceGenKnownTypeRegistry.RegisterTypes({BuildTypeofArgumentListExpression(knownTypeNames)});");
        }
        sourceBuilder.AppendLine(
            $"            global::XamlToCSharpGenerator.Runtime.XamlSourceGenTypeUriRegistry.Register(typeof({className}), \"{escapedUri}\");");
        sourceBuilder.AppendLine(
            $"            global::XamlToCSharpGenerator.Runtime.XamlSourceGenArtifactRefreshRegistry.Register(typeof({className}), __RegisterXamlSourceGenArtifacts);");
        sourceBuilder.AppendLine($"            global::XamlToCSharpGenerator.Runtime.XamlSourceGenRegistry.Register(\"{escapedUri}\", static __serviceProvider =>");
        sourceBuilder.AppendLine("            {");
            sourceBuilder.AppendLine("                var __instance = __CreateRootInstance(__serviceProvider);");
            sourceBuilder.AppendLine("                __PopulateGeneratedObjectGraph(__instance, __serviceProvider);");
            sourceBuilder.AppendLine("                return __instance;");
        sourceBuilder.AppendLine("            });");

        if (viewModel.CreateSourceInfo)
        {
            EmitSourceInfoRegistrations(viewModel, sourceBuilder, escapedUri);
        }

        foreach (var resource in viewModel.Resources)
        {
            sourceBuilder.AppendLine(
                $"            global::XamlToCSharpGenerator.Runtime.XamlResourceRegistry.Register(new global::XamlToCSharpGenerator.Runtime.SourceGenResourceDescriptor(\"{escapedUri}\", \"{Escape(resource.Key)}\", \"{Escape(resource.TypeName)}\", \"{Escape(resource.RawXaml)}\"));");
        }

        foreach (var template in viewModel.Templates)
        {
            sourceBuilder.AppendLine(
                $"            global::XamlToCSharpGenerator.Runtime.XamlTemplateRegistry.Register(new global::XamlToCSharpGenerator.Runtime.SourceGenTemplateDescriptor(\"{escapedUri}\", \"{Escape(template.Kind)}\", {ToQuotedOrNull(template.Key)}, {ToQuotedOrNull(template.TargetTypeName)}, {ToQuotedOrNull(template.DataType)}, \"{Escape(template.RawXaml)}\"));");
        }

        foreach (var style in viewModel.Styles)
        {
            sourceBuilder.AppendLine(
                $"            global::XamlToCSharpGenerator.Runtime.XamlStyleRegistry.Register(new global::XamlToCSharpGenerator.Runtime.SourceGenStyleDescriptor(\"{escapedUri}\", {ToQuotedOrNull(style.Key)}, \"{Escape(style.Selector)}\", {ToQuotedOrNull(style.TargetTypeName)}, \"{Escape(style.RawXaml)}\"));");
        }

        for (var controlThemeIndex = 0; controlThemeIndex < viewModel.ControlThemes.Length; controlThemeIndex++)
        {
            var controlTheme = viewModel.ControlThemes[controlThemeIndex];
            sourceBuilder.AppendLine(
                $"            global::XamlToCSharpGenerator.Runtime.XamlControlThemeRegistry.Register(new global::XamlToCSharpGenerator.Runtime.SourceGenControlThemeDescriptor(\"{escapedUri}\", {ToQuotedOrNull(controlTheme.Key)}, {ToQuotedOrNull(controlTheme.TargetTypeName)}, {ToQuotedOrNull(controlTheme.BasedOn)}, {ToQuotedOrNull(controlTheme.ThemeVariant)}, \"{Escape(controlTheme.RawXaml)}\", Factory: static () => __BuildGeneratedControlTheme({controlThemeIndex})));");
        }

        foreach (var include in viewModel.Includes)
        {
            sourceBuilder.AppendLine(
                $"            global::XamlToCSharpGenerator.Runtime.XamlIncludeRegistry.Register(new global::XamlToCSharpGenerator.Runtime.SourceGenIncludeDescriptor(\"{escapedUri}\", \"{Escape(include.Kind)}\", \"{Escape(include.Source)}\", \"{Escape(include.MergeTarget)}\", {BoolLiteral(include.IsAbsoluteUri)}, \"{Escape(include.RawXaml)}\"));");

            if (!string.IsNullOrWhiteSpace(include.ResolvedSourceUri) && include.IsProjectLocal)
            {
                sourceBuilder.AppendLine(
                    $"            global::XamlToCSharpGenerator.Runtime.XamlIncludeGraphRegistry.Register(\"{escapedUri}\", \"{Escape(include.ResolvedSourceUri!)}\", \"{Escape(include.MergeTarget)}\");");
            }
        }

        for (var index = 0; index < viewModel.CompiledBindings.Length; index++)
        {
            var compiledBinding = viewModel.CompiledBindings[index];
            sourceBuilder.AppendLine(
                $"            global::XamlToCSharpGenerator.Runtime.XamlCompiledBindingRegistry.Register(new global::XamlToCSharpGenerator.Runtime.SourceGenCompiledBindingDescriptor(\"{escapedUri}\", \"{Escape(compiledBinding.TargetTypeName)}\", \"{Escape(compiledBinding.TargetPropertyName)}\", \"{Escape(compiledBinding.Path)}\", \"{Escape(compiledBinding.SourceTypeName)}\", static source => __CompiledBindingAccessor({index}, source)));");
        }

        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine(
            $"        internal static void __PopulateGeneratedObjectGraph({viewModel.RootObject.TypeName} __root, global::System.IServiceProvider? __serviceProvider = null, bool __replaceExistingCollections = false)");
        sourceBuilder.AppendLine("        {");
        if (viewModel.EmitNameScopeRegistration)
        {
            sourceBuilder.AppendLine("            var __nameScope = new global::Avalonia.Controls.NameScope();");
            sourceBuilder.AppendLine("            var __rootObject = (object)__root;");
            sourceBuilder.AppendLine("            if (__rootObject is global::Avalonia.StyledElement __rootStyledElement)");
            sourceBuilder.AppendLine("            {");
            sourceBuilder.AppendLine("                __AXSGObjectGraph.TrySetNameScope(__rootStyledElement, __nameScope);");
            sourceBuilder.AppendLine("            }");
        }

        var nodeCounter = 0;
        EmitNode(
            viewModel.RootObject,
            sourceBuilder,
            ref nodeCounter,
            "            ",
            "__root",
            namedFieldMap,
            emittedEventBindingMethodNames,
            viewModel.EmitNameScopeRegistration,
            viewModel.EmitNameScopeRegistration ? "__nameScope" : null,
            "__root",
            topDownAttachmentTemplate: null,
            completeNameScopeOnNodeCompletion: viewModel.EmitNameScopeRegistration,
            serviceProviderReference: "__serviceProvider",
            baseUriExpression: "\"" + escapedUri + "\"",
            parentStackReferences: ImmutableArray<string>.Empty,
            intermediateRootReference: "__root",
            emitDebugLineDirectives: emitDebugLineDirectives,
            lineDirectiveFilePath: lineDirectiveFilePath);

        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine(
            $"        internal static {viewModel.RootObject.TypeName} __CreateRootInstance(global::System.IServiceProvider? __serviceProvider)");
        sourceBuilder.AppendLine("        {");
        sourceBuilder.AppendLine($"            return {BuildObjectCreationExpression(viewModel.RootObject, "__serviceProvider", "\"" + escapedUri + "\"")};");
        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine($"        internal static {viewModel.RootObject.TypeName} __BuildGeneratedObjectGraph()");
        sourceBuilder.AppendLine("        {");
        sourceBuilder.AppendLine("            var __root = __CreateRootInstance(null);");
        sourceBuilder.AppendLine("            __PopulateGeneratedObjectGraph(__root, null);");
        sourceBuilder.AppendLine("            return __root;");

        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();

        if (viewModel.Document.IsClassBacked && (viewModel.EnableHotReload || viewModel.EnableHotDesign))
        {
            sourceBuilder.AppendLine(
                "        private static global::XamlToCSharpGenerator.Runtime.SourceGenHotReloadCleanupDescriptor[] __GetSourceGenHotReloadCollectionCleanupDescriptors()");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine(
                $"            return {BuildHotReloadCollectionCleanupDescriptorArrayExpression(hotReloadCollectionMembers, viewModel.RootObject.TypeName)};");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
            sourceBuilder.AppendLine(
                "        private static global::XamlToCSharpGenerator.Runtime.SourceGenHotReloadCleanupDescriptor[] __GetSourceGenHotReloadClrPropertyCleanupDescriptors()");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine(
                $"            return {BuildHotReloadClrPropertyCleanupDescriptorArrayExpression(hotReloadClrPropertyMembers, viewModel.RootObject.TypeName)};");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
            sourceBuilder.AppendLine(
                "        private static global::XamlToCSharpGenerator.Runtime.SourceGenHotReloadCleanupDescriptor[] __GetSourceGenHotReloadAvaloniaPropertyCleanupDescriptors()");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine(
                $"            return {BuildHotReloadAvaloniaPropertyCleanupDescriptorArrayExpression(hotReloadAvaloniaPropertyMembers)};");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
            sourceBuilder.AppendLine(
                "        private static global::XamlToCSharpGenerator.Runtime.SourceGenHotReloadCleanupDescriptor[] __GetSourceGenHotReloadRootEventCleanupDescriptors()");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine(
                $"            return {BuildHotReloadEventCleanupDescriptorArrayExpression(hotReloadRootEventSubscriptions, viewModel.RootObject.TypeName, emittedEventBindingMethodNames)};");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
            sourceBuilder.AppendLine("        private static void __TrackAndReconcileSourceGenHotReloadState(object __instance)");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine("            global::XamlToCSharpGenerator.Runtime.XamlSourceGenHotReloadStateTracker.Reconcile(");
            sourceBuilder.AppendLine("                __instance,");
            sourceBuilder.AppendLine("                __GetSourceGenHotReloadCollectionCleanupDescriptors(),");
            sourceBuilder.AppendLine("                __GetSourceGenHotReloadClrPropertyCleanupDescriptors(),");
            sourceBuilder.AppendLine("                __GetSourceGenHotReloadAvaloniaPropertyCleanupDescriptors(),");
            sourceBuilder.AppendLine($"                {BoolLiteral(hotReloadClearsRootCollection)},");
            sourceBuilder.AppendLine("                __GetSourceGenHotReloadRootEventCleanupDescriptors());");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
            sourceBuilder.AppendLine("        internal void __ApplySourceGenHotReload()");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine("            __RegisterXamlSourceGenArtifacts();");
            sourceBuilder.AppendLine("            __TrackAndReconcileSourceGenHotReloadState(this);");
            sourceBuilder.AppendLine("            __PopulateGeneratedObjectGraph(this, null, true);");
            sourceBuilder.AppendLine("            var __self = (object)this;");
            sourceBuilder.AppendLine("            if (__self is global::Avalonia.Layout.Layoutable __layoutable)");
            sourceBuilder.AppendLine("            {");
            sourceBuilder.AppendLine("                __layoutable.InvalidateMeasure();");
            sourceBuilder.AppendLine("                __layoutable.InvalidateArrange();");
            sourceBuilder.AppendLine("            }");
            sourceBuilder.AppendLine();
            sourceBuilder.AppendLine("            if (__self is global::Avalonia.Visual __visual)");
            sourceBuilder.AppendLine("            {");
            sourceBuilder.AppendLine("                __visual.InvalidateVisual();");
            sourceBuilder.AppendLine("            }");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
        }

        sourceBuilder.AppendLine("        private static object? __CompiledBindingAccessor(int __index, object __source)");
        sourceBuilder.AppendLine("        {");
        sourceBuilder.AppendLine("            switch (__index)");
        sourceBuilder.AppendLine("            {");
        for (var index = 0; index < viewModel.CompiledBindings.Length; index++)
        {
            var compiledBinding = viewModel.CompiledBindings[index];
            sourceBuilder.AppendLine($"                case {index}:");
            sourceBuilder.AppendLine("                {");
            sourceBuilder.AppendLine($"                    var source = ({compiledBinding.SourceTypeName})__source;");
            sourceBuilder.AppendLine($"                    return {compiledBinding.AccessorExpression};");
            sourceBuilder.AppendLine("                }");
        }
        sourceBuilder.AppendLine("                default:");
        sourceBuilder.AppendLine("                    return null;");
        sourceBuilder.AppendLine("            }");
        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();

        if (eventBindingDefinitions.Length > 0)
        {
            EmitEventBindingMethods(sourceBuilder, eventBindingDefinitions, emittedEventBindingMethodNames);
        }

        sourceBuilder.AppendLine("        private static global::Avalonia.Styling.ControlTheme __BuildGeneratedControlTheme(int __index)");
        sourceBuilder.AppendLine("        {");
        sourceBuilder.AppendLine("            switch (__index)");
        sourceBuilder.AppendLine("            {");
        for (var themeIndex = 0; themeIndex < viewModel.ControlThemes.Length; themeIndex++)
        {
            var controlTheme = viewModel.ControlThemes[themeIndex];
            sourceBuilder.AppendLine($"                case {themeIndex}:");
            sourceBuilder.AppendLine("                {");
            sourceBuilder.AppendLine("                    var __theme = new global::Avalonia.Styling.ControlTheme();");

            if (!string.IsNullOrWhiteSpace(controlTheme.TargetTypeName))
            {
                sourceBuilder.AppendLine(
                    $"                    __theme.TargetType = typeof({controlTheme.TargetTypeName});");
            }

            for (var setterIndex = 0; setterIndex < controlTheme.Setters.Length; setterIndex++)
            {
                var setter = controlTheme.Setters[setterIndex];
                var controlThemeSetterPropertyExpression = BuildAvaloniaPropertyExpression(setter);
                if (controlThemeSetterPropertyExpression is null)
                {
                    continue;
                }

                var controlThemeSetterPropertyExpressionValue = controlThemeSetterPropertyExpression.Trim();
                if (controlThemeSetterPropertyExpressionValue.Length == 0)
                {
                    continue;
                }

                var expandedSetterValueExpression = ExpandMarkupContextExpression(
                    setter.ValueExpression,
                    "null",
                    "__theme",
                    "__theme",
                    "__theme",
                    controlThemeSetterPropertyExpressionValue,
                    "\"" + escapedUri + "\"",
                    "new object[] { __theme }");

                sourceBuilder.AppendLine(
                    $"                    __theme.Setters.Add(new global::Avalonia.Styling.Setter({controlThemeSetterPropertyExpressionValue}, {expandedSetterValueExpression}));");
            }

            sourceBuilder.AppendLine("                    return __theme;");
            sourceBuilder.AppendLine("                }");
        }
        sourceBuilder.AppendLine("                default:");
        sourceBuilder.AppendLine("                    return new global::Avalonia.Styling.ControlTheme();");
        sourceBuilder.AppendLine("            }");
        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();

        if (viewModel.EmitStaticResourceResolver)
        {
            sourceBuilder.AppendLine("        private static object? __ResolveStaticResource(object? anchor, object key)");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine(
                $"            return global::XamlToCSharpGenerator.Runtime.SourceGenStaticResourceResolver.Resolve(anchor, key, \"{escapedUri}\");");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
        }


        if (viewModel.Document.IsClassBacked)
        {
            sourceBuilder.AppendLine("        public void InitializeComponent(bool loadXaml = true)");
            sourceBuilder.AppendLine("            {");
            sourceBuilder.AppendLine("                var __loadedWithSourceGen = false;");
            sourceBuilder.AppendLine("                if (loadXaml)");
            sourceBuilder.AppendLine("                {");
            sourceBuilder.AppendLine("                    __PopulateGeneratedObjectGraph(this, null);");
            sourceBuilder.AppendLine("                    __loadedWithSourceGen = true;");
            sourceBuilder.AppendLine("                }");
            sourceBuilder.AppendLine();
            sourceBuilder.AppendLine("                if (!__loadedWithSourceGen)");
            sourceBuilder.AppendLine("                {");
            foreach (var namedElement in viewModel.NamedElements)
            {
                sourceBuilder.AppendLine(
                    $"                    {SanitizeIdentifier(namedElement.Name)} = this.FindNameScope()?.Find<{namedElement.TypeName}>(\"{Escape(namedElement.Name)}\");");
            }
            sourceBuilder.AppendLine("                }");
            sourceBuilder.AppendLine();
            if (viewModel.EnableHotReload || viewModel.EnableHotDesign)
            {
                var escapedSourcePath = Escape(viewModel.Document.FilePath);
                sourceBuilder.AppendLine("                if (__loadedWithSourceGen)");
                sourceBuilder.AppendLine("                {");
                sourceBuilder.AppendLine("                    __TrackAndReconcileSourceGenHotReloadState(this);");

                if (viewModel.EnableHotReload)
                {
                    sourceBuilder.AppendLine(
                        $"                    global::XamlToCSharpGenerator.Runtime.XamlSourceGenHotReloadManager.Register(this, static __instance => (({viewModel.RootObject.TypeName})__instance).__ApplySourceGenHotReload(), new global::XamlToCSharpGenerator.Runtime.SourceGenHotReloadRegistrationOptions");
                    sourceBuilder.AppendLine("                    {");
                    sourceBuilder.AppendLine($"                        BuildUri = \"{escapedUri}\",");
                    sourceBuilder.AppendLine($"                        SourcePath = \"{escapedSourcePath}\",");
                    sourceBuilder.AppendLine("                        CaptureState = static __instance =>");
                    sourceBuilder.AppendLine("                            __instance is global::Avalonia.StyledElement __styledElement");
                    sourceBuilder.AppendLine("                                ? __styledElement.DataContext");
                    sourceBuilder.AppendLine("                                : null,");
                    sourceBuilder.AppendLine("                        RestoreState = static (__instance, __state) =>");
                    sourceBuilder.AppendLine("                        {");
                    sourceBuilder.AppendLine("                            if (__instance is global::Avalonia.StyledElement __styledElement &&");
                    sourceBuilder.AppendLine("                                __styledElement.DataContext is null)");
                    sourceBuilder.AppendLine("                            {");
                    sourceBuilder.AppendLine("                                __styledElement.DataContext = __state;");
                    sourceBuilder.AppendLine("                            }");
                    sourceBuilder.AppendLine("                        }");
                    sourceBuilder.AppendLine("                    });");
                }

                if (viewModel.EnableHotDesign)
                {
                    sourceBuilder.AppendLine(
                        $"                    global::XamlToCSharpGenerator.Runtime.XamlSourceGenHotDesignManager.Register(this, static __instance => (({viewModel.RootObject.TypeName})__instance).__ApplySourceGenHotReload(), new global::XamlToCSharpGenerator.Runtime.SourceGenHotDesignRegistrationOptions");
                    sourceBuilder.AppendLine("                    {");
                    sourceBuilder.AppendLine($"                        BuildUri = \"{escapedUri}\",");
                    sourceBuilder.AppendLine($"                        SourcePath = \"{escapedSourcePath}\",");
                    sourceBuilder.AppendLine($"                        DocumentRole = {hotDesignDocumentRoleExpression},");
                    sourceBuilder.AppendLine($"                        ArtifactKind = {hotDesignArtifactKindExpression},");
                    sourceBuilder.AppendLine($"                        ScopeHints = {hotDesignScopeHintsExpression}");
                    sourceBuilder.AppendLine("                    });");
                }

                sourceBuilder.AppendLine("                }");
            }

            sourceBuilder.AppendLine("            }");
        }
        sourceBuilder.AppendLine("    }");
        sourceBuilder.AppendLine("}");

        var hintName = BuildHintName(viewModel);
        return (hintName, sourceBuilder.ToString());
    }

    private static int EstimateSourceCapacity(ResolvedViewModel viewModel)
    {
        var estimate = 16_384;
        estimate += viewModel.NamedElements.Length * 72;
        estimate += viewModel.Resources.Length * 220;
        estimate += viewModel.Templates.Length * 220;
        estimate += viewModel.CompiledBindings.Length * 180;
        estimate += viewModel.Styles.Length * 220;
        estimate += viewModel.ControlThemes.Length * 260;
        estimate += viewModel.Includes.Length * 180;
        estimate += viewModel.PassExecutionTrace.Length * 96;
        estimate += viewModel.HotDesignScopeHints.Length * 64;
        estimate += EstimateObjectNodeContribution(viewModel.RootObject);

        if (viewModel.Document.IsClassBacked)
        {
            estimate += 8_192;
        }

        if (estimate < 16_384)
        {
            return 16_384;
        }

        if (estimate > 4_000_000)
        {
            return 4_000_000;
        }

        return estimate;
    }

    private static int EstimateObjectNodeContribution(ResolvedObjectNode node)
    {
        var estimate = 320;
        estimate += node.PropertyAssignments.Length * 96;
        estimate += node.PropertyElementAssignments.Length * 96;
        estimate += node.EventSubscriptions.Length * 80;
        estimate += node.ChildAddInstructions.Length * 48;

        for (var childIndex = 0; childIndex < node.Children.Length; childIndex++)
        {
            estimate += EstimateObjectNodeContribution(node.Children[childIndex]);
        }

        return estimate;
    }

    private static string EmitNode(
        ResolvedObjectNode node,
        StringBuilder sourceBuilder,
        ref int nodeCounter,
        string indent,
        string rootReference,
        IReadOnlyDictionary<string, string> namedFieldMap,
        IReadOnlyDictionary<string, string> emittedEventBindingMethodNames,
        bool emitNameScopeRegistration,
        string? nameScopeReference,
        string? existingVariableName = null,
        string? topDownAttachmentTemplate = null,
        bool completeNameScopeOnNodeCompletion = false,
        string serviceProviderReference = "__serviceProvider",
        string baseUriExpression = "null",
        ImmutableArray<string> parentStackReferences = default,
        string? intermediateRootReference = null,
        bool emitDebugLineDirectives = false,
        string lineDirectiveFilePath = "")
    {
        var variableName = existingVariableName ?? ("__n" + nodeCounter.ToString());
        intermediateRootReference ??= variableName;
        var nodeParentStackReferences = ExtendParentStack(parentStackReferences, variableName);
        var nodeParentStackExpression = BuildParentStackExpression(nodeParentStackReferences);
        void EmitNodeStatement(string statement)
        {
            AppendSourceMappedLine(
                sourceBuilder,
                indent + statement,
                emitDebugLineDirectives,
                lineDirectiveFilePath,
                node.Line,
                node.Column);
        }

        void EmitStatementAt(string statement, int line, int column)
        {
            AppendSourceMappedLine(
                sourceBuilder,
                indent + statement,
                emitDebugLineDirectives,
                lineDirectiveFilePath,
                line,
                column);
        }

        string AttachBindingNameScope(string expression, ResolvedValueKind valueKind)
        {
            if (string.IsNullOrWhiteSpace(nameScopeReference) || !IsBindingValueKind(valueKind))
            {
                return expression;
            }

            return $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.AttachBindingNameScope({expression}, {nameScopeReference})";
        }

        string AttachBindingNodeNameScope(ResolvedObjectNode nodeValue, string expression)
        {
            if (string.IsNullOrWhiteSpace(nameScopeReference) || !nodeValue.IsBindingObjectNode)
            {
                return expression;
            }

            return $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.AttachBindingNameScope({expression}, {nameScopeReference})";
        }

        string WrapSetValueExpression(string expression, string targetObjectExpression, string targetPropertyExpression)
        {
            return "global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ProvideMarkupExtensionValue(" +
                   expression +
                   ", " + serviceProviderReference +
                   ", " + rootReference +
                   ", " + intermediateRootReference +
                   ", " + targetObjectExpression +
                   ", " + targetPropertyExpression +
                   ", " + baseUriExpression +
                   ", " + nodeParentStackExpression +
                   ")";
        }

        if (TryEmitDeferredTemplateNode(
                node,
                sourceBuilder,
                ref nodeCounter,
                indent,
                rootReference,
                variableName,
                namedFieldMap,
                emittedEventBindingMethodNames,
                emitNameScopeRegistration,
                nameScopeReference,
                existingVariableName is null,
                topDownAttachmentTemplate,
                completeNameScopeOnNodeCompletion,
                serviceProviderReference,
                baseUriExpression,
                nodeParentStackReferences,
                intermediateRootReference,
                emitDebugLineDirectives,
                lineDirectiveFilePath))
        {
            return variableName;
        }

        if (existingVariableName is null)
        {
            nodeCounter++;
            EmitNodeStatement($"var {variableName} = {BuildObjectCreationExpression(node, serviceProviderReference, baseUriExpression)};");
        }

        if (emitNameScopeRegistration &&
            !string.IsNullOrWhiteSpace(nameScopeReference))
        {
            var scopedElementVariable = "__scopedStyledElement" + nodeCounter.ToString();
            EmitNodeStatement($"if ((object){variableName} is global::Avalonia.StyledElement {scopedElementVariable})");
            EmitNodeStatement("{");
            EmitNodeStatement($"    __AXSGObjectGraph.TrySetNameScope({scopedElementVariable}, {nameScopeReference});");
            EmitNodeStatement("}");
        }

        EmitNodeStatement($"__AXSGObjectGraph.BeginInit({variableName});");
        if (node.ChildAttachmentMode == ResolvedChildAttachmentMode.DictionaryAdd &&
            (node.Children.Length > 0 || existingVariableName is not null))
        {
            EmitNodeStatement($"if (__replaceExistingCollections) __AXSGObjectGraph.TryClearDictionaryEntries({variableName});");
        }

        string? pendingTopDownAttachmentStatement = null;
        if (node.UseTopDownInitialization &&
            !string.IsNullOrWhiteSpace(topDownAttachmentTemplate))
        {
            var attachmentValueExpression = BuildAttachedNodeValueExpression(
                node,
                variableName,
                serviceProviderReference,
                rootReference,
                intermediateRootReference,
                baseUriExpression,
                nodeParentStackExpression);
            pendingTopDownAttachmentStatement = ExpandTopDownAttachmentTemplate(
                topDownAttachmentTemplate!,
                attachmentValueExpression);
        }

        if (!string.IsNullOrWhiteSpace(node.Name))
        {
            EmitNodeStatement(
                $"if ((object){variableName} is global::Avalonia.StyledElement) ((global::Avalonia.StyledElement)(object){variableName}).Name = \"{Escape(node.Name!)}\";");

            if (namedFieldMap.TryGetValue(node.Name!, out var fieldName))
            {
                EmitNodeStatement($"{rootReference}.{fieldName} = {variableName};");
            }

            if (emitNameScopeRegistration &&
                !string.IsNullOrWhiteSpace(nameScopeReference))
            {
                EmitNodeStatement(
                    $"{nameScopeReference}.Register(\"{Escape(node.Name!)}\", {variableName});");
            }
        }

        if (pendingTopDownAttachmentStatement is not null)
        {
            var pendingTopDownAttachmentStatementValue = pendingTopDownAttachmentStatement.Trim();
            if (pendingTopDownAttachmentStatementValue.Length > 0)
            {
                EmitNodeStatement(pendingTopDownAttachmentStatementValue);
            }
        }

        foreach (var assignment in node.PropertyAssignments)
        {
            var avaloniaPropertyExpression = BuildAvaloniaPropertyExpression(assignment);
            var avaloniaPriorityExpression = BuildAvaloniaPriorityExpression(assignment);
            var targetPropertyExpression = BuildTargetPropertyExpression(assignment);
            var bindingAnchorExpression =
                $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ResolveBindingAnchor({variableName}, {nodeParentStackExpression})";
            var valueExpression = ExpandMarkupContextExpression(
                assignment.ValueExpression,
                serviceProviderReference,
                rootReference,
                intermediateRootReference,
                variableName,
                targetPropertyExpression,
                baseUriExpression,
                nodeParentStackExpression);

            if (avaloniaPropertyExpression is null || avaloniaPropertyExpression.Trim().Length == 0)
            {
                var clrAssignmentValueExpression = AttachBindingNameScope(valueExpression, assignment.ValueKind);

                if (TryBuildSpecialClrSetterInvocation(
                        variableName,
                        assignment.PropertyName,
                        assignment.ClrPropertyOwnerTypeName,
                        assignment.ClrPropertyTypeName,
                        clrAssignmentValueExpression,
                        out var specialClrSetterInvocation))
                {
                    EmitStatementAt(specialClrSetterInvocation, assignment.Line, assignment.Column);
                }
                else if (TryBuildDirectClrPropertyAssignment(
                        variableName,
                        assignment.PropertyName,
                        assignment.ClrPropertyTypeName,
                        clrAssignmentValueExpression,
                        out var directClrAssignment))
                {
                    EmitStatementAt(directClrAssignment, assignment.Line, assignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.{assignment.PropertyName} = {clrAssignmentValueExpression};",
                        assignment.Line,
                        assignment.Column);
                }
            }
            else
            {
                var avaloniaPropertyExpressionValue = avaloniaPropertyExpression;
                if (IsBindingValueKind(assignment.ValueKind))
                {
                    var bindingExpression = AttachBindingNameScope(valueExpression, assignment.ValueKind);
                    EmitStatementAt(
                        $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ApplyBinding({variableName}, {avaloniaPropertyExpressionValue}, {bindingExpression}, {bindingAnchorExpression});",
                        assignment.Line,
                        assignment.Column);
                    continue;
                }

                if (string.IsNullOrWhiteSpace(avaloniaPriorityExpression))
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpressionValue}, {WrapSetValueExpression(valueExpression, variableName, avaloniaPropertyExpressionValue)});",
                        assignment.Line,
                        assignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpressionValue}, {WrapSetValueExpression(valueExpression, variableName, avaloniaPropertyExpressionValue)}, {avaloniaPriorityExpression});",
                        assignment.Line,
                        assignment.Column);
                }
            }
        }

        foreach (var eventSubscription in node.EventSubscriptions)
        {
            EmitEventSubscription(
                sourceBuilder,
                indent,
                variableName,
                rootReference,
                eventSubscription,
                emittedEventBindingMethodNames,
                emitDebugLineDirectives,
                lineDirectiveFilePath);
        }

        foreach (var propertyElementAssignment in node.PropertyElementAssignments)
        {
            if (propertyElementAssignment.IsCollectionAdd)
            {
                EmitStatementAt(
                    $"if (__replaceExistingCollections) __AXSGObjectGraph.TryClearCollection({variableName}.{propertyElementAssignment.PropertyName});",
                    propertyElementAssignment.Line,
                    propertyElementAssignment.Column);
                var isMergedDictionariesProperty = string.Equals(
                    propertyElementAssignment.PropertyName,
                    "MergedDictionaries",
                    StringComparison.Ordinal);
                var collectionExpression = $"{variableName}.{propertyElementAssignment.PropertyName}";
                for (var valueIndex = 0; valueIndex < propertyElementAssignment.ObjectValues.Length; valueIndex++)
                {
                    var value = propertyElementAssignment.ObjectValues[valueIndex];
                    var addInstruction = ResolveCollectionAddInstruction(
                        propertyElementAssignment.CollectionAddInstructions,
                        valueIndex,
                        node.TypeName + "." + propertyElementAssignment.PropertyName);
                    var isMergeResourceIncludeValue =
                        isMergedDictionariesProperty &&
                        IsMergeResourceIncludeNode(value);
                    var isStyleIncludeValue = IsStyleIncludeNode(value);
                    var requiresDeferredCollectionAttach =
                        isMergeResourceIncludeValue ||
                        isStyleIncludeValue;
                    var inlineAddValueToken = AttachBindingNodeNameScope(value, TopDownAttachValueToken);
                    var inlineAddTemplate = BuildCollectionAddStatement(
                        collectionExpression,
                        inlineAddValueToken,
                        addInstruction);
                    var valueVariable = EmitNode(
                        value,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emittedEventBindingMethodNames,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: requiresDeferredCollectionAttach ? null : inlineAddTemplate,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference,
                        emitDebugLineDirectives: emitDebugLineDirectives,
                        lineDirectiveFilePath: lineDirectiveFilePath);
                    if (isMergeResourceIncludeValue)
                    {
                        var mergeIncludeExpression = BuildAttachedNodeValueExpression(
                            value,
                            valueVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, valueVariable)));
                        EmitStatementAt(
                            $"if (!__AXSGObjectGraph.TryApplyMergedResourceInclude({variableName}, {mergeIncludeExpression}, __SourceGenDocumentUri)) {BuildCollectionAddStatement(collectionExpression, mergeIncludeExpression, addInstruction)}",
                            value.Line,
                            value.Column);
                        continue;
                    }

                    if (isStyleIncludeValue)
                    {
                        var styleIncludeExpression = BuildAttachedNodeValueExpression(
                            value,
                            valueVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, valueVariable)));
                        EmitStatementAt(
                            $"if (!__AXSGObjectGraph.TryApplyStyleInclude({collectionExpression}, {variableName}, {styleIncludeExpression}, __SourceGenDocumentUri)) {BuildCollectionAddStatement(collectionExpression, styleIncludeExpression, addInstruction)}",
                            value.Line,
                            value.Column);
                        continue;
                    }

                    if (!value.UseTopDownInitialization)
                    {
                        var valueExpression = BuildAttachedNodeValueExpression(
                            value,
                            valueVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, valueVariable)));
                        valueExpression = AttachBindingNodeNameScope(value, valueExpression);
                        EmitStatementAt(
                            BuildCollectionAddStatement(collectionExpression, valueExpression, addInstruction),
                            value.Line,
                            value.Column);
                    }
                }

                continue;
            }

            if (propertyElementAssignment.IsDictionaryMerge)
            {
                if (TryEmitDictionaryMergePropertyElement(
                        propertyElementAssignment,
                        variableName,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emittedEventBindingMethodNames,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference,
                        baseUriExpression,
                        nodeParentStackReferences,
                        intermediateRootReference,
                        emitDebugLineDirectives,
                        lineDirectiveFilePath))
                {
                    continue;
                }
            }

            if (propertyElementAssignment.ObjectValues.Length == 0)
            {
                continue;
            }

            var avaloniaPropertyExpression = BuildAvaloniaPropertyExpression(propertyElementAssignment);
            var avaloniaPriorityExpression = BuildAvaloniaPriorityExpression(propertyElementAssignment);
            string assignmentTemplate;
            if (avaloniaPropertyExpression is null || avaloniaPropertyExpression.Trim().Length == 0)
            {
                if (TryBuildSpecialClrSetterInvocation(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyOwnerTypeName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        TopDownAttachValueToken,
                        out var specialTopDownSetterInvocation))
                {
                    assignmentTemplate = specialTopDownSetterInvocation;
                }
                else if (TryBuildDirectClrPropertyAssignment(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        TopDownAttachValueToken,
                        out var topDownClrAssignment))
                {
                    assignmentTemplate = topDownClrAssignment;
                }
                else
                {
                    assignmentTemplate = $"{variableName}.{propertyElementAssignment.PropertyName} = {TopDownAttachValueToken};";
                }
            }
            else
            {
                var avaloniaPropertyExpressionValue = avaloniaPropertyExpression;
                if (propertyElementAssignment.ObjectValues[0].IsBindingObjectNode)
                {
                    var bindingValueExpression = string.IsNullOrWhiteSpace(nameScopeReference)
                        ? TopDownAttachValueToken
                        : $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.AttachBindingNameScope({TopDownAttachValueToken}, {nameScopeReference})";
                    assignmentTemplate =
                        $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ApplyBinding({variableName}, {avaloniaPropertyExpressionValue}, {bindingValueExpression}, global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ResolveBindingAnchor({variableName}, {nodeParentStackExpression}));";
                }
                else if (string.IsNullOrWhiteSpace(avaloniaPriorityExpression))
                {
                    assignmentTemplate =
                        $"{variableName}.SetValue({avaloniaPropertyExpressionValue}, {WrapSetValueExpression(TopDownAttachValueToken, variableName, avaloniaPropertyExpressionValue)});";
                }
                else
                {
                    assignmentTemplate =
                        $"{variableName}.SetValue({avaloniaPropertyExpressionValue}, {WrapSetValueExpression(TopDownAttachValueToken, variableName, avaloniaPropertyExpressionValue)}, {avaloniaPriorityExpression});";
                }
            }

            var assignedNode = propertyElementAssignment.ObjectValues[0];
            var assignedValue = EmitNode(
                assignedNode,
                sourceBuilder,
                ref nodeCounter,
                indent,
                rootReference,
                namedFieldMap,
                emittedEventBindingMethodNames,
                emitNameScopeRegistration,
                nameScopeReference,
                serviceProviderReference: serviceProviderReference,
                topDownAttachmentTemplate: assignmentTemplate,
                baseUriExpression: baseUriExpression,
                parentStackReferences: nodeParentStackReferences,
                intermediateRootReference: intermediateRootReference,
                emitDebugLineDirectives: emitDebugLineDirectives,
                lineDirectiveFilePath: lineDirectiveFilePath);
            if (assignedNode.UseTopDownInitialization)
            {
                continue;
            }

            assignedValue = BuildAttachedNodeValueExpression(
                assignedNode,
                assignedValue,
                serviceProviderReference,
                rootReference,
                intermediateRootReference,
                baseUriExpression,
                BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, assignedValue)));

            if (avaloniaPropertyExpression is null || avaloniaPropertyExpression.Trim().Length == 0)
            {
                if (TryBuildSpecialClrSetterInvocation(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyOwnerTypeName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        assignedValue,
                        out var specialClrSetterInvocation))
                {
                    EmitStatementAt(
                        specialClrSetterInvocation,
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else if (TryBuildDirectClrPropertyAssignment(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        assignedValue,
                        out var directClrAssignment))
                {
                    EmitStatementAt(
                        directClrAssignment,
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.{propertyElementAssignment.PropertyName} = {assignedValue};",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
            }
            else
            {
                var avaloniaPropertyExpressionValue = avaloniaPropertyExpression;
                if (assignedNode.IsBindingObjectNode)
                {
                    var bindingValueExpression = string.IsNullOrWhiteSpace(nameScopeReference)
                        ? assignedValue
                        : $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.AttachBindingNameScope({assignedValue}, {nameScopeReference})";
                    EmitStatementAt(
                        $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ApplyBinding({variableName}, {avaloniaPropertyExpressionValue}, {bindingValueExpression}, global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ResolveBindingAnchor({variableName}, {nodeParentStackExpression}));",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else if (string.IsNullOrWhiteSpace(avaloniaPriorityExpression))
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpressionValue}, {WrapSetValueExpression(assignedValue, variableName, avaloniaPropertyExpressionValue)});",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpressionValue}, {WrapSetValueExpression(assignedValue, variableName, avaloniaPropertyExpressionValue)}, {avaloniaPriorityExpression});",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
            }
        }

        switch (node.ChildAttachmentMode)
        {
            case ResolvedChildAttachmentMode.Content:
            {
                var contentPropertyName = string.IsNullOrWhiteSpace(node.ContentPropertyName)
                    ? "Content"
                    : node.ContentPropertyName!;
                TryBuildDirectClrPropertyAssignment(
                    variableName,
                    contentPropertyName,
                    null,
                    TopDownAttachValueToken,
                    out var topDownContentAssignment);
                var shouldResetImplicitContent =
                    existingVariableName is not null &&
                    string.IsNullOrWhiteSpace(topDownAttachmentTemplate);
                if (node.Children.Length > 0)
                {
                    var firstChildNode = node.Children[0];
                    var firstChild = EmitNode(
                        firstChildNode,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emittedEventBindingMethodNames,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: topDownContentAssignment,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference,
                        emitDebugLineDirectives: emitDebugLineDirectives,
                        lineDirectiveFilePath: lineDirectiveFilePath);
                    if (!firstChildNode.UseTopDownInitialization)
                    {
                        var firstChildValueExpression = BuildAttachedNodeValueExpression(
                            firstChildNode,
                            firstChild,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, firstChild)));
                        TryBuildDirectClrPropertyAssignment(
                            variableName,
                            contentPropertyName,
                            null,
                            firstChildValueExpression,
                            out var directContentAssignment);
                        EmitStatementAt(
                            directContentAssignment,
                            firstChildNode.Line,
                            firstChildNode.Column);
                    }
                }
                else if (shouldResetImplicitContent && !HasExplicitContentValue(node, contentPropertyName))
                {
                    TryBuildDirectClrPropertyAssignment(
                        variableName,
                        contentPropertyName,
                        null,
                        "default",
                        out var resetContentAssignment);
                    EmitNodeStatement(resetContentAssignment);
                }

                break;
            }
            case ResolvedChildAttachmentMode.ChildrenCollection:
            {
                if (node.Children.Length > 0 || existingVariableName is not null)
                {
                    EmitNodeStatement($"if (__replaceExistingCollections) __AXSGObjectGraph.TryClearCollection({variableName}.Children);");
                }
                for (var childIndex = 0; childIndex < node.Children.Length; childIndex++)
                {
                    var child = node.Children[childIndex];
                    var addInstruction = ResolveCollectionAddInstruction(
                        node.ChildAddInstructions,
                        childIndex,
                        node.TypeName + ".Children");
                    var inlineAddValueToken = AttachBindingNodeNameScope(child, TopDownAttachValueToken);
                    var inlineAddTemplate = BuildCollectionAddStatement(
                        $"{variableName}.Children",
                        inlineAddValueToken,
                        addInstruction);
                    var childVariable = EmitNode(
                        child,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emittedEventBindingMethodNames,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: inlineAddTemplate,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference,
                        emitDebugLineDirectives: emitDebugLineDirectives,
                        lineDirectiveFilePath: lineDirectiveFilePath);
                    if (!child.UseTopDownInitialization)
                    {
                        var childValueExpression = BuildAttachedNodeValueExpression(
                            child,
                            childVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, childVariable)));
                        childValueExpression = AttachBindingNodeNameScope(child, childValueExpression);
                        EmitStatementAt(
                            BuildCollectionAddStatement($"{variableName}.Children", childValueExpression, addInstruction),
                            child.Line,
                            child.Column);
                    }
                }

                break;
            }
            case ResolvedChildAttachmentMode.ItemsCollection:
            {
                EmitNodeStatement($"if (__replaceExistingCollections) __AXSGObjectGraph.TryClearCollection({variableName}.Items);");
                for (var childIndex = 0; childIndex < node.Children.Length; childIndex++)
                {
                    var child = node.Children[childIndex];
                    var addInstruction = ResolveCollectionAddInstruction(
                        node.ChildAddInstructions,
                        childIndex,
                        node.TypeName + ".Items");
                    var inlineAddValueToken = AttachBindingNodeNameScope(child, TopDownAttachValueToken);
                    var inlineAddTemplate = BuildCollectionAddStatement(
                        $"{variableName}.Items",
                        inlineAddValueToken,
                        addInstruction);
                    var childVariable = EmitNode(
                        child,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emittedEventBindingMethodNames,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: inlineAddTemplate,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference,
                        emitDebugLineDirectives: emitDebugLineDirectives,
                        lineDirectiveFilePath: lineDirectiveFilePath);
                    if (!child.UseTopDownInitialization)
                    {
                        var childValueExpression = BuildAttachedNodeValueExpression(
                            child,
                            childVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, childVariable)));
                        childValueExpression = AttachBindingNodeNameScope(child, childValueExpression);
                        EmitStatementAt(
                            BuildCollectionAddStatement($"{variableName}.Items", childValueExpression, addInstruction),
                            child.Line,
                            child.Column);
                    }
                }

                break;
            }
            case ResolvedChildAttachmentMode.DirectAdd:
            {
                if (node.Children.Length > 0 || existingVariableName is not null)
                {
                    EmitNodeStatement($"if (__replaceExistingCollections) __AXSGObjectGraph.TryClearCollection({variableName});");
                }
                for (var childIndex = 0; childIndex < node.Children.Length; childIndex++)
                {
                    var child = node.Children[childIndex];
                    var addInstruction = ResolveCollectionAddInstruction(
                        node.ChildAddInstructions,
                        childIndex,
                        node.TypeName);
                    var isStyleIncludeChild = IsStyleIncludeNode(child);
                    var inlineAddValueToken = AttachBindingNodeNameScope(child, TopDownAttachValueToken);
                    var inlineAddTemplate = BuildCollectionAddStatement(
                        variableName,
                        inlineAddValueToken,
                        addInstruction);
                    var childVariable = EmitNode(
                        child,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emittedEventBindingMethodNames,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: isStyleIncludeChild ? null : inlineAddTemplate,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference,
                        emitDebugLineDirectives: emitDebugLineDirectives,
                        lineDirectiveFilePath: lineDirectiveFilePath);
                    if (isStyleIncludeChild)
                    {
                        var styleIncludeExpression = BuildAttachedNodeValueExpression(
                            child,
                            childVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, childVariable)));
                        EmitStatementAt(
                            $"if (!__AXSGObjectGraph.TryApplyStyleInclude({variableName}, {variableName}, {styleIncludeExpression}, __SourceGenDocumentUri)) {BuildCollectionAddStatement(variableName, styleIncludeExpression, addInstruction)}",
                            child.Line,
                            child.Column);
                        continue;
                    }

                    if (!child.UseTopDownInitialization)
                    {
                        var childValueExpression = BuildAttachedNodeValueExpression(
                            child,
                            childVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, childVariable)));
                        childValueExpression = AttachBindingNodeNameScope(child, childValueExpression);
                        EmitStatementAt(
                            BuildCollectionAddStatement(variableName, childValueExpression, addInstruction),
                            child.Line,
                            child.Column);
                    }
                }

                break;
            }
            case ResolvedChildAttachmentMode.DictionaryAdd:
            {
                foreach (var child in node.Children)
                {
                    if (string.IsNullOrWhiteSpace(child.KeyExpression))
                    {
                        continue;
                    }

                    var childVariable = EmitNode(
                        child,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emittedEventBindingMethodNames,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: $"__AXSGObjectGraph.TryAddToDictionary({variableName}, {child.KeyExpression}, {TopDownAttachValueToken}, __SourceGenDocumentUri);",
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference,
                        emitDebugLineDirectives: emitDebugLineDirectives,
                        lineDirectiveFilePath: lineDirectiveFilePath);
                    if (!child.UseTopDownInitialization)
                    {
                        var childValueExpression = BuildAttachedNodeValueExpression(
                            child,
                            childVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, childVariable)));
                        EmitStatementAt(
                            $"__AXSGObjectGraph.TryAddToDictionary({variableName}, {child.KeyExpression}, {childValueExpression}, __SourceGenDocumentUri);",
                            child.Line,
                            child.Column);
                    }
                }

                break;
            }
        }

        if (completeNameScopeOnNodeCompletion &&
            !string.IsNullOrWhiteSpace(nameScopeReference))
        {
            EmitNodeStatement($"__AXSGObjectGraph.TryCompleteNameScope({nameScopeReference});");
        }

        EmitNodeStatement($"__AXSGObjectGraph.EndInit({variableName});");
        return variableName;
    }

    private static bool TryEmitDeferredTemplateNode(
        ResolvedObjectNode node,
        StringBuilder sourceBuilder,
        ref int nodeCounter,
        string indent,
        string rootReference,
        string variableName,
        IReadOnlyDictionary<string, string> namedFieldMap,
        IReadOnlyDictionary<string, string> emittedEventBindingMethodNames,
        bool emitNameScopeRegistration,
        string? nameScopeReference,
        bool shouldCreateVariable,
        string? topDownAttachmentTemplate,
        bool completeNameScopeOnNodeCompletion,
        string serviceProviderReference,
        string baseUriExpression,
        ImmutableArray<string> parentStackReferences,
        string intermediateRootReference,
        bool emitDebugLineDirectives,
        string lineDirectiveFilePath)
    {
        if (!IsDeferredTemplateNode(node))
        {
            return false;
        }

        var nodeParentStackReferences = ExtendParentStack(parentStackReferences, variableName);
        var nodeParentStackExpression = BuildParentStackExpression(nodeParentStackReferences);
        void EmitNodeStatement(string statement)
        {
            AppendSourceMappedLine(
                sourceBuilder,
                indent + statement,
                emitDebugLineDirectives,
                lineDirectiveFilePath,
                node.Line,
                node.Column);
        }

        void EmitStatementAt(string statement, int line, int column)
        {
            AppendSourceMappedLine(
                sourceBuilder,
                indent + statement,
                emitDebugLineDirectives,
                lineDirectiveFilePath,
                line,
                column);
        }

        string AttachBindingNameScope(string expression, ResolvedValueKind valueKind)
        {
            if (string.IsNullOrWhiteSpace(nameScopeReference) || !IsBindingValueKind(valueKind))
            {
                return expression;
            }

            return $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.AttachBindingNameScope({expression}, {nameScopeReference})";
        }

        string AttachBindingNodeNameScope(ResolvedObjectNode nodeValue, string expression)
        {
            if (string.IsNullOrWhiteSpace(nameScopeReference) || !nodeValue.IsBindingObjectNode)
            {
                return expression;
            }

            return $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.AttachBindingNameScope({expression}, {nameScopeReference})";
        }

        string WrapSetValueExpression(string expression, string targetObjectExpression, string targetPropertyExpression)
        {
            return "global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ProvideMarkupExtensionValue(" +
                   expression +
                   ", " + serviceProviderReference +
                   ", " + rootReference +
                   ", " + intermediateRootReference +
                   ", " + targetObjectExpression +
                   ", " + targetPropertyExpression +
                   ", " + baseUriExpression +
                   ", " + nodeParentStackExpression +
                   ")";
        }

        if (shouldCreateVariable)
        {
            nodeCounter++;
            EmitNodeStatement($"var {variableName} = {BuildObjectCreationExpression(node, serviceProviderReference, baseUriExpression)};");
        }

        if (emitNameScopeRegistration &&
            !string.IsNullOrWhiteSpace(nameScopeReference))
        {
            var scopedElementVariable = "__scopedStyledElement" + nodeCounter.ToString();
            EmitNodeStatement($"if ((object){variableName} is global::Avalonia.StyledElement {scopedElementVariable})");
            EmitNodeStatement("{");
            EmitNodeStatement($"    __AXSGObjectGraph.TrySetNameScope({scopedElementVariable}, {nameScopeReference});");
            EmitNodeStatement("}");
        }

        EmitNodeStatement($"__AXSGObjectGraph.BeginInit({variableName});");
        string? pendingTopDownAttachmentStatement = null;
        if (node.UseTopDownInitialization &&
            !string.IsNullOrWhiteSpace(topDownAttachmentTemplate))
        {
            var attachmentValueExpression = BuildAttachedNodeValueExpression(
                node,
                variableName,
                serviceProviderReference,
                rootReference,
                intermediateRootReference,
                baseUriExpression,
                nodeParentStackExpression);
            pendingTopDownAttachmentStatement = ExpandTopDownAttachmentTemplate(
                topDownAttachmentTemplate!,
                attachmentValueExpression);
        }

        if (!string.IsNullOrWhiteSpace(node.Name))
        {
            EmitNodeStatement(
                $"if ((object){variableName} is global::Avalonia.StyledElement) ((global::Avalonia.StyledElement)(object){variableName}).Name = \"{Escape(node.Name!)}\";");

            if (namedFieldMap.TryGetValue(node.Name!, out var fieldName))
            {
                EmitNodeStatement($"{rootReference}.{fieldName} = {variableName};");
            }

            if (emitNameScopeRegistration &&
                !string.IsNullOrWhiteSpace(nameScopeReference))
            {
                EmitNodeStatement(
                    $"{nameScopeReference}.Register(\"{Escape(node.Name!)}\", {variableName});");
            }
        }

        if (pendingTopDownAttachmentStatement is not null)
        {
            var pendingTopDownAttachmentStatementValue = pendingTopDownAttachmentStatement.Trim();
            if (pendingTopDownAttachmentStatementValue.Length > 0)
            {
                EmitNodeStatement(pendingTopDownAttachmentStatementValue);
            }
        }

        foreach (var assignment in node.PropertyAssignments)
        {
            if (IsContentPropertyName(assignment.PropertyName))
            {
                continue;
            }

            var avaloniaPropertyExpression = BuildAvaloniaPropertyExpression(assignment);
            var avaloniaPriorityExpression = BuildAvaloniaPriorityExpression(assignment);
            var targetPropertyExpression = BuildTargetPropertyExpression(assignment);
            var bindingAnchorExpression =
                $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ResolveBindingAnchor({variableName}, {nodeParentStackExpression})";
            var valueExpression = ExpandMarkupContextExpression(
                assignment.ValueExpression,
                serviceProviderReference,
                rootReference,
                intermediateRootReference,
                variableName,
                targetPropertyExpression,
                baseUriExpression,
                nodeParentStackExpression);

            if (avaloniaPropertyExpression is null || avaloniaPropertyExpression.Trim().Length == 0)
            {
                var clrAssignmentValueExpression = AttachBindingNameScope(valueExpression, assignment.ValueKind);

                if (TryBuildSpecialClrSetterInvocation(
                        variableName,
                        assignment.PropertyName,
                        assignment.ClrPropertyOwnerTypeName,
                        assignment.ClrPropertyTypeName,
                        clrAssignmentValueExpression,
                        out var specialClrSetterInvocation))
                {
                    EmitStatementAt(specialClrSetterInvocation, assignment.Line, assignment.Column);
                }
                else if (TryBuildDirectClrPropertyAssignment(
                        variableName,
                        assignment.PropertyName,
                        assignment.ClrPropertyTypeName,
                        clrAssignmentValueExpression,
                        out var directClrAssignment))
                {
                    EmitStatementAt(directClrAssignment, assignment.Line, assignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.{assignment.PropertyName} = {clrAssignmentValueExpression};",
                        assignment.Line,
                        assignment.Column);
                }
            }
            else
            {
                var avaloniaPropertyExpressionValue = avaloniaPropertyExpression;
                if (IsBindingValueKind(assignment.ValueKind))
                {
                    var bindingExpression = AttachBindingNameScope(valueExpression, assignment.ValueKind);
                    EmitStatementAt(
                        $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ApplyBinding({variableName}, {avaloniaPropertyExpressionValue}, {bindingExpression}, {bindingAnchorExpression});",
                        assignment.Line,
                        assignment.Column);
                    continue;
                }

                if (string.IsNullOrWhiteSpace(avaloniaPriorityExpression))
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpressionValue}, {WrapSetValueExpression(valueExpression, variableName, avaloniaPropertyExpressionValue)});",
                        assignment.Line,
                        assignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpressionValue}, {WrapSetValueExpression(valueExpression, variableName, avaloniaPropertyExpressionValue)}, {avaloniaPriorityExpression});",
                        assignment.Line,
                        assignment.Column);
                }
            }
        }

        foreach (var eventSubscription in node.EventSubscriptions)
        {
            EmitEventSubscription(
                sourceBuilder,
                indent,
                variableName,
                rootReference,
                eventSubscription,
                emittedEventBindingMethodNames,
                emitDebugLineDirectives,
                lineDirectiveFilePath);
        }

        foreach (var propertyElementAssignment in node.PropertyElementAssignments)
        {
            if (IsContentPropertyName(propertyElementAssignment.PropertyName))
            {
                continue;
            }

            if (propertyElementAssignment.IsCollectionAdd)
            {
                EmitStatementAt(
                    $"if (__replaceExistingCollections) __AXSGObjectGraph.TryClearCollection({variableName}.{propertyElementAssignment.PropertyName});",
                    propertyElementAssignment.Line,
                    propertyElementAssignment.Column);
                var isMergedDictionariesProperty = string.Equals(
                    propertyElementAssignment.PropertyName,
                    "MergedDictionaries",
                    StringComparison.Ordinal);
                var collectionExpression = $"{variableName}.{propertyElementAssignment.PropertyName}";
                for (var valueIndex = 0; valueIndex < propertyElementAssignment.ObjectValues.Length; valueIndex++)
                {
                    var value = propertyElementAssignment.ObjectValues[valueIndex];
                    var addInstruction = ResolveCollectionAddInstruction(
                        propertyElementAssignment.CollectionAddInstructions,
                        valueIndex,
                        node.TypeName + "." + propertyElementAssignment.PropertyName);
                    var isMergeResourceIncludeValue =
                        isMergedDictionariesProperty &&
                        IsMergeResourceIncludeNode(value);
                    var isStyleIncludeValue = IsStyleIncludeNode(value);
                    var requiresDeferredCollectionAttach =
                        isMergeResourceIncludeValue ||
                        isStyleIncludeValue;
                    var inlineAddValueToken = AttachBindingNodeNameScope(value, TopDownAttachValueToken);
                    var inlineAddTemplate = BuildCollectionAddStatement(
                        collectionExpression,
                        inlineAddValueToken,
                        addInstruction);
                    var valueVariable = EmitNode(
                        value,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emittedEventBindingMethodNames,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: requiresDeferredCollectionAttach ? null : inlineAddTemplate,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference,
                        emitDebugLineDirectives: emitDebugLineDirectives,
                        lineDirectiveFilePath: lineDirectiveFilePath);
                    if (isMergeResourceIncludeValue)
                    {
                        var mergeIncludeExpression = BuildAttachedNodeValueExpression(
                            value,
                            valueVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, valueVariable)));
                        EmitStatementAt(
                            $"if (!__AXSGObjectGraph.TryApplyMergedResourceInclude({variableName}, {mergeIncludeExpression}, __SourceGenDocumentUri)) {BuildCollectionAddStatement(collectionExpression, mergeIncludeExpression, addInstruction)}",
                            value.Line,
                            value.Column);
                        continue;
                    }

                    if (isStyleIncludeValue)
                    {
                        var styleIncludeExpression = BuildAttachedNodeValueExpression(
                            value,
                            valueVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, valueVariable)));
                        EmitStatementAt(
                            $"if (!__AXSGObjectGraph.TryApplyStyleInclude({collectionExpression}, {variableName}, {styleIncludeExpression}, __SourceGenDocumentUri)) {BuildCollectionAddStatement(collectionExpression, styleIncludeExpression, addInstruction)}",
                            value.Line,
                            value.Column);
                        continue;
                    }

                    if (!value.UseTopDownInitialization)
                    {
                        var valueExpression = BuildAttachedNodeValueExpression(
                            value,
                            valueVariable,
                            serviceProviderReference,
                            rootReference,
                            intermediateRootReference,
                            baseUriExpression,
                            BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, valueVariable)));
                        valueExpression = AttachBindingNodeNameScope(value, valueExpression);
                        EmitStatementAt(
                            BuildCollectionAddStatement(collectionExpression, valueExpression, addInstruction),
                            value.Line,
                            value.Column);
                    }
                }

                continue;
            }

            if (propertyElementAssignment.IsDictionaryMerge)
            {
                if (TryEmitDictionaryMergePropertyElement(
                        propertyElementAssignment,
                        variableName,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emittedEventBindingMethodNames,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference,
                        baseUriExpression,
                        nodeParentStackReferences,
                        intermediateRootReference,
                        emitDebugLineDirectives,
                        lineDirectiveFilePath))
                {
                    continue;
                }
            }

            if (propertyElementAssignment.ObjectValues.Length == 0)
            {
                continue;
            }

            var avaloniaPropertyExpression = BuildAvaloniaPropertyExpression(propertyElementAssignment);
            var avaloniaPriorityExpression = BuildAvaloniaPriorityExpression(propertyElementAssignment);
            string assignmentTemplate;
            if (avaloniaPropertyExpression is null || avaloniaPropertyExpression.Trim().Length == 0)
            {
                if (TryBuildSpecialClrSetterInvocation(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyOwnerTypeName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        TopDownAttachValueToken,
                        out var specialTopDownSetterInvocation))
                {
                    assignmentTemplate = specialTopDownSetterInvocation;
                }
                else if (TryBuildDirectClrPropertyAssignment(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        TopDownAttachValueToken,
                        out var topDownClrAssignment))
                {
                    assignmentTemplate = topDownClrAssignment;
                }
                else
                {
                    assignmentTemplate = $"{variableName}.{propertyElementAssignment.PropertyName} = {TopDownAttachValueToken};";
                }
            }
            else
            {
                var avaloniaPropertyExpressionValue = avaloniaPropertyExpression;
                if (propertyElementAssignment.ObjectValues[0].IsBindingObjectNode)
                {
                    var bindingValueExpression = string.IsNullOrWhiteSpace(nameScopeReference)
                        ? TopDownAttachValueToken
                        : $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.AttachBindingNameScope({TopDownAttachValueToken}, {nameScopeReference})";
                    assignmentTemplate =
                        $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ApplyBinding({variableName}, {avaloniaPropertyExpressionValue}, {bindingValueExpression}, global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ResolveBindingAnchor({variableName}, {nodeParentStackExpression}));";
                }
                else if (string.IsNullOrWhiteSpace(avaloniaPriorityExpression))
                {
                    assignmentTemplate =
                        $"{variableName}.SetValue({avaloniaPropertyExpressionValue}, {WrapSetValueExpression(TopDownAttachValueToken, variableName, avaloniaPropertyExpressionValue)});";
                }
                else
                {
                    assignmentTemplate =
                        $"{variableName}.SetValue({avaloniaPropertyExpressionValue}, {WrapSetValueExpression(TopDownAttachValueToken, variableName, avaloniaPropertyExpressionValue)}, {avaloniaPriorityExpression});";
                }
            }

            var assignedNode = propertyElementAssignment.ObjectValues[0];
            var assignedValue = EmitNode(
                assignedNode,
                sourceBuilder,
                ref nodeCounter,
                indent,
                rootReference,
                namedFieldMap,
                emittedEventBindingMethodNames,
                emitNameScopeRegistration,
                nameScopeReference,
                serviceProviderReference: serviceProviderReference,
                topDownAttachmentTemplate: assignmentTemplate,
                baseUriExpression: baseUriExpression,
                parentStackReferences: nodeParentStackReferences,
                intermediateRootReference: intermediateRootReference,
                emitDebugLineDirectives: emitDebugLineDirectives,
                lineDirectiveFilePath: lineDirectiveFilePath);
            if (assignedNode.UseTopDownInitialization)
            {
                continue;
            }

            assignedValue = BuildAttachedNodeValueExpression(
                assignedNode,
                assignedValue,
                serviceProviderReference,
                rootReference,
                intermediateRootReference,
                baseUriExpression,
                BuildParentStackExpression(ExtendParentStack(nodeParentStackReferences, assignedValue)));

            if (avaloniaPropertyExpression is null || avaloniaPropertyExpression.Trim().Length == 0)
            {
                if (TryBuildSpecialClrSetterInvocation(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyOwnerTypeName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        assignedValue,
                        out var specialClrSetterInvocation))
                {
                    EmitStatementAt(
                        specialClrSetterInvocation,
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else if (TryBuildDirectClrPropertyAssignment(
                        variableName,
                        propertyElementAssignment.PropertyName,
                        propertyElementAssignment.ClrPropertyTypeName,
                        assignedValue,
                        out var directClrAssignment))
                {
                    EmitStatementAt(
                        directClrAssignment,
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.{propertyElementAssignment.PropertyName} = {assignedValue};",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
            }
            else
            {
                var avaloniaPropertyExpressionValue = avaloniaPropertyExpression;
                if (assignedNode.IsBindingObjectNode)
                {
                    var bindingValueExpression = string.IsNullOrWhiteSpace(nameScopeReference)
                        ? assignedValue
                        : $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.AttachBindingNameScope({assignedValue}, {nameScopeReference})";
                    EmitStatementAt(
                        $"global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ApplyBinding({variableName}, {avaloniaPropertyExpressionValue}, {bindingValueExpression}, global::XamlToCSharpGenerator.Runtime.SourceGenMarkupExtensionRuntime.ResolveBindingAnchor({variableName}, {nodeParentStackExpression}));",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else if (string.IsNullOrWhiteSpace(avaloniaPriorityExpression))
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpressionValue}, {WrapSetValueExpression(assignedValue, variableName, avaloniaPropertyExpressionValue)});",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
                else
                {
                    EmitStatementAt(
                        $"{variableName}.SetValue({avaloniaPropertyExpressionValue}, {WrapSetValueExpression(assignedValue, variableName, avaloniaPropertyExpressionValue)}, {avaloniaPriorityExpression});",
                        propertyElementAssignment.Line,
                        propertyElementAssignment.Column);
                }
            }
        }

        var contentNode = TryGetTemplateContentNode(node);
        var templateContentPropertyName = string.IsNullOrWhiteSpace(node.ContentPropertyName)
            ? "Content"
            : node.ContentPropertyName!;
        if (contentNode is not null)
        {
            var factoryIndent = indent + "    ";
            var templateScopeName = "__templateScope" + nodeCounter.ToString();
            var templateProviderName = "__templateProvider" + nodeCounter.ToString();
            sourceBuilder.AppendLine(
                $"{indent}{variableName}.{templateContentPropertyName} = (global::System.Func<global::System.IServiceProvider?, object?>)(__templateServiceProvider =>");
            sourceBuilder.AppendLine($"{indent}{{");
            sourceBuilder.AppendLine(
                $"{factoryIndent}var {templateScopeName} = global::XamlToCSharpGenerator.Runtime.SourceGenDeferredServiceProviderFactory.CreateTemplateNameScope(__templateServiceProvider);");
            sourceBuilder.AppendLine(
                $"{factoryIndent}var {templateProviderName} = global::XamlToCSharpGenerator.Runtime.SourceGenDeferredServiceProviderFactory.CreateDeferredTemplateServiceProvider(__templateServiceProvider, {rootReference}, {templateScopeName});");
            var templateRoot = EmitNode(
                contentNode,
                sourceBuilder,
                ref nodeCounter,
                factoryIndent,
                rootReference,
                EmptyNamedFieldMap,
                emittedEventBindingMethodNames,
                emitNameScopeRegistration: true,
                nameScopeReference: templateScopeName,
                completeNameScopeOnNodeCompletion: true,
                serviceProviderReference: templateProviderName,
                baseUriExpression: baseUriExpression,
                parentStackReferences: nodeParentStackReferences,
                intermediateRootReference: variableName,
                emitDebugLineDirectives: emitDebugLineDirectives,
                lineDirectiveFilePath: lineDirectiveFilePath);
            sourceBuilder.AppendLine($"{factoryIndent}if ({templateRoot} is global::Avalonia.StyledElement __templateStyledElement)");
            sourceBuilder.AppendLine($"{factoryIndent}{{");
            sourceBuilder.AppendLine(
                $"{factoryIndent}    __AXSGObjectGraph.TrySetNameScope(__templateStyledElement, {templateScopeName});");
            sourceBuilder.AppendLine($"{factoryIndent}}}");
            sourceBuilder.AppendLine(
                $"{factoryIndent}return new global::Avalonia.Controls.Templates.TemplateResult<global::Avalonia.Controls.Control>((global::Avalonia.Controls.Control){templateRoot}, {templateScopeName});");
            sourceBuilder.AppendLine($"{indent}}});");
        }

        if (completeNameScopeOnNodeCompletion &&
            !string.IsNullOrWhiteSpace(nameScopeReference))
        {
            EmitNodeStatement($"__AXSGObjectGraph.TryCompleteNameScope({nameScopeReference});");
        }

        EmitNodeStatement($"__AXSGObjectGraph.EndInit({variableName});");
        return true;
    }

    private static void EmitEventSubscription(
        StringBuilder sourceBuilder,
        string indent,
        string variableName,
        string rootReference,
        ResolvedEventSubscription eventSubscription,
        IReadOnlyDictionary<string, string> emittedEventBindingMethodNames,
        bool emitDebugLineDirectives,
        string lineDirectiveFilePath)
    {
        var handlerMethodName = ResolveEmittedEventBindingMethodName(
            eventSubscription.HandlerMethodName,
            emittedEventBindingMethodNames);

        if (string.IsNullOrWhiteSpace(handlerMethodName))
        {
            return;
        }

        if (eventSubscription.Kind == ResolvedEventSubscriptionKind.RoutedEvent &&
            !string.IsNullOrWhiteSpace(eventSubscription.RoutedEventOwnerTypeName) &&
            !string.IsNullOrWhiteSpace(eventSubscription.RoutedEventFieldName))
        {
            var routedEventReference = eventSubscription.RoutedEventOwnerTypeName + "." + eventSubscription.RoutedEventFieldName;
            var handlerExpression = string.IsNullOrWhiteSpace(eventSubscription.RoutedEventHandlerTypeName)
                ? rootReference + "." + handlerMethodName
                : "(" + eventSubscription.RoutedEventHandlerTypeName + ")" + rootReference + "." + handlerMethodName;
            AppendSourceMappedLine(
                sourceBuilder,
                $"{indent}{variableName}.RemoveHandler({routedEventReference}, {handlerExpression});",
                emitDebugLineDirectives,
                lineDirectiveFilePath,
                eventSubscription.Line,
                eventSubscription.Column);
            AppendSourceMappedLine(
                sourceBuilder,
                $"{indent}{variableName}.AddHandler({routedEventReference}, {handlerExpression});",
                emitDebugLineDirectives,
                lineDirectiveFilePath,
                eventSubscription.Line,
                eventSubscription.Column);
            return;
        }

        AppendSourceMappedLine(
            sourceBuilder,
            $"{indent}{variableName}.{eventSubscription.EventName} -= {rootReference}.{handlerMethodName};",
            emitDebugLineDirectives,
            lineDirectiveFilePath,
            eventSubscription.Line,
            eventSubscription.Column);
        AppendSourceMappedLine(
            sourceBuilder,
            $"{indent}{variableName}.{eventSubscription.EventName} += {rootReference}.{handlerMethodName};",
            emitDebugLineDirectives,
            lineDirectiveFilePath,
            eventSubscription.Line,
            eventSubscription.Column);
    }

    private static bool TryEmitDictionaryMergePropertyElement(
        ResolvedPropertyElementAssignment propertyElementAssignment,
        string variableName,
        StringBuilder sourceBuilder,
        ref int nodeCounter,
        string indent,
        string rootReference,
        IReadOnlyDictionary<string, string> namedFieldMap,
        IReadOnlyDictionary<string, string> emittedEventBindingMethodNames,
        bool emitNameScopeRegistration,
        string? nameScopeReference,
        string serviceProviderReference,
        string baseUriExpression,
        ImmutableArray<string> parentStackReferences,
        string intermediateRootReference,
        bool emitDebugLineDirectives,
        string lineDirectiveFilePath)
    {
        AppendSourceMappedLine(
            sourceBuilder,
            $"{indent}if (__replaceExistingCollections) __AXSGObjectGraph.TryClearDictionaryEntries({variableName}.{propertyElementAssignment.PropertyName});",
            emitDebugLineDirectives,
            lineDirectiveFilePath,
            propertyElementAssignment.Line,
            propertyElementAssignment.Column);

        if (propertyElementAssignment.ObjectValues.Length == 0)
        {
            return true;
        }

        var hasKeyedObjectValues = true;
        foreach (var objectValue in propertyElementAssignment.ObjectValues)
        {
            if (string.IsNullOrWhiteSpace(objectValue.KeyExpression))
            {
                hasKeyedObjectValues = false;
                break;
            }
        }

        if (hasKeyedObjectValues)
        {
            foreach (var keyedObjectValue in propertyElementAssignment.ObjectValues)
            {
                var dictionaryKeyExpression = BuildDictionaryKeyExpression(
                    propertyElementAssignment.PropertyName,
                    keyedObjectValue.KeyExpression!);
                var keyedChildVariable = EmitNode(
                    keyedObjectValue,
                    sourceBuilder,
                    ref nodeCounter,
                    indent,
                    rootReference,
                    namedFieldMap,
                    emittedEventBindingMethodNames,
                    emitNameScopeRegistration,
                    nameScopeReference,
                    serviceProviderReference: serviceProviderReference,
                    topDownAttachmentTemplate: $"__AXSGObjectGraph.TryAddToDictionary({variableName}.{propertyElementAssignment.PropertyName}, {dictionaryKeyExpression}, {TopDownAttachValueToken}, __SourceGenDocumentUri);",
                    baseUriExpression: baseUriExpression,
                    parentStackReferences: parentStackReferences,
                    intermediateRootReference: intermediateRootReference,
                    emitDebugLineDirectives: emitDebugLineDirectives,
                    lineDirectiveFilePath: lineDirectiveFilePath);
                if (!keyedObjectValue.UseTopDownInitialization)
                {
                    var keyedChildValueExpression = BuildAttachedNodeValueExpression(
                        keyedObjectValue,
                        keyedChildVariable,
                        serviceProviderReference,
                        rootReference,
                        intermediateRootReference,
                        baseUriExpression,
                        BuildParentStackExpression(ExtendParentStack(parentStackReferences, keyedChildVariable)));
                    AppendSourceMappedLine(
                        sourceBuilder,
                        $"{indent}__AXSGObjectGraph.TryAddToDictionary({variableName}.{propertyElementAssignment.PropertyName}, {dictionaryKeyExpression}, {keyedChildValueExpression}, __SourceGenDocumentUri);",
                        emitDebugLineDirectives,
                        lineDirectiveFilePath,
                        keyedObjectValue.Line,
                        keyedObjectValue.Column);
                }
            }

            return true;
        }

        var valueNode = propertyElementAssignment.ObjectValues[0];
        if (valueNode.ChildAttachmentMode == ResolvedChildAttachmentMode.DictionaryAdd)
        {
            foreach (var child in valueNode.Children)
            {
                if (string.IsNullOrWhiteSpace(child.KeyExpression))
                {
                    continue;
                }

                var dictionaryKeyExpression = BuildDictionaryKeyExpression(
                    propertyElementAssignment.PropertyName,
                    child.KeyExpression!);
                var childVariable = EmitNode(
                    child,
                    sourceBuilder,
                    ref nodeCounter,
                    indent,
                    rootReference,
                    namedFieldMap,
                    emittedEventBindingMethodNames,
                    emitNameScopeRegistration,
                    nameScopeReference,
                    serviceProviderReference: serviceProviderReference,
                    topDownAttachmentTemplate: $"__AXSGObjectGraph.TryAddToDictionary({variableName}.{propertyElementAssignment.PropertyName}, {dictionaryKeyExpression}, {TopDownAttachValueToken}, __SourceGenDocumentUri);",
                    baseUriExpression: baseUriExpression,
                    parentStackReferences: parentStackReferences,
                    intermediateRootReference: intermediateRootReference,
                    emitDebugLineDirectives: emitDebugLineDirectives,
                    lineDirectiveFilePath: lineDirectiveFilePath);
                if (!child.UseTopDownInitialization)
                {
                    var childValueExpression = BuildAttachedNodeValueExpression(
                        child,
                        childVariable,
                        serviceProviderReference,
                        rootReference,
                        intermediateRootReference,
                        baseUriExpression,
                        BuildParentStackExpression(ExtendParentStack(parentStackReferences, childVariable)));
                    AppendSourceMappedLine(
                        sourceBuilder,
                        $"{indent}__AXSGObjectGraph.TryAddToDictionary({variableName}.{propertyElementAssignment.PropertyName}, {dictionaryKeyExpression}, {childValueExpression}, __SourceGenDocumentUri);",
                        emitDebugLineDirectives,
                        lineDirectiveFilePath,
                        child.Line,
                        child.Column);
                }
            }

            return true;
        }

        var assignedValue = EmitNode(
            valueNode,
            sourceBuilder,
            ref nodeCounter,
            indent,
            rootReference,
            namedFieldMap,
            emittedEventBindingMethodNames,
            emitNameScopeRegistration,
            nameScopeReference,
            serviceProviderReference: serviceProviderReference,
            baseUriExpression: baseUriExpression,
            parentStackReferences: parentStackReferences,
            intermediateRootReference: intermediateRootReference,
            emitDebugLineDirectives: emitDebugLineDirectives,
            lineDirectiveFilePath: lineDirectiveFilePath);
        AppendSourceMappedLine(
            sourceBuilder,
            $"{indent}__AXSGObjectGraph.TryMergeDictionary({variableName}.{propertyElementAssignment.PropertyName}, {assignedValue});",
            emitDebugLineDirectives,
            lineDirectiveFilePath,
            propertyElementAssignment.Line,
            propertyElementAssignment.Column);
        return true;
    }

    private static void AppendSourceMappedLine(
        StringBuilder sourceBuilder,
        string lineText,
        bool emitDebugLineDirectives,
        string lineDirectiveFilePath,
        int line,
        int column)
    {
        if (!emitDebugLineDirectives ||
            string.IsNullOrWhiteSpace(lineDirectiveFilePath))
        {
            sourceBuilder.AppendLine(lineText);
            return;
        }

        var normalizedLine = Math.Max(1, line);
        var normalizedColumn = Math.Max(1, column);
        var indent = ExtractLeadingIndent(lineText);
        sourceBuilder.AppendLine($"{indent}// AXSG:XAML {normalizedLine}:{normalizedColumn}");
        sourceBuilder.AppendLine($"{indent}#line {normalizedLine} \"{EscapeLineDirectivePath(lineDirectiveFilePath)}\"");
        sourceBuilder.AppendLine(lineText);
        sourceBuilder.AppendLine($"{indent}#line default");
        sourceBuilder.AppendLine($"{indent}#line hidden");
    }

    private static string NormalizeLineDirectivePath(string? filePath)
    {
        if (string.IsNullOrWhiteSpace(filePath))
        {
            return string.Empty;
        }

        return filePath!.Replace('\\', '/');
    }

    private static string EscapeLineDirectivePath(string filePath)
    {
        return filePath.Replace("\"", "\\\"");
    }

    private static string ExtractLeadingIndent(string text)
    {
        var index = 0;
        while (index < text.Length &&
               (text[index] == ' ' || text[index] == '\t'))
        {
            index++;
        }

        return text.Substring(0, index);
    }

