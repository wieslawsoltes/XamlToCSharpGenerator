    private static string ReplaceOrdinal(string source, string oldValue, string newValue)
    {
        if (string.IsNullOrEmpty(source) || string.IsNullOrEmpty(oldValue))
        {
            return source;
        }

        var firstMatch = source.IndexOf(oldValue, StringComparison.Ordinal);
        if (firstMatch < 0)
        {
            return source;
        }

        var builder = new StringBuilder(source.Length);
        var copyIndex = 0;
        var matchIndex = firstMatch;
        while (matchIndex >= 0)
        {
            builder.Append(source, copyIndex, matchIndex - copyIndex);
            builder.Append(newValue);
            copyIndex = matchIndex + oldValue.Length;
            matchIndex = source.IndexOf(oldValue, copyIndex, StringComparison.Ordinal);
        }

        builder.Append(source, copyIndex, source.Length - copyIndex);
        return builder.ToString();
    }

    private static void EmitSourceInfoRegistrations(ResolvedViewModel viewModel, StringBuilder sourceBuilder, string escapedUri)
    {
        var document = viewModel.Document;
        var escapedFilePath = Escape(document.FilePath);
        EmitSourceInfoRegistration(
            sourceBuilder,
            escapedUri,
            escapedFilePath,
            "Root",
            document.ClassFullName ?? document.ClassName,
            document.RootObject.Line,
            document.RootObject.Column);

        EmitObjectSourceInfoRegistrations(
            viewModel.RootObject,
            sourceBuilder,
            escapedUri,
            escapedFilePath,
            "Object:0");

        for (var index = 0; index < viewModel.NamedElements.Length; index++)
        {
            var namedElement = viewModel.NamedElements[index];
            EmitSourceInfoRegistration(
                sourceBuilder,
                escapedUri,
                escapedFilePath,
                "NamedElement",
                BuildIndexedIdentity("NamedElement", index, namedElement.Name),
                namedElement.Line,
                namedElement.Column);
        }

        for (var index = 0; index < viewModel.Resources.Length; index++)
        {
            var resource = viewModel.Resources[index];
            EmitSourceInfoRegistration(
                sourceBuilder,
                escapedUri,
                escapedFilePath,
                "Resource",
                BuildIndexedIdentity("Resource", index, resource.Key),
                resource.Line,
                resource.Column);
        }

        for (var index = 0; index < viewModel.Templates.Length; index++)
        {
            var template = viewModel.Templates[index];
            EmitSourceInfoRegistration(
                sourceBuilder,
                escapedUri,
                escapedFilePath,
                "Template",
                BuildIndexedIdentity("Template", index, (template.Key ?? string.Empty) + "|" + template.Kind),
                template.Line,
                template.Column);
        }

        for (var styleIndex = 0; styleIndex < viewModel.Styles.Length; styleIndex++)
        {
            var style = viewModel.Styles[styleIndex];
            var styleIdentity = BuildIndexedIdentity("Style", styleIndex, style.Selector);
            EmitSourceInfoRegistration(
                sourceBuilder,
                escapedUri,
                escapedFilePath,
                "Style",
                styleIdentity,
                style.Line,
                style.Column);

            for (var setterIndex = 0; setterIndex < style.Setters.Length; setterIndex++)
            {
                var setter = style.Setters[setterIndex];
                EmitSourceInfoRegistration(
                    sourceBuilder,
                    escapedUri,
                    escapedFilePath,
                    "StyleSetter",
                    styleIdentity + "/Setter:" + setterIndex.ToString() + ":" + setter.PropertyName,
                    setter.Line,
                    setter.Column);
            }
        }

        for (var themeIndex = 0; themeIndex < viewModel.ControlThemes.Length; themeIndex++)
        {
            var controlTheme = viewModel.ControlThemes[themeIndex];
            var themeIdentity = BuildIndexedIdentity("ControlTheme", themeIndex, controlTheme.Key);
            EmitSourceInfoRegistration(
                sourceBuilder,
                escapedUri,
                escapedFilePath,
                "ControlTheme",
                themeIdentity,
                controlTheme.Line,
                controlTheme.Column);

            for (var setterIndex = 0; setterIndex < controlTheme.Setters.Length; setterIndex++)
            {
                var setter = controlTheme.Setters[setterIndex];
                EmitSourceInfoRegistration(
                    sourceBuilder,
                    escapedUri,
                    escapedFilePath,
                    "ControlThemeSetter",
                    themeIdentity + "/Setter:" + setterIndex.ToString() + ":" + setter.PropertyName,
                    setter.Line,
                    setter.Column);
            }
        }

        for (var index = 0; index < viewModel.Includes.Length; index++)
        {
            var include = viewModel.Includes[index];
            EmitSourceInfoRegistration(
                sourceBuilder,
                escapedUri,
                escapedFilePath,
                "Include",
                BuildIndexedIdentity("Include", index, include.Source + "|" + include.MergeTarget),
                include.Line,
                include.Column);
        }

        for (var index = 0; index < viewModel.CompiledBindings.Length; index++)
        {
            var binding = viewModel.CompiledBindings[index];
            EmitSourceInfoRegistration(
                sourceBuilder,
                escapedUri,
                escapedFilePath,
                "CompiledBinding",
                BuildIndexedIdentity("CompiledBinding", index, binding.TargetPropertyName + "|" + binding.Path),
                binding.Line,
                binding.Column);
        }
    }

    private static void EmitObjectSourceInfoRegistrations(
        ResolvedObjectNode node,
        StringBuilder sourceBuilder,
        string escapedUri,
        string escapedFilePath,
        string objectIdentity)
    {
        EmitSourceInfoRegistration(
            sourceBuilder,
            escapedUri,
            escapedFilePath,
            "Object",
            objectIdentity + ":" + node.TypeName,
            node.Line,
            node.Column);

        for (var assignmentIndex = 0; assignmentIndex < node.PropertyAssignments.Length; assignmentIndex++)
        {
            var assignment = node.PropertyAssignments[assignmentIndex];
            EmitSourceInfoRegistration(
                sourceBuilder,
                escapedUri,
                escapedFilePath,
                "Property",
                objectIdentity + "/Property:" + assignmentIndex.ToString() + ":" + assignment.PropertyName,
                assignment.Line,
                assignment.Column);
        }

        for (var eventIndex = 0; eventIndex < node.EventSubscriptions.Length; eventIndex++)
        {
            var eventSubscription = node.EventSubscriptions[eventIndex];
            EmitSourceInfoRegistration(
                sourceBuilder,
                escapedUri,
                escapedFilePath,
                "Event",
                objectIdentity + "/Event:" + eventIndex.ToString() + ":" + eventSubscription.EventName,
                eventSubscription.Line,
                eventSubscription.Column);
        }

        for (var assignmentIndex = 0; assignmentIndex < node.PropertyElementAssignments.Length; assignmentIndex++)
        {
            var propertyElement = node.PropertyElementAssignments[assignmentIndex];
            var propertyElementIdentity = objectIdentity + "/PropertyElement:" + assignmentIndex.ToString() + ":" +
                                          propertyElement.PropertyName;
            EmitSourceInfoRegistration(
                sourceBuilder,
                escapedUri,
                escapedFilePath,
                "PropertyElement",
                propertyElementIdentity,
                propertyElement.Line,
                propertyElement.Column);

            for (var valueIndex = 0; valueIndex < propertyElement.ObjectValues.Length; valueIndex++)
            {
                EmitObjectSourceInfoRegistrations(
                    propertyElement.ObjectValues[valueIndex],
                    sourceBuilder,
                    escapedUri,
                    escapedFilePath,
                    propertyElementIdentity + "/Value:" + valueIndex.ToString());
            }
        }

        for (var childIndex = 0; childIndex < node.Children.Length; childIndex++)
        {
            EmitObjectSourceInfoRegistrations(
                node.Children[childIndex],
                sourceBuilder,
                escapedUri,
                escapedFilePath,
                objectIdentity + "/Child:" + childIndex.ToString());
        }
    }

