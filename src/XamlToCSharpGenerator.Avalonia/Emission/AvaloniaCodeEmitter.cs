    private static string ExtractMemberName(string propertyName)
    {
        if (string.IsNullOrWhiteSpace(propertyName))
        {
            return string.Empty;
        }

        var separatorIndex = propertyName.LastIndexOf('.');
        if (separatorIndex < 0 || separatorIndex >= propertyName.Length - 1)
        {
            return propertyName;
        }

        return propertyName.Substring(separatorIndex + 1);
    }

    private static ImmutableArray<string> ToImmutableArray(SortedSet<string> values)
    {
        if (values.Count == 0)
        {
            return ImmutableArray<string>.Empty;
        }

        var builder = ImmutableArray.CreateBuilder<string>(values.Count);
        foreach (var value in values)
        {
            builder.Add(value);
        }

        return builder.ToImmutable();
    }

