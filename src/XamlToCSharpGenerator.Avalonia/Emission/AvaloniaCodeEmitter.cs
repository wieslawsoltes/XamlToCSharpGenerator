    private static string BuildIndexedIdentity(string kind, int index, string? hint)
    {
        var normalizedHint = string.IsNullOrWhiteSpace(hint) ? "<none>" : hint!;
        return kind + ":" + index.ToString() + ":" + normalizedHint;
    }

    private static string ToQuotedOrNull(string? value)
    {
        if (string.IsNullOrWhiteSpace(value))
        {
            return "null";
        }

        return "\"" + Escape(value!) + "\"";
    }

    private static string BoolLiteral(bool value)
    {
        return value ? "true" : "false";
    }

    private static string BuildStringArrayExpression(ImmutableArray<string> values)
    {
        if (values.IsDefaultOrEmpty)
        {
            return "global::System.Array.Empty<string>()";
        }

        var builder = new StringBuilder();
        builder.Append("new string[] { ");
        for (var index = 0; index < values.Length; index++)
        {
            if (index > 0)
            {
                builder.Append(", ");
            }

            builder.Append('"');
            builder.Append(Escape(values[index]));
            builder.Append('"');
        }

        builder.Append(" }");
        return builder.ToString();
    }

    private static ImmutableArray<string> BuildRootHotReloadCollectionMembers(ResolvedObjectNode rootNode)
    {
        var members = new SortedSet<string>(StringComparer.Ordinal);

        foreach (var assignment in rootNode.PropertyElementAssignments)
        {
            if (!assignment.IsCollectionAdd && !assignment.IsDictionaryMerge)
            {
                continue;
            }

            var memberName = ExtractMemberName(assignment.PropertyName);
            if (!string.IsNullOrWhiteSpace(memberName))
            {
                members.Add(memberName);
            }
        }

        switch (rootNode.ChildAttachmentMode)
        {
            case ResolvedChildAttachmentMode.ChildrenCollection:
                members.Add("Children");
                break;
            case ResolvedChildAttachmentMode.ItemsCollection:
                members.Add("Items");
                break;
        }

        return ToImmutableArray(members);
    }

    private static ImmutableArray<string> BuildRootHotReloadClrPropertyMembers(
        ResolvedObjectNode rootNode,
        IReadOnlyDictionary<string, string> namedFieldMap)
    {
        var members = new SortedSet<string>(StringComparer.Ordinal);

        foreach (var assignment in rootNode.PropertyAssignments)
        {
            if (!string.IsNullOrWhiteSpace(assignment.AvaloniaPropertyOwnerTypeName) &&
                !string.IsNullOrWhiteSpace(assignment.AvaloniaPropertyFieldName))
            {
                continue;
            }

            var memberName = ExtractMemberName(assignment.PropertyName);
            if (!string.IsNullOrWhiteSpace(memberName))
            {
                members.Add(memberName);
            }
        }

        foreach (var assignment in rootNode.PropertyElementAssignments)
        {
            if (assignment.IsCollectionAdd ||
                assignment.IsDictionaryMerge ||
                assignment.ObjectValues.Length == 0)
            {
                continue;
            }

            if (!string.IsNullOrWhiteSpace(assignment.AvaloniaPropertyOwnerTypeName) &&
                !string.IsNullOrWhiteSpace(assignment.AvaloniaPropertyFieldName))
            {
                continue;
            }

            var memberName = ExtractMemberName(assignment.PropertyName);
            if (!string.IsNullOrWhiteSpace(memberName))
            {
                members.Add(memberName);
            }
        }

        foreach (var fieldName in namedFieldMap.Values)
        {
            if (!string.IsNullOrWhiteSpace(fieldName))
            {
                members.Add(fieldName);
            }
        }

        return ToImmutableArray(members);
    }

    private static ImmutableArray<string> BuildRootHotReloadAvaloniaPropertyMembers(ResolvedObjectNode rootNode)
    {
        var members = new SortedSet<string>(StringComparer.Ordinal);

        foreach (var assignment in rootNode.PropertyAssignments)
        {
            if (string.IsNullOrWhiteSpace(assignment.AvaloniaPropertyOwnerTypeName) ||
                string.IsNullOrWhiteSpace(assignment.AvaloniaPropertyFieldName))
            {
                continue;
            }

            members.Add(assignment.AvaloniaPropertyOwnerTypeName + "." + assignment.AvaloniaPropertyFieldName);
        }

        foreach (var assignment in rootNode.PropertyElementAssignments)
        {
            if (assignment.IsCollectionAdd ||
                assignment.IsDictionaryMerge ||
                assignment.ObjectValues.Length == 0 ||
                string.IsNullOrWhiteSpace(assignment.AvaloniaPropertyOwnerTypeName) ||
                string.IsNullOrWhiteSpace(assignment.AvaloniaPropertyFieldName))
            {
                continue;
            }

            members.Add(assignment.AvaloniaPropertyOwnerTypeName + "." + assignment.AvaloniaPropertyFieldName);
        }

        return ToImmutableArray(members);
    }

    private static ImmutableArray<ResolvedEventSubscription> BuildRootHotReloadEventSubscriptions(ResolvedObjectNode rootNode)
    {
        if (rootNode.EventSubscriptions.IsDefaultOrEmpty)
        {
            return ImmutableArray<ResolvedEventSubscription>.Empty;
        }

        var builder = ImmutableArray.CreateBuilder<ResolvedEventSubscription>(rootNode.EventSubscriptions.Length);
        foreach (var eventSubscription in rootNode.EventSubscriptions)
        {
            if (string.IsNullOrWhiteSpace(eventSubscription.HandlerMethodName))
            {
                continue;
            }

            if (eventSubscription.Kind == ResolvedEventSubscriptionKind.ClrEvent &&
                string.IsNullOrWhiteSpace(eventSubscription.EventName))
            {
                continue;
            }

            if (eventSubscription.Kind == ResolvedEventSubscriptionKind.RoutedEvent &&
                (string.IsNullOrWhiteSpace(eventSubscription.RoutedEventOwnerTypeName) ||
                 string.IsNullOrWhiteSpace(eventSubscription.RoutedEventFieldName) ||
                 string.IsNullOrWhiteSpace(eventSubscription.RoutedEventHandlerTypeName)))
            {
                continue;
            }

            builder.Add(eventSubscription);
        }

        return builder.Count == 0
            ? ImmutableArray<ResolvedEventSubscription>.Empty
            : builder.ToImmutable();
    }

    private static bool ShouldClearRootSelfCollection(ResolvedObjectNode rootNode)
    {
        return rootNode.ChildAttachmentMode is ResolvedChildAttachmentMode.DirectAdd or ResolvedChildAttachmentMode.DictionaryAdd;
    }

    private static string ExtractMemberName(string propertyName)
    {
        if (string.IsNullOrWhiteSpace(propertyName))
        {
            return string.Empty;
        }

        var separatorIndex = propertyName.LastIndexOf('.');
        if (separatorIndex < 0 || separatorIndex >= propertyName.Length - 1)
        {
            return propertyName;
        }

        return propertyName.Substring(separatorIndex + 1);
    }

    private static ImmutableArray<string> ToImmutableArray(SortedSet<string> values)
    {
        if (values.Count == 0)
        {
            return ImmutableArray<string>.Empty;
        }

        var builder = ImmutableArray.CreateBuilder<string>(values.Count);
        foreach (var value in values)
        {
            builder.Add(value);
        }

        return builder.ToImmutable();
    }

    private static string BuildHotReloadEventDescriptorArrayExpression(ImmutableArray<ResolvedEventSubscription> eventSubscriptions)
    {
        if (eventSubscriptions.IsDefaultOrEmpty)
        {
            return "global::System.Array.Empty<global::XamlToCSharpGenerator.Runtime.SourceGenHotReloadEventDescriptor>()";
        }

        var builder = new StringBuilder();
        builder.Append("new global::XamlToCSharpGenerator.Runtime.SourceGenHotReloadEventDescriptor[] { ");
        for (var index = 0; index < eventSubscriptions.Length; index++)
        {
            if (index > 0)
            {
                builder.Append(", ");
            }

            var eventSubscription = eventSubscriptions[index];
            builder.Append("new global::XamlToCSharpGenerator.Runtime.SourceGenHotReloadEventDescriptor(");
            builder.Append('"').Append(Escape(eventSubscription.EventName ?? string.Empty)).Append('"').Append(", ");
            builder.Append('"').Append(Escape(eventSubscription.HandlerMethodName)).Append('"').Append(", ");
            builder.Append(BoolLiteral(eventSubscription.Kind == ResolvedEventSubscriptionKind.RoutedEvent)).Append(", ");
            builder.Append(ToQuotedOrNull(eventSubscription.RoutedEventOwnerTypeName)).Append(", ");
            builder.Append(ToQuotedOrNull(eventSubscription.RoutedEventFieldName)).Append(", ");
            builder.Append(ToQuotedOrNull(eventSubscription.RoutedEventHandlerTypeName));
            builder.Append(")");
        }

        builder.Append(" }");
        return builder.ToString();
    }

    private static string Escape(string value)
    {
        return value.Replace("\\", "\\\\")
            .Replace("\"", "\\\"")
            .Replace("\r", string.Empty)
            .Replace("\n", "\\n");
    }

    private static string EscapeComment(string value)
    {
        return value.Replace("\r", " ").Replace("\n", " ");
    }

    private static string SanitizeIdentifier(string identifier)
    {
        if (string.IsNullOrWhiteSpace(identifier))
        {
            return "UnnamedElement";
        }

        var chars = identifier.ToCharArray();
        for (var index = 0; index < chars.Length; index++)
        {
            if (!char.IsLetterOrDigit(chars[index]) && chars[index] != '_')
            {
                chars[index] = '_';
            }
        }

        if (!char.IsLetter(chars[0]) && chars[0] != '_')
        {
            return "_" + new string(chars);
        }

        return new string(chars);
    }

    private static string BuildTargetPropertyExpression(ResolvedPropertyAssignment assignment)
    {
        if (!string.IsNullOrWhiteSpace(assignment.AvaloniaPropertyOwnerTypeName) &&
            !string.IsNullOrWhiteSpace(assignment.AvaloniaPropertyFieldName))
        {
            return assignment.AvaloniaPropertyOwnerTypeName + "." + assignment.AvaloniaPropertyFieldName;
        }

        if (!string.IsNullOrWhiteSpace(assignment.ClrPropertyOwnerTypeName) &&
            !string.IsNullOrWhiteSpace(assignment.ClrPropertyTypeName))
        {
            return
                "global::XamlToCSharpGenerator.Runtime.SourceGenProvideValueTargetPropertyFactory.CreateWritable<" +
                assignment.ClrPropertyOwnerTypeName +
                ", " +
                assignment.ClrPropertyTypeName +
                ">(\"" +
                Escape(assignment.PropertyName) +
                "\", static (__target, __value) => __target." +
                assignment.PropertyName +
                " = __value)";
        }

        return "null";
    }

    private static bool LooksLikeBindingExpression(string valueExpression)
    {
        return valueExpression.StartsWith("new global::Avalonia.Data.Binding(", StringComparison.Ordinal) ||
               valueExpression.StartsWith("new Avalonia.Data.Binding(", StringComparison.Ordinal) ||
               valueExpression.StartsWith("new Binding(", StringComparison.Ordinal) ||
               valueExpression.Contains(
                   "SourceGenMarkupExtensionRuntime.ProvideReflectionBinding(",
                   StringComparison.Ordinal) ||
               valueExpression.Contains(
                   "SourceGenMarkupExtensionRuntime.ProvideDynamicResource(",
                   StringComparison.Ordinal);
    }

    private static bool HasExplicitContentValue(ResolvedObjectNode node, string contentPropertyName)
    {
        foreach (var assignment in node.PropertyAssignments)
        {
            if (IsContentPropertyName(assignment.PropertyName, contentPropertyName))
            {
                return true;
            }
        }

        foreach (var assignment in node.PropertyElementAssignments)
        {
            if (IsContentPropertyName(assignment.PropertyName, contentPropertyName))
            {
                return true;
            }
        }

        return false;
    }

    private static bool IsContentPropertyName(string propertyName, string contentPropertyName = "Content")
    {
        if (propertyName.Equals(contentPropertyName, StringComparison.OrdinalIgnoreCase))
        {
            return true;
        }

        var separatorIndex = propertyName.LastIndexOf('.');
        if (separatorIndex >= 0 &&
            separatorIndex < propertyName.Length - 1)
        {
            return propertyName.Substring(separatorIndex + 1)
                .Equals(contentPropertyName, StringComparison.OrdinalIgnoreCase);
        }

        return false;
    }

    private static string BuildHintName(ResolvedViewModel viewModel)
    {
        var sourceIdentity = viewModel.BuildUri;
        if (string.IsNullOrWhiteSpace(sourceIdentity))
        {
            sourceIdentity = (viewModel.Document.ClassFullName ?? viewModel.Document.ClassName) + "|" + viewModel.Document.TargetPath;
        }

        var hash = ComputeStableHashHex(sourceIdentity);
        var baseName = viewModel.Document.ClassFullName ?? (viewModel.Document.ClassNamespace + "." + viewModel.Document.ClassName);
        var sanitizedBaseName = SanitizeHintSegment(baseName);
        return sanitizedBaseName + "." + hash + ".XamlSourceGen.g.cs";
    }

    private static string SanitizeHintSegment(string value)
    {
        if (string.IsNullOrWhiteSpace(value))
        {
            return "GeneratedXaml";
        }

        var chars = value.ToCharArray();
        for (var index = 0; index < chars.Length; index++)
        {
            var c = chars[index];
            if (!char.IsLetterOrDigit(c) && c != '_' && c != '.')
            {
                chars[index] = '_';
            }
        }

        return new string(chars);
    }

    private static string ComputeStableHashHex(string text)
    {
        unchecked
        {
            var hash = 2166136261u;
            foreach (var c in text)
            {
                hash ^= c;
                hash *= 16777619u;
            }

            return hash.ToString("x8");
        }
    }
