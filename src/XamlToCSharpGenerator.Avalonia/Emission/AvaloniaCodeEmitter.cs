    private static ImmutableArray<string> BuildRootHotReloadCollectionMembers(ResolvedObjectNode rootNode)
    {
        var members = new SortedSet<string>(StringComparer.Ordinal);

        foreach (var assignment in rootNode.PropertyElementAssignments)
        {
            if (!assignment.IsCollectionAdd && !assignment.IsDictionaryMerge)
            {
                continue;
            }

            var memberName = ExtractMemberName(assignment.PropertyName);
            if (!string.IsNullOrWhiteSpace(memberName))
            {
                members.Add(memberName);
            }
        }

        switch (rootNode.ChildAttachmentMode)
        {
            case ResolvedChildAttachmentMode.ChildrenCollection:
                members.Add("Children");
                break;
            case ResolvedChildAttachmentMode.ItemsCollection:
                members.Add("Items");
