using System;
using System.Collections.Generic;
using System.Collections.Immutable;
using System.Text;
using XamlToCSharpGenerator.Core.Abstractions;
using XamlToCSharpGenerator.Core.Models;

namespace XamlToCSharpGenerator.Avalonia.Emission;

public sealed class AvaloniaCodeEmitter : IXamlCodeEmitter
{
    private const string TopDownAttachValueToken = "__AXSG_VALUE__";
    private const string MarkupContextServiceProviderToken = "__AXSG_CTX_SERVICE_PROVIDER__";
    private const string MarkupContextRootObjectToken = "__AXSG_CTX_ROOT_OBJECT__";
    private const string MarkupContextIntermediateRootObjectToken = "__AXSG_CTX_INTERMEDIATE_ROOT_OBJECT__";
    private const string MarkupContextTargetObjectToken = "__AXSG_CTX_TARGET_OBJECT__";
    private const string MarkupContextTargetPropertyToken = "__AXSG_CTX_TARGET_PROPERTY__";
    private const string MarkupContextBaseUriToken = "__AXSG_CTX_BASE_URI__";
    private const string MarkupContextParentStackToken = "__AXSG_CTX_PARENT_STACK__";

    private static readonly IReadOnlyDictionary<string, string> EmptyNamedFieldMap =
        new Dictionary<string, string>(StringComparer.Ordinal);

    public (string HintName, string Source) Emit(ResolvedViewModel viewModel)
    {
        var document = viewModel.Document;
        var namespaceName = document.ClassNamespace;
        var className = document.ClassName;
        var escapedUri = Escape(viewModel.BuildUri);
        var emitHotReloadStateTracking = viewModel.Document.IsClassBacked && viewModel.EnableHotReload;
        var namedFieldMap = BuildNamedFieldMap(viewModel);
        var hotReloadCollectionMembers = ImmutableArray<string>.Empty;
        var hotReloadClrPropertyMembers = ImmutableArray<string>.Empty;
        var hotReloadAvaloniaPropertyMembers = ImmutableArray<string>.Empty;
        var hotReloadRootEventSubscriptions = ImmutableArray<ResolvedEventSubscription>.Empty;
        var hotReloadClearsRootCollection = false;

        if (emitHotReloadStateTracking)
        {
            hotReloadCollectionMembers = BuildRootHotReloadCollectionMembers(viewModel.RootObject);
            hotReloadClrPropertyMembers = BuildRootHotReloadClrPropertyMembers(viewModel.RootObject, namedFieldMap);
            hotReloadAvaloniaPropertyMembers = BuildRootHotReloadAvaloniaPropertyMembers(viewModel.RootObject);
            hotReloadRootEventSubscriptions = BuildRootHotReloadEventSubscriptions(viewModel.RootObject);
            hotReloadClearsRootCollection = ShouldClearRootSelfCollection(viewModel.RootObject);
        }

        var sourceBuilder = new StringBuilder();
        sourceBuilder.AppendLine("// <auto-generated />");
        if (viewModel.PassExecutionTrace.Length > 0)
        {
            foreach (var traceEntry in viewModel.PassExecutionTrace)
            {
                sourceBuilder.AppendLine("// pass: " + EscapeComment(traceEntry));
            }
        }
        sourceBuilder.AppendLine("#nullable enable");
        sourceBuilder.AppendLine("using global::Avalonia.Controls;");
        sourceBuilder.AppendLine("using global::Avalonia.Markup.Xaml;");
        sourceBuilder.AppendLine("using global::System.Runtime.CompilerServices;");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine($"namespace {namespaceName}");
        sourceBuilder.AppendLine("{");
        var classKeyword = viewModel.Document.IsClassBacked ? "partial class" : "static class";
        sourceBuilder.AppendLine($"    {viewModel.ClassModifier} {classKeyword} {className}");
        sourceBuilder.AppendLine("    {");

        if (viewModel.Document.IsClassBacked)
        {
            foreach (var namedElement in viewModel.NamedElements)
            {
                sourceBuilder.AppendLine($"        {namedElement.FieldModifier} {namedElement.TypeName}? {SanitizeIdentifier(namedElement.Name)};");
            }
        }

        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine("        [ModuleInitializer]");
        sourceBuilder.AppendLine("        internal static void __RegisterXamlSourceGenArtifacts()");
        sourceBuilder.AppendLine("        {");
        sourceBuilder.AppendLine(
            $"            global::XamlToCSharpGenerator.Runtime.XamlSourceGenRegistry.Unregister(\"{escapedUri}\");");
        sourceBuilder.AppendLine(
            $"            global::XamlToCSharpGenerator.Runtime.XamlSourceInfoRegistry.Clear(\"{escapedUri}\");");
        sourceBuilder.AppendLine(
            $"            global::XamlToCSharpGenerator.Runtime.XamlResourceRegistry.Clear(\"{escapedUri}\");");
        sourceBuilder.AppendLine(
            $"            global::XamlToCSharpGenerator.Runtime.XamlTemplateRegistry.Clear(\"{escapedUri}\");");
        sourceBuilder.AppendLine(
            $"            global::XamlToCSharpGenerator.Runtime.XamlStyleRegistry.Clear(\"{escapedUri}\");");
        sourceBuilder.AppendLine(
            $"            global::XamlToCSharpGenerator.Runtime.XamlControlThemeRegistry.Clear(\"{escapedUri}\");");
        sourceBuilder.AppendLine(
            $"            global::XamlToCSharpGenerator.Runtime.XamlIncludeRegistry.Clear(\"{escapedUri}\");");
        sourceBuilder.AppendLine(
            $"            global::XamlToCSharpGenerator.Runtime.XamlIncludeGraphRegistry.Clear(\"{escapedUri}\");");
        sourceBuilder.AppendLine(
            $"            global::XamlToCSharpGenerator.Runtime.XamlCompiledBindingRegistry.Clear(\"{escapedUri}\");");
        sourceBuilder.AppendLine($"            global::XamlToCSharpGenerator.Runtime.XamlSourceGenRegistry.Register(\"{escapedUri}\", static __serviceProvider =>");
        sourceBuilder.AppendLine("            {");
            sourceBuilder.AppendLine("                var __instance = __CreateRootInstance(__serviceProvider);");
            sourceBuilder.AppendLine("                __PopulateGeneratedObjectGraph(__instance, __serviceProvider);");
            sourceBuilder.AppendLine("                return __instance;");
        sourceBuilder.AppendLine("            });");

        if (viewModel.CreateSourceInfo)
        {
            EmitSourceInfoRegistrations(viewModel, sourceBuilder, escapedUri);
        }

        foreach (var resource in viewModel.Resources)
        {
            sourceBuilder.AppendLine(
                $"            global::XamlToCSharpGenerator.Runtime.XamlResourceRegistry.Register(\"{escapedUri}\", \"{Escape(resource.Key)}\", \"{Escape(resource.TypeName)}\", \"{Escape(resource.RawXaml)}\");");
        }

        foreach (var template in viewModel.Templates)
        {
            sourceBuilder.AppendLine(
                $"            global::XamlToCSharpGenerator.Runtime.XamlTemplateRegistry.Register(\"{escapedUri}\", \"{Escape(template.Kind)}\", {ToQuotedOrNull(template.Key)}, {ToQuotedOrNull(template.TargetTypeName)}, {ToQuotedOrNull(template.DataType)}, \"{Escape(template.RawXaml)}\");");
        }

        foreach (var style in viewModel.Styles)
        {
            sourceBuilder.AppendLine(
                $"            global::XamlToCSharpGenerator.Runtime.XamlStyleRegistry.Register(\"{escapedUri}\", {ToQuotedOrNull(style.Key)}, \"{Escape(style.Selector)}\", {ToQuotedOrNull(style.TargetTypeName)}, \"{Escape(style.RawXaml)}\");");
        }

        for (var controlThemeIndex = 0; controlThemeIndex < viewModel.ControlThemes.Length; controlThemeIndex++)
        {
            var controlTheme = viewModel.ControlThemes[controlThemeIndex];
            sourceBuilder.AppendLine(
                $"            global::XamlToCSharpGenerator.Runtime.XamlControlThemeRegistry.Register(\"{escapedUri}\", {ToQuotedOrNull(controlTheme.Key)}, {ToQuotedOrNull(controlTheme.TargetTypeName)}, {ToQuotedOrNull(controlTheme.BasedOn)}, {ToQuotedOrNull(controlTheme.ThemeVariant)}, \"{Escape(controlTheme.RawXaml)}\", static () => __BuildGeneratedControlTheme{controlThemeIndex}());");
        }

        foreach (var include in viewModel.Includes)
        {
            sourceBuilder.AppendLine(
                $"            global::XamlToCSharpGenerator.Runtime.XamlIncludeRegistry.Register(\"{escapedUri}\", \"{Escape(include.Kind)}\", \"{Escape(include.Source)}\", \"{Escape(include.MergeTarget)}\", {BoolLiteral(include.IsAbsoluteUri)}, \"{Escape(include.RawXaml)}\");");

            if (!string.IsNullOrWhiteSpace(include.ResolvedSourceUri) && include.IsProjectLocal)
            {
                sourceBuilder.AppendLine(
                    $"            global::XamlToCSharpGenerator.Runtime.XamlIncludeGraphRegistry.Register(\"{escapedUri}\", \"{Escape(include.ResolvedSourceUri!)}\", \"{Escape(include.MergeTarget)}\");");
            }
        }

        for (var index = 0; index < viewModel.CompiledBindings.Length; index++)
        {
            var compiledBinding = viewModel.CompiledBindings[index];
            sourceBuilder.AppendLine(
                $"            global::XamlToCSharpGenerator.Runtime.XamlCompiledBindingRegistry.Register(\"{escapedUri}\", \"{Escape(compiledBinding.TargetTypeName)}\", \"{Escape(compiledBinding.TargetPropertyName)}\", \"{Escape(compiledBinding.Path)}\", \"{Escape(compiledBinding.SourceTypeName)}\", static source => __CompiledBindingAccessor{index}(({compiledBinding.SourceTypeName})source));");
        }

        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine(
            $"        internal static void __PopulateGeneratedObjectGraph({viewModel.RootObject.TypeName} __root, global::System.IServiceProvider? __serviceProvider = null)");
        sourceBuilder.AppendLine("        {");
        if (viewModel.EmitNameScopeRegistration)
        {
            sourceBuilder.AppendLine("            var __nameScope = new global::Avalonia.Controls.NameScope();");
            sourceBuilder.AppendLine("            if (__root is global::Avalonia.StyledElement __rootStyledElement)");
            sourceBuilder.AppendLine("            {");
            sourceBuilder.AppendLine("                global::Avalonia.Controls.NameScope.SetNameScope(__rootStyledElement, __nameScope);");
            sourceBuilder.AppendLine("            }");
        }

        var nodeCounter = 0;
        EmitNode(
            viewModel.RootObject,
            sourceBuilder,
            ref nodeCounter,
            "            ",
            "__root",
            namedFieldMap,
            viewModel.EmitNameScopeRegistration,
            viewModel.EmitNameScopeRegistration ? "__nameScope" : null,
            "__root",
            topDownAttachmentTemplate: null,
            completeNameScopeOnNodeCompletion: viewModel.EmitNameScopeRegistration,
            serviceProviderReference: "__serviceProvider",
            baseUriExpression: "\"" + escapedUri + "\"",
            parentStackReferences: ImmutableArray<string>.Empty,
            intermediateRootReference: "__root");

        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine(
            $"        internal static {viewModel.RootObject.TypeName} __CreateRootInstance(global::System.IServiceProvider? __serviceProvider)");
        sourceBuilder.AppendLine("        {");
        sourceBuilder.AppendLine($"            return {BuildObjectCreationExpression(viewModel.RootObject, "__serviceProvider")};");
        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine($"        internal static {viewModel.RootObject.TypeName} __BuildGeneratedObjectGraph()");
        sourceBuilder.AppendLine("        {");
        sourceBuilder.AppendLine("            var __root = __CreateRootInstance(null);");
        sourceBuilder.AppendLine("            __PopulateGeneratedObjectGraph(__root, null);");
        sourceBuilder.AppendLine("            return __root;");

        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();

        if (viewModel.Document.IsClassBacked && viewModel.EnableHotReload)
        {
            sourceBuilder.AppendLine("        private static string[] __GetSourceGenHotReloadCollectionMembers()");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine($"            return {BuildStringArrayExpression(hotReloadCollectionMembers)};");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
            sourceBuilder.AppendLine("        private static string[] __GetSourceGenHotReloadClrPropertyMembers()");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine($"            return {BuildStringArrayExpression(hotReloadClrPropertyMembers)};");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
            sourceBuilder.AppendLine("        private static string[] __GetSourceGenHotReloadAvaloniaPropertyMembers()");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine($"            return {BuildStringArrayExpression(hotReloadAvaloniaPropertyMembers)};");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
            sourceBuilder.AppendLine(
                "        private static global::XamlToCSharpGenerator.Runtime.SourceGenHotReloadEventDescriptor[] __GetSourceGenHotReloadRootEventSubscriptions()");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine(
                $"            return {BuildHotReloadEventDescriptorArrayExpression(hotReloadRootEventSubscriptions)};");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
            sourceBuilder.AppendLine("        private static void __TrackAndReconcileSourceGenHotReloadState(object __instance)");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine("            global::XamlToCSharpGenerator.Runtime.XamlSourceGenHotReloadStateTracker.Reconcile(");
            sourceBuilder.AppendLine("                __instance,");
            sourceBuilder.AppendLine("                __GetSourceGenHotReloadCollectionMembers(),");
            sourceBuilder.AppendLine("                __GetSourceGenHotReloadClrPropertyMembers(),");
            sourceBuilder.AppendLine("                __GetSourceGenHotReloadAvaloniaPropertyMembers(),");
            sourceBuilder.AppendLine($"                {BoolLiteral(hotReloadClearsRootCollection)},");
            sourceBuilder.AppendLine("                __GetSourceGenHotReloadRootEventSubscriptions());");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
            sourceBuilder.AppendLine("        internal void __ApplySourceGenHotReload()");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine("            __RegisterXamlSourceGenArtifacts();");
            sourceBuilder.AppendLine("            __TrackAndReconcileSourceGenHotReloadState(this);");
            sourceBuilder.AppendLine("            __PopulateGeneratedObjectGraph(this, null);");
            sourceBuilder.AppendLine("            var __self = (object)this;");
            sourceBuilder.AppendLine("            if (__self is global::Avalonia.Layout.Layoutable __layoutable)");
            sourceBuilder.AppendLine("            {");
            sourceBuilder.AppendLine("                __layoutable.InvalidateMeasure();");
            sourceBuilder.AppendLine("                __layoutable.InvalidateArrange();");
            sourceBuilder.AppendLine("            }");
            sourceBuilder.AppendLine();
            sourceBuilder.AppendLine("            if (__self is global::Avalonia.Visual __visual)");
            sourceBuilder.AppendLine("            {");
            sourceBuilder.AppendLine("                __visual.InvalidateVisual();");
            sourceBuilder.AppendLine("            }");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
        }

        if (viewModel.CompiledBindings.Length > 0)
        {
            sourceBuilder.AppendLine();
        }

        for (var index = 0; index < viewModel.CompiledBindings.Length; index++)
        {
            var compiledBinding = viewModel.CompiledBindings[index];
            sourceBuilder.AppendLine($"        private static object? __CompiledBindingAccessor{index}({compiledBinding.SourceTypeName} source)");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine($"            return {compiledBinding.AccessorExpression};");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
        }

        for (var themeIndex = 0; themeIndex < viewModel.ControlThemes.Length; themeIndex++)
        {
            var controlTheme = viewModel.ControlThemes[themeIndex];
            sourceBuilder.AppendLine(
                $"        private static global::Avalonia.Styling.ControlTheme __BuildGeneratedControlTheme{themeIndex}()");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine("            var __theme = new global::Avalonia.Styling.ControlTheme();");

            if (!string.IsNullOrWhiteSpace(controlTheme.TargetTypeName))
            {
                sourceBuilder.AppendLine(
                    $"            __theme.TargetType = typeof({controlTheme.TargetTypeName});");
            }

            for (var setterIndex = 0; setterIndex < controlTheme.Setters.Length; setterIndex++)
            {
                var setter = controlTheme.Setters[setterIndex];
                if (string.IsNullOrWhiteSpace(setter.AvaloniaPropertyOwnerTypeName) ||
                    string.IsNullOrWhiteSpace(setter.AvaloniaPropertyFieldName))
                {
                    continue;
                }

                sourceBuilder.AppendLine(
                    $"            __theme.Setters.Add(new global::Avalonia.Styling.Setter({setter.AvaloniaPropertyOwnerTypeName}.{setter.AvaloniaPropertyFieldName}, {setter.ValueExpression}));");
            }

            sourceBuilder.AppendLine("            return __theme;");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
        }

        if (viewModel.EmitStaticResourceResolver)
        {
            sourceBuilder.AppendLine("        private static object? __ResolveStaticResource(object? anchor, object key)");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine(
                $"            return global::XamlToCSharpGenerator.Runtime.SourceGenStaticResourceResolver.Resolve(anchor, key, \"{escapedUri}\");");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
        }

        sourceBuilder.AppendLine("        private static void __TryClearCollection(object? value)");
        sourceBuilder.AppendLine("        {");
        sourceBuilder.AppendLine("            switch (value)");
        sourceBuilder.AppendLine("            {");
        sourceBuilder.AppendLine("                case null:");
        sourceBuilder.AppendLine("                    return;");
        sourceBuilder.AppendLine("                case global::Avalonia.Controls.IResourceDictionary resourceDictionary:");
        sourceBuilder.AppendLine("                    try");
        sourceBuilder.AppendLine("                    {");
        sourceBuilder.AppendLine("                        resourceDictionary.Clear();");
        sourceBuilder.AppendLine("                    }");
        sourceBuilder.AppendLine("                    catch (global::System.NotSupportedException)");
        sourceBuilder.AppendLine("                    {");
        sourceBuilder.AppendLine("                    }");
        sourceBuilder.AppendLine("                    __TryClearCollection(resourceDictionary.MergedDictionaries);");
        sourceBuilder.AppendLine("                    __TryClearCollection(resourceDictionary.ThemeDictionaries);");
        sourceBuilder.AppendLine("                    return;");
        sourceBuilder.AppendLine("                case global::System.Collections.IDictionary dictionary:");
        sourceBuilder.AppendLine("                    try");
        sourceBuilder.AppendLine("                    {");
        sourceBuilder.AppendLine("                        dictionary.Clear();");
        sourceBuilder.AppendLine("                    }");
        sourceBuilder.AppendLine("                    catch (global::System.NotSupportedException)");
        sourceBuilder.AppendLine("                    {");
        sourceBuilder.AppendLine("                    }");
        sourceBuilder.AppendLine("                    return;");
        sourceBuilder.AppendLine("                case global::System.Collections.IList list:");
        sourceBuilder.AppendLine("                    try");
        sourceBuilder.AppendLine("                    {");
        sourceBuilder.AppendLine("                        if (list.IsReadOnly || list.IsFixedSize)");
        sourceBuilder.AppendLine("                        {");
        sourceBuilder.AppendLine("                            return;");
        sourceBuilder.AppendLine("                        }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine("                        list.Clear();");
        sourceBuilder.AppendLine("                    }");
        sourceBuilder.AppendLine("                    catch (global::System.InvalidOperationException)");
        sourceBuilder.AppendLine("                    {");
        sourceBuilder.AppendLine("                    }");
        sourceBuilder.AppendLine("                    catch (global::System.NotSupportedException)");
        sourceBuilder.AppendLine("                    {");
        sourceBuilder.AppendLine("                    }");
        sourceBuilder.AppendLine("                    return;");
        sourceBuilder.AppendLine("            }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine("            __TryInvokeClearMethod(value);");
        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine("        private static void __TryInvokeClearMethod(object? value)");
        sourceBuilder.AppendLine("        {");
        sourceBuilder.AppendLine("            if (value is null)");
        sourceBuilder.AppendLine("            {");
        sourceBuilder.AppendLine("                return;");
        sourceBuilder.AppendLine("            }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine("            var __type = value.GetType();");
        sourceBuilder.AppendLine("            var __methods = __type.GetMethods(global::System.Reflection.BindingFlags.Instance | global::System.Reflection.BindingFlags.Public | global::System.Reflection.BindingFlags.NonPublic);");
        sourceBuilder.AppendLine("            for (var __index = 0; __index < __methods.Length; __index++)");
        sourceBuilder.AppendLine("            {");
        sourceBuilder.AppendLine("                var __method = __methods[__index];");
        sourceBuilder.AppendLine("                if (!string.Equals(__method.Name, \"Clear\", global::System.StringComparison.Ordinal) ||");
        sourceBuilder.AppendLine("                    __method.IsStatic ||");
        sourceBuilder.AppendLine("                    __method.IsAbstract ||");
        sourceBuilder.AppendLine("                    __method.ReturnType != typeof(void) ||");
        sourceBuilder.AppendLine("                    __method.GetParameters().Length != 0)");
        sourceBuilder.AppendLine("                {");
        sourceBuilder.AppendLine("                    continue;");
        sourceBuilder.AppendLine("                }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine("                try");
        sourceBuilder.AppendLine("                {");
        sourceBuilder.AppendLine("                    __method.Invoke(value, null);");
        sourceBuilder.AppendLine("                }");
        sourceBuilder.AppendLine("                catch");
        sourceBuilder.AppendLine("                {");
        sourceBuilder.AppendLine("                }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine("                return;");
        sourceBuilder.AppendLine("            }");
        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine("        private static void __TryMergeDictionary(object? destination, object? source)");
        sourceBuilder.AppendLine("        {");
        sourceBuilder.AppendLine("            if (destination is not global::System.Collections.IDictionary destinationDictionary ||");
        sourceBuilder.AppendLine("                source is not global::System.Collections.IDictionary sourceDictionary)");
        sourceBuilder.AppendLine("            {");
        sourceBuilder.AppendLine("                return;");
        sourceBuilder.AppendLine("            }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine("            foreach (global::System.Collections.DictionaryEntry entry in sourceDictionary)");
        sourceBuilder.AppendLine("            {");
        sourceBuilder.AppendLine("                destinationDictionary[entry.Key] = entry.Value;");
        sourceBuilder.AppendLine("            }");
        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine("        private static void __BeginInit(object? value)");
        sourceBuilder.AppendLine("        {");
        sourceBuilder.AppendLine("            if (value is global::System.ComponentModel.ISupportInitialize initialize)");
        sourceBuilder.AppendLine("            {");
        sourceBuilder.AppendLine("                initialize.BeginInit();");
        sourceBuilder.AppendLine("            }");
        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine("        private static void __EndInit(object? value)");
        sourceBuilder.AppendLine("        {");
        sourceBuilder.AppendLine("            if (value is global::System.ComponentModel.ISupportInitialize initialize)");
        sourceBuilder.AppendLine("            {");
        sourceBuilder.AppendLine("                initialize.EndInit();");
        sourceBuilder.AppendLine("            }");
        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();
        sourceBuilder.AppendLine("        private static void __TryCompleteNameScope(object? scope)");
        sourceBuilder.AppendLine("        {");
        sourceBuilder.AppendLine("            if (scope is global::Avalonia.Controls.INameScope nameScope && !nameScope.IsCompleted)");
        sourceBuilder.AppendLine("            {");
        sourceBuilder.AppendLine("                nameScope.Complete();");
        sourceBuilder.AppendLine("            }");
        sourceBuilder.AppendLine("        }");
        sourceBuilder.AppendLine();

        if (viewModel.Document.IsClassBacked)
        {
            sourceBuilder.AppendLine("        public void InitializeComponent(bool loadXaml = true)");
            sourceBuilder.AppendLine("            {");
            sourceBuilder.AppendLine("                var __loadedWithSourceGen = false;");
            sourceBuilder.AppendLine("                if (loadXaml)");
            sourceBuilder.AppendLine("                {");
            sourceBuilder.AppendLine("                    __PopulateGeneratedObjectGraph(this, null);");
            sourceBuilder.AppendLine("                    __loadedWithSourceGen = true;");
            sourceBuilder.AppendLine("                }");
            sourceBuilder.AppendLine();
            sourceBuilder.AppendLine("                if (!__loadedWithSourceGen)");
            sourceBuilder.AppendLine("                {");
            foreach (var namedElement in viewModel.NamedElements)
            {
                sourceBuilder.AppendLine(
                    $"                    {SanitizeIdentifier(namedElement.Name)} = this.FindNameScope()?.Find<{namedElement.TypeName}>(\"{Escape(namedElement.Name)}\");");
            }
            sourceBuilder.AppendLine("                }");
            sourceBuilder.AppendLine();
            if (viewModel.EnableHotReload)
            {
                var escapedSourcePath = Escape(viewModel.Document.FilePath);
                sourceBuilder.AppendLine("                if (__loadedWithSourceGen)");
                sourceBuilder.AppendLine("                {");
                sourceBuilder.AppendLine("                    __TrackAndReconcileSourceGenHotReloadState(this);");
                sourceBuilder.AppendLine(
                    $"                    global::XamlToCSharpGenerator.Runtime.XamlSourceGenHotReloadManager.Register(this, static __instance => (({viewModel.RootObject.TypeName})__instance).__ApplySourceGenHotReload(), new global::XamlToCSharpGenerator.Runtime.SourceGenHotReloadRegistrationOptions");
                sourceBuilder.AppendLine("                    {");
                sourceBuilder.AppendLine($"                        SourcePath = \"{escapedSourcePath}\",");
                sourceBuilder.AppendLine("                        CaptureState = static __instance =>");
                sourceBuilder.AppendLine("                            __instance is global::Avalonia.StyledElement __styledElement");
                sourceBuilder.AppendLine("                                ? __styledElement.DataContext");
                sourceBuilder.AppendLine("                                : null,");
                sourceBuilder.AppendLine("                        RestoreState = static (__instance, __state) =>");
                sourceBuilder.AppendLine("                        {");
                sourceBuilder.AppendLine("                            if (__instance is global::Avalonia.StyledElement __styledElement &&");
                sourceBuilder.AppendLine("                                __styledElement.DataContext is null)");
                sourceBuilder.AppendLine("                            {");
                sourceBuilder.AppendLine("                                __styledElement.DataContext = __state;");
                sourceBuilder.AppendLine("                            }");
                sourceBuilder.AppendLine("                        }");
                sourceBuilder.AppendLine("                    });");
                sourceBuilder.AppendLine("                }");
            }

            sourceBuilder.AppendLine("            }");
        }
        sourceBuilder.AppendLine("    }");
        sourceBuilder.AppendLine("}");

        var hintName = BuildHintName(viewModel);
        return (hintName, sourceBuilder.ToString());
    }

    private static string EmitNode(
        ResolvedObjectNode node,
        StringBuilder sourceBuilder,
        ref int nodeCounter,
        string indent,
        string rootReference,
        IReadOnlyDictionary<string, string> namedFieldMap,
        bool emitNameScopeRegistration,
        string? nameScopeReference,
        string? existingVariableName = null,
        string? topDownAttachmentTemplate = null,
        bool completeNameScopeOnNodeCompletion = false,
        string serviceProviderReference = "__serviceProvider",
        string baseUriExpression = "null",
        ImmutableArray<string> parentStackReferences = default,
        string? intermediateRootReference = null)
    {
        var variableName = existingVariableName ?? ("__n" + nodeCounter.ToString());
        intermediateRootReference ??= variableName;
        var nodeParentStackReferences = ExtendParentStack(parentStackReferences, variableName);
        var nodeParentStackExpression = BuildParentStackExpression(nodeParentStackReferences);

        if (TryEmitDeferredTemplateNode(
                node,
                sourceBuilder,
                ref nodeCounter,
                indent,
                rootReference,
                variableName,
                namedFieldMap,
                emitNameScopeRegistration,
                nameScopeReference,
                existingVariableName is null,
                topDownAttachmentTemplate,
                completeNameScopeOnNodeCompletion,
                serviceProviderReference,
                baseUriExpression,
                nodeParentStackReferences,
                intermediateRootReference))
        {
            return variableName;
        }

        if (existingVariableName is null)
        {
            nodeCounter++;
            sourceBuilder.AppendLine($"{indent}var {variableName} = {BuildObjectCreationExpression(node, serviceProviderReference)};");
        }

        sourceBuilder.AppendLine($"{indent}__BeginInit({variableName});");
        if (node.UseTopDownInitialization &&
            !string.IsNullOrWhiteSpace(topDownAttachmentTemplate))
        {
            sourceBuilder.AppendLine($"{indent}{ExpandTopDownAttachmentTemplate(topDownAttachmentTemplate!, variableName)}");
        }

        if (!string.IsNullOrWhiteSpace(node.Name))
        {
            if (namedFieldMap.TryGetValue(node.Name!, out var fieldName))
            {
                sourceBuilder.AppendLine($"{indent}{rootReference}.{fieldName} = {variableName};");
            }

            if (emitNameScopeRegistration &&
                !string.IsNullOrWhiteSpace(nameScopeReference))
            {
                sourceBuilder.AppendLine(
                    $"{indent}{nameScopeReference}.Register(\"{Escape(node.Name!)}\", {variableName});");
            }
        }

        foreach (var assignment in node.PropertyAssignments)
        {
            var targetPropertyExpression = BuildTargetPropertyExpression(assignment);
            var valueExpression = ExpandMarkupContextExpression(
                assignment.ValueExpression,
                serviceProviderReference,
                rootReference,
                intermediateRootReference,
                variableName,
                targetPropertyExpression,
                baseUriExpression,
                nodeParentStackExpression);

            if (string.IsNullOrWhiteSpace(assignment.AvaloniaPropertyOwnerTypeName) ||
                string.IsNullOrWhiteSpace(assignment.AvaloniaPropertyFieldName))
            {
                sourceBuilder.AppendLine($"{indent}{variableName}.{assignment.PropertyName} = {valueExpression};");
            }
            else
            {
                if (LooksLikeBindingExpression(valueExpression))
                {
                    sourceBuilder.AppendLine(
                        $"{indent}{variableName}[new global::Avalonia.Data.IndexerDescriptor {{ Property = {assignment.AvaloniaPropertyOwnerTypeName}.{assignment.AvaloniaPropertyFieldName} }}] = {valueExpression};");
                    continue;
                }

                if (string.IsNullOrWhiteSpace(assignment.BindingPriorityExpression))
                {
                    sourceBuilder.AppendLine(
                        $"{indent}{variableName}.SetValue({assignment.AvaloniaPropertyOwnerTypeName}.{assignment.AvaloniaPropertyFieldName}, {valueExpression});");
                }
                else
                {
                    sourceBuilder.AppendLine(
                        $"{indent}{variableName}.SetValue({assignment.AvaloniaPropertyOwnerTypeName}.{assignment.AvaloniaPropertyFieldName}, {valueExpression}, {assignment.BindingPriorityExpression});");
                }
            }
        }

        foreach (var eventSubscription in node.EventSubscriptions)
        {
            EmitEventSubscription(
                sourceBuilder,
                indent,
                variableName,
                rootReference,
                eventSubscription);
        }

        foreach (var propertyElementAssignment in node.PropertyElementAssignments)
        {
            if (propertyElementAssignment.IsCollectionAdd)
            {
                sourceBuilder.AppendLine($"{indent}__TryClearCollection({variableName}.{propertyElementAssignment.PropertyName});");
                foreach (var value in propertyElementAssignment.ObjectValues)
                {
                    var inlineAddTemplate = $"{variableName}.{propertyElementAssignment.PropertyName}.Add({TopDownAttachValueToken});";
                    var valueVariable = EmitNode(
                        value,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: inlineAddTemplate,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference);
                    if (!value.UseTopDownInitialization)
                    {
                        sourceBuilder.AppendLine($"{indent}{variableName}.{propertyElementAssignment.PropertyName}.Add({valueVariable});");
                    }
                }

                continue;
            }

            if (propertyElementAssignment.IsDictionaryMerge)
            {
                if (TryEmitDictionaryMergePropertyElement(
                        propertyElementAssignment,
                        variableName,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference,
                        baseUriExpression,
                        nodeParentStackReferences,
                        intermediateRootReference))
                {
                    continue;
                }
            }

            if (propertyElementAssignment.ObjectValues.Length == 0)
            {
                continue;
            }

            string assignmentTemplate;
            if (string.IsNullOrWhiteSpace(propertyElementAssignment.AvaloniaPropertyOwnerTypeName) ||
                string.IsNullOrWhiteSpace(propertyElementAssignment.AvaloniaPropertyFieldName))
            {
                assignmentTemplate = $"{variableName}.{propertyElementAssignment.PropertyName} = {TopDownAttachValueToken};";
            }
            else if (string.IsNullOrWhiteSpace(propertyElementAssignment.BindingPriorityExpression))
            {
                assignmentTemplate =
                    $"{variableName}.SetValue({propertyElementAssignment.AvaloniaPropertyOwnerTypeName}.{propertyElementAssignment.AvaloniaPropertyFieldName}, {TopDownAttachValueToken});";
            }
            else
            {
                assignmentTemplate =
                    $"{variableName}.SetValue({propertyElementAssignment.AvaloniaPropertyOwnerTypeName}.{propertyElementAssignment.AvaloniaPropertyFieldName}, {TopDownAttachValueToken}, {propertyElementAssignment.BindingPriorityExpression});";
            }

            var assignedNode = propertyElementAssignment.ObjectValues[0];
            var assignedValue = EmitNode(
                assignedNode,
                sourceBuilder,
                ref nodeCounter,
                indent,
                rootReference,
                namedFieldMap,
                emitNameScopeRegistration,
                nameScopeReference,
                serviceProviderReference: serviceProviderReference,
                topDownAttachmentTemplate: assignmentTemplate,
                baseUriExpression: baseUriExpression,
                parentStackReferences: nodeParentStackReferences,
                intermediateRootReference: intermediateRootReference);
            if (assignedNode.UseTopDownInitialization)
            {
                continue;
            }

            if (string.IsNullOrWhiteSpace(propertyElementAssignment.AvaloniaPropertyOwnerTypeName) ||
                string.IsNullOrWhiteSpace(propertyElementAssignment.AvaloniaPropertyFieldName))
            {
                sourceBuilder.AppendLine($"{indent}{variableName}.{propertyElementAssignment.PropertyName} = {assignedValue};");
            }
            else
            {
                if (string.IsNullOrWhiteSpace(propertyElementAssignment.BindingPriorityExpression))
                {
                    sourceBuilder.AppendLine(
                        $"{indent}{variableName}.SetValue({propertyElementAssignment.AvaloniaPropertyOwnerTypeName}.{propertyElementAssignment.AvaloniaPropertyFieldName}, {assignedValue});");
                }
                else
                {
                    sourceBuilder.AppendLine(
                        $"{indent}{variableName}.SetValue({propertyElementAssignment.AvaloniaPropertyOwnerTypeName}.{propertyElementAssignment.AvaloniaPropertyFieldName}, {assignedValue}, {propertyElementAssignment.BindingPriorityExpression});");
                }
            }
        }

        switch (node.ChildAttachmentMode)
        {
            case ResolvedChildAttachmentMode.Content:
            {
                var contentPropertyName = string.IsNullOrWhiteSpace(node.ContentPropertyName)
                    ? "Content"
                    : node.ContentPropertyName!;
                var shouldResetImplicitContent =
                    existingVariableName is not null &&
                    string.IsNullOrWhiteSpace(topDownAttachmentTemplate);
                if (node.Children.Length > 0)
                {
                    var firstChildNode = node.Children[0];
                    var firstChild = EmitNode(
                        firstChildNode,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: $"{variableName}.{contentPropertyName} = {TopDownAttachValueToken};",
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference);
                    if (!firstChildNode.UseTopDownInitialization)
                    {
                        sourceBuilder.AppendLine($"{indent}{variableName}.{contentPropertyName} = {firstChild};");
                    }
                }
                else if (shouldResetImplicitContent && !HasExplicitContentValue(node, contentPropertyName))
                {
                    sourceBuilder.AppendLine($"{indent}{variableName}.{contentPropertyName} = default!;");
                }

                break;
            }
            case ResolvedChildAttachmentMode.ChildrenCollection:
            {
                sourceBuilder.AppendLine($"{indent}__TryClearCollection({variableName}.Children);");
                foreach (var child in node.Children)
                {
                    var inlineAddTemplate = $"{variableName}.Children.Add({TopDownAttachValueToken});";
                    var childVariable = EmitNode(
                        child,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: inlineAddTemplate,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference);
                    if (!child.UseTopDownInitialization)
                    {
                        sourceBuilder.AppendLine($"{indent}{variableName}.Children.Add({childVariable});");
                    }
                }

                break;
            }
            case ResolvedChildAttachmentMode.ItemsCollection:
            {
                sourceBuilder.AppendLine($"{indent}__TryClearCollection({variableName}.Items);");
                foreach (var child in node.Children)
                {
                    var inlineAddTemplate = $"{variableName}.Items.Add({TopDownAttachValueToken});";
                    var childVariable = EmitNode(
                        child,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: inlineAddTemplate,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference);
                    if (!child.UseTopDownInitialization)
                    {
                        sourceBuilder.AppendLine($"{indent}{variableName}.Items.Add({childVariable});");
                    }
                }

                break;
            }
            case ResolvedChildAttachmentMode.DirectAdd:
            {
                sourceBuilder.AppendLine($"{indent}__TryClearCollection({variableName});");
                foreach (var child in node.Children)
                {
                    var inlineAddTemplate = $"{variableName}.Add({TopDownAttachValueToken});";
                    var childVariable = EmitNode(
                        child,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: inlineAddTemplate,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference);
                    if (!child.UseTopDownInitialization)
                    {
                        sourceBuilder.AppendLine($"{indent}{variableName}.Add({childVariable});");
                    }
                }

                break;
            }
            case ResolvedChildAttachmentMode.DictionaryAdd:
            {
                sourceBuilder.AppendLine($"{indent}__TryClearCollection({variableName});");
                foreach (var child in node.Children)
                {
                    if (string.IsNullOrWhiteSpace(child.KeyExpression))
                    {
                        continue;
                    }

                    var childVariable = EmitNode(
                        child,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: $"{variableName}.Add({child.KeyExpression}, {TopDownAttachValueToken});",
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference);
                    if (!child.UseTopDownInitialization)
                    {
                        sourceBuilder.AppendLine($"{indent}{variableName}.Add({child.KeyExpression}, {childVariable});");
                    }
                }

                break;
            }
        }

        if (completeNameScopeOnNodeCompletion &&
            !string.IsNullOrWhiteSpace(nameScopeReference))
        {
            sourceBuilder.AppendLine($"{indent}__TryCompleteNameScope({nameScopeReference});");
        }

        sourceBuilder.AppendLine($"{indent}__EndInit({variableName});");
        return variableName;
    }

    private static bool TryEmitDeferredTemplateNode(
        ResolvedObjectNode node,
        StringBuilder sourceBuilder,
        ref int nodeCounter,
        string indent,
        string rootReference,
        string variableName,
        IReadOnlyDictionary<string, string> namedFieldMap,
        bool emitNameScopeRegistration,
        string? nameScopeReference,
        bool shouldCreateVariable,
        string? topDownAttachmentTemplate,
        bool completeNameScopeOnNodeCompletion,
        string serviceProviderReference,
        string baseUriExpression,
        ImmutableArray<string> parentStackReferences,
        string intermediateRootReference)
    {
        if (!IsDeferredTemplateNode(node))
        {
            return false;
        }

        var nodeParentStackReferences = ExtendParentStack(parentStackReferences, variableName);
        var nodeParentStackExpression = BuildParentStackExpression(nodeParentStackReferences);

        if (shouldCreateVariable)
        {
            nodeCounter++;
            sourceBuilder.AppendLine($"{indent}var {variableName} = {BuildObjectCreationExpression(node, serviceProviderReference)};");
        }

        sourceBuilder.AppendLine($"{indent}__BeginInit({variableName});");
        if (node.UseTopDownInitialization &&
            !string.IsNullOrWhiteSpace(topDownAttachmentTemplate))
        {
            sourceBuilder.AppendLine($"{indent}{ExpandTopDownAttachmentTemplate(topDownAttachmentTemplate!, variableName)}");
        }

        if (!string.IsNullOrWhiteSpace(node.Name))
        {
            if (namedFieldMap.TryGetValue(node.Name!, out var fieldName))
            {
                sourceBuilder.AppendLine($"{indent}{rootReference}.{fieldName} = {variableName};");
            }

            if (emitNameScopeRegistration &&
                !string.IsNullOrWhiteSpace(nameScopeReference))
            {
                sourceBuilder.AppendLine(
                    $"{indent}{nameScopeReference}.Register(\"{Escape(node.Name!)}\", {variableName});");
            }
        }

        foreach (var assignment in node.PropertyAssignments)
        {
            if (IsContentPropertyName(assignment.PropertyName))
            {
                continue;
            }

            var targetPropertyExpression = BuildTargetPropertyExpression(assignment);
            var valueExpression = ExpandMarkupContextExpression(
                assignment.ValueExpression,
                serviceProviderReference,
                rootReference,
                intermediateRootReference,
                variableName,
                targetPropertyExpression,
                baseUriExpression,
                nodeParentStackExpression);

            if (string.IsNullOrWhiteSpace(assignment.AvaloniaPropertyOwnerTypeName) ||
                string.IsNullOrWhiteSpace(assignment.AvaloniaPropertyFieldName))
            {
                sourceBuilder.AppendLine($"{indent}{variableName}.{assignment.PropertyName} = {valueExpression};");
            }
            else
            {
                if (LooksLikeBindingExpression(valueExpression))
                {
                    sourceBuilder.AppendLine(
                        $"{indent}{variableName}[new global::Avalonia.Data.IndexerDescriptor {{ Property = {assignment.AvaloniaPropertyOwnerTypeName}.{assignment.AvaloniaPropertyFieldName} }}] = {valueExpression};");
                    continue;
                }

                if (string.IsNullOrWhiteSpace(assignment.BindingPriorityExpression))
                {
                    sourceBuilder.AppendLine(
                        $"{indent}{variableName}.SetValue({assignment.AvaloniaPropertyOwnerTypeName}.{assignment.AvaloniaPropertyFieldName}, {valueExpression});");
                }
                else
                {
                    sourceBuilder.AppendLine(
                        $"{indent}{variableName}.SetValue({assignment.AvaloniaPropertyOwnerTypeName}.{assignment.AvaloniaPropertyFieldName}, {valueExpression}, {assignment.BindingPriorityExpression});");
                }
            }
        }

        foreach (var eventSubscription in node.EventSubscriptions)
        {
            EmitEventSubscription(
                sourceBuilder,
                indent,
                variableName,
                rootReference,
                eventSubscription);
        }

        foreach (var propertyElementAssignment in node.PropertyElementAssignments)
        {
            if (IsContentPropertyName(propertyElementAssignment.PropertyName))
            {
                continue;
            }

            if (propertyElementAssignment.IsCollectionAdd)
            {
                sourceBuilder.AppendLine($"{indent}__TryClearCollection({variableName}.{propertyElementAssignment.PropertyName});");
                foreach (var value in propertyElementAssignment.ObjectValues)
                {
                    var inlineAddTemplate = $"{variableName}.{propertyElementAssignment.PropertyName}.Add({TopDownAttachValueToken});";
                    var valueVariable = EmitNode(
                        value,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference: serviceProviderReference,
                        topDownAttachmentTemplate: inlineAddTemplate,
                        baseUriExpression: baseUriExpression,
                        parentStackReferences: nodeParentStackReferences,
                        intermediateRootReference: intermediateRootReference);
                    if (!value.UseTopDownInitialization)
                    {
                        sourceBuilder.AppendLine($"{indent}{variableName}.{propertyElementAssignment.PropertyName}.Add({valueVariable});");
                    }
                }

                continue;
            }

            if (propertyElementAssignment.IsDictionaryMerge)
            {
                if (TryEmitDictionaryMergePropertyElement(
                        propertyElementAssignment,
                        variableName,
                        sourceBuilder,
                        ref nodeCounter,
                        indent,
                        rootReference,
                        namedFieldMap,
                        emitNameScopeRegistration,
                        nameScopeReference,
                        serviceProviderReference,
                        baseUriExpression,
                        nodeParentStackReferences,
                        intermediateRootReference))
                {
                    continue;
                }
            }

            if (propertyElementAssignment.ObjectValues.Length == 0)
            {
                continue;
            }

            string assignmentTemplate;
            if (string.IsNullOrWhiteSpace(propertyElementAssignment.AvaloniaPropertyOwnerTypeName) ||
                string.IsNullOrWhiteSpace(propertyElementAssignment.AvaloniaPropertyFieldName))
            {
                assignmentTemplate = $"{variableName}.{propertyElementAssignment.PropertyName} = {TopDownAttachValueToken};";
            }
            else if (string.IsNullOrWhiteSpace(propertyElementAssignment.BindingPriorityExpression))
            {
                assignmentTemplate =
                    $"{variableName}.SetValue({propertyElementAssignment.AvaloniaPropertyOwnerTypeName}.{propertyElementAssignment.AvaloniaPropertyFieldName}, {TopDownAttachValueToken});";
            }
            else
            {
                assignmentTemplate =
                    $"{variableName}.SetValue({propertyElementAssignment.AvaloniaPropertyOwnerTypeName}.{propertyElementAssignment.AvaloniaPropertyFieldName}, {TopDownAttachValueToken}, {propertyElementAssignment.BindingPriorityExpression});";
            }

            var assignedNode = propertyElementAssignment.ObjectValues[0];
            var assignedValue = EmitNode(
                assignedNode,
                sourceBuilder,
                ref nodeCounter,
                indent,
                rootReference,
                namedFieldMap,
                emitNameScopeRegistration,
                nameScopeReference,
                serviceProviderReference: serviceProviderReference,
                topDownAttachmentTemplate: assignmentTemplate,
                baseUriExpression: baseUriExpression,
                parentStackReferences: nodeParentStackReferences,
                intermediateRootReference: intermediateRootReference);
            if (assignedNode.UseTopDownInitialization)
            {
                continue;
            }

            if (string.IsNullOrWhiteSpace(propertyElementAssignment.AvaloniaPropertyOwnerTypeName) ||
                string.IsNullOrWhiteSpace(propertyElementAssignment.AvaloniaPropertyFieldName))
            {
                sourceBuilder.AppendLine($"{indent}{variableName}.{propertyElementAssignment.PropertyName} = {assignedValue};");
            }
            else
            {
                if (string.IsNullOrWhiteSpace(propertyElementAssignment.BindingPriorityExpression))
                {
                    sourceBuilder.AppendLine(
                        $"{indent}{variableName}.SetValue({propertyElementAssignment.AvaloniaPropertyOwnerTypeName}.{propertyElementAssignment.AvaloniaPropertyFieldName}, {assignedValue});");
                }
                else
                {
                    sourceBuilder.AppendLine(
                        $"{indent}{variableName}.SetValue({propertyElementAssignment.AvaloniaPropertyOwnerTypeName}.{propertyElementAssignment.AvaloniaPropertyFieldName}, {assignedValue}, {propertyElementAssignment.BindingPriorityExpression});");
                }
            }
        }

        var contentNode = TryGetTemplateContentNode(node);
        var templateContentPropertyName = string.IsNullOrWhiteSpace(node.ContentPropertyName)
            ? "Content"
            : node.ContentPropertyName!;
        if (contentNode is not null)
        {
            var factoryIndent = indent + "    ";
            var templateScopeName = "__templateScope" + nodeCounter.ToString();
            var templateProviderName = "__templateProvider" + nodeCounter.ToString();
            sourceBuilder.AppendLine(
                $"{indent}{variableName}.{templateContentPropertyName} = (global::System.Func<global::System.IServiceProvider?, object?>)(__templateServiceProvider =>");
            sourceBuilder.AppendLine($"{indent}{{");
            sourceBuilder.AppendLine(
                $"{factoryIndent}var {templateScopeName} = global::XamlToCSharpGenerator.Runtime.SourceGenDeferredServiceProviderFactory.CreateTemplateNameScope(__templateServiceProvider);");
            sourceBuilder.AppendLine(
                $"{factoryIndent}var {templateProviderName} = global::XamlToCSharpGenerator.Runtime.SourceGenDeferredServiceProviderFactory.CreateDeferredTemplateServiceProvider(__templateServiceProvider, {rootReference}, {templateScopeName});");
            var templateRoot = EmitNode(
                contentNode,
                sourceBuilder,
                ref nodeCounter,
                factoryIndent,
                rootReference,
                EmptyNamedFieldMap,
                emitNameScopeRegistration: true,
                nameScopeReference: templateScopeName,
                completeNameScopeOnNodeCompletion: true,
                serviceProviderReference: templateProviderName,
                baseUriExpression: baseUriExpression,
                parentStackReferences: nodeParentStackReferences,
                intermediateRootReference: variableName);
            sourceBuilder.AppendLine($"{factoryIndent}if ({templateRoot} is global::Avalonia.StyledElement __templateStyledElement)");
            sourceBuilder.AppendLine($"{factoryIndent}{{");
            sourceBuilder.AppendLine(
                $"{factoryIndent}    global::Avalonia.Controls.NameScope.SetNameScope(__templateStyledElement, {templateScopeName});");
            sourceBuilder.AppendLine($"{factoryIndent}}}");
            sourceBuilder.AppendLine(
                $"{factoryIndent}return new global::Avalonia.Controls.Templates.TemplateResult<global::Avalonia.Controls.Control>((global::Avalonia.Controls.Control){templateRoot}, {templateScopeName});");
            sourceBuilder.AppendLine($"{indent}}});");
        }

        if (completeNameScopeOnNodeCompletion &&
            !string.IsNullOrWhiteSpace(nameScopeReference))
        {
            sourceBuilder.AppendLine($"{indent}__TryCompleteNameScope({nameScopeReference});");
        }

        sourceBuilder.AppendLine($"{indent}__EndInit({variableName});");
        return true;
    }

    private static void EmitEventSubscription(
        StringBuilder sourceBuilder,
        string indent,
        string variableName,
        string rootReference,
        ResolvedEventSubscription eventSubscription)
    {
        if (eventSubscription.Kind == ResolvedEventSubscriptionKind.RoutedEvent &&
            !string.IsNullOrWhiteSpace(eventSubscription.RoutedEventOwnerTypeName) &&
            !string.IsNullOrWhiteSpace(eventSubscription.RoutedEventFieldName))
        {
            var routedEventReference = eventSubscription.RoutedEventOwnerTypeName + "." + eventSubscription.RoutedEventFieldName;
            var handlerExpression = string.IsNullOrWhiteSpace(eventSubscription.RoutedEventHandlerTypeName)
                ? rootReference + "." + eventSubscription.HandlerMethodName
                : "(" + eventSubscription.RoutedEventHandlerTypeName + ")" + rootReference + "." + eventSubscription.HandlerMethodName;
            sourceBuilder.AppendLine(
                $"{indent}{variableName}.RemoveHandler({routedEventReference}, {handlerExpression});");
            sourceBuilder.AppendLine(
                $"{indent}{variableName}.AddHandler({routedEventReference}, {handlerExpression});");
            return;
        }

        sourceBuilder.AppendLine(
            $"{indent}{variableName}.{eventSubscription.EventName} -= {rootReference}.{eventSubscription.HandlerMethodName};");
        sourceBuilder.AppendLine(
            $"{indent}{variableName}.{eventSubscription.EventName} += {rootReference}.{eventSubscription.HandlerMethodName};");
    }

    private static bool TryEmitDictionaryMergePropertyElement(
        ResolvedPropertyElementAssignment propertyElementAssignment,
        string variableName,
        StringBuilder sourceBuilder,
        ref int nodeCounter,
        string indent,
        string rootReference,
        IReadOnlyDictionary<string, string> namedFieldMap,
        bool emitNameScopeRegistration,
        string? nameScopeReference,
        string serviceProviderReference,
        string baseUriExpression,
        ImmutableArray<string> parentStackReferences,
        string intermediateRootReference)
    {
        sourceBuilder.AppendLine($"{indent}__TryClearCollection({variableName}.{propertyElementAssignment.PropertyName});");

        if (propertyElementAssignment.ObjectValues.Length == 0)
        {
            return true;
        }

        var hasKeyedObjectValues = true;
        foreach (var objectValue in propertyElementAssignment.ObjectValues)
        {
            if (string.IsNullOrWhiteSpace(objectValue.KeyExpression))
            {
                hasKeyedObjectValues = false;
                break;
            }
        }

        if (hasKeyedObjectValues)
        {
            foreach (var keyedObjectValue in propertyElementAssignment.ObjectValues)
            {
                var keyedChildVariable = EmitNode(
                    keyedObjectValue,
                    sourceBuilder,
                    ref nodeCounter,
                    indent,
                    rootReference,
                    namedFieldMap,
                    emitNameScopeRegistration,
                    nameScopeReference,
                    serviceProviderReference: serviceProviderReference,
                    topDownAttachmentTemplate: $"{variableName}.{propertyElementAssignment.PropertyName}.Add({keyedObjectValue.KeyExpression}, {TopDownAttachValueToken});",
                    baseUriExpression: baseUriExpression,
                    parentStackReferences: parentStackReferences,
                    intermediateRootReference: intermediateRootReference);
                if (!keyedObjectValue.UseTopDownInitialization)
                {
                    sourceBuilder.AppendLine(
                        $"{indent}{variableName}.{propertyElementAssignment.PropertyName}.Add({keyedObjectValue.KeyExpression}, {keyedChildVariable});");
                }
            }

            return true;
        }

        var valueNode = propertyElementAssignment.ObjectValues[0];
        if (valueNode.ChildAttachmentMode == ResolvedChildAttachmentMode.DictionaryAdd)
        {
            foreach (var child in valueNode.Children)
            {
                if (string.IsNullOrWhiteSpace(child.KeyExpression))
                {
                    continue;
                }

                var childVariable = EmitNode(
                    child,
                    sourceBuilder,
                    ref nodeCounter,
                    indent,
                    rootReference,
                    namedFieldMap,
                    emitNameScopeRegistration,
                    nameScopeReference,
                    serviceProviderReference: serviceProviderReference,
                    topDownAttachmentTemplate: $"{variableName}.{propertyElementAssignment.PropertyName}.Add({child.KeyExpression}, {TopDownAttachValueToken});",
                    baseUriExpression: baseUriExpression,
                    parentStackReferences: parentStackReferences,
                    intermediateRootReference: intermediateRootReference);
                if (!child.UseTopDownInitialization)
                {
                    sourceBuilder.AppendLine(
                        $"{indent}{variableName}.{propertyElementAssignment.PropertyName}.Add({child.KeyExpression}, {childVariable});");
                }
            }

            return true;
        }

        var assignedValue = EmitNode(
            valueNode,
            sourceBuilder,
            ref nodeCounter,
            indent,
            rootReference,
            namedFieldMap,
            emitNameScopeRegistration,
            nameScopeReference,
            serviceProviderReference: serviceProviderReference,
            baseUriExpression: baseUriExpression,
            parentStackReferences: parentStackReferences,
            intermediateRootReference: intermediateRootReference);
        sourceBuilder.AppendLine(
            $"{indent}__TryMergeDictionary({variableName}.{propertyElementAssignment.PropertyName}, {assignedValue});");
        return true;
    }

    private static ResolvedObjectNode? TryGetTemplateContentNode(ResolvedObjectNode node)
    {
        foreach (var assignment in node.PropertyElementAssignments)
        {
            if (IsContentPropertyName(assignment.PropertyName) &&
                assignment.ObjectValues.Length > 0)
            {
                return assignment.ObjectValues[0];
            }
        }

        if (node.Children.Length > 0)
        {
            return node.Children[0];
        }

        return null;
    }

    private static bool IsDeferredTemplateNode(ResolvedObjectNode node)
    {
        return node.TypeName is "global::Avalonia.Markup.Xaml.Templates.Template"
            or "global::Avalonia.Markup.Xaml.Templates.DataTemplate"
            or "global::Avalonia.Markup.Xaml.Templates.TreeDataTemplate"
            or "global::Avalonia.Markup.Xaml.Templates.ControlTemplate"
            or "global::Avalonia.Markup.Xaml.Templates.FocusAdornerTemplate"
            or "global::Avalonia.Markup.Xaml.Templates.ItemsPanelTemplate";
    }

    private static IReadOnlyDictionary<string, string> BuildNamedFieldMap(ResolvedViewModel viewModel)
    {
        if (!viewModel.Document.IsClassBacked)
        {
            return new Dictionary<string, string>(StringComparer.Ordinal);
        }

        var map = new Dictionary<string, string>(StringComparer.Ordinal);
        foreach (var namedElement in viewModel.NamedElements)
        {
            map[namedElement.Name] = SanitizeIdentifier(namedElement.Name);
        }

        return map;
    }

    private static ImmutableArray<string> ExtendParentStack(ImmutableArray<string> parentStackReferences, string currentReference)
    {
        if (!parentStackReferences.IsDefaultOrEmpty &&
            parentStackReferences[0].Equals(currentReference, StringComparison.Ordinal))
        {
            return parentStackReferences;
        }

        var builder = ImmutableArray.CreateBuilder<string>(parentStackReferences.Length + 1);
        builder.Add(currentReference);
        if (!parentStackReferences.IsDefaultOrEmpty)
        {
            builder.AddRange(parentStackReferences);
        }

        return builder.ToImmutable();
    }

    private static string BuildParentStackExpression(ImmutableArray<string> parentStackReferences)
    {
        if (parentStackReferences.IsDefaultOrEmpty)
        {
            return "global::System.Array.Empty<object>()";
        }

        return "new object[] { " + string.Join(", ", parentStackReferences) + " }";
    }

