    private static ImmutableArray<string> BuildRootHotReloadCollectionMembers(ResolvedObjectNode rootNode)
    {
        var members = new SortedSet<string>(StringComparer.Ordinal);

        foreach (var assignment in rootNode.PropertyElementAssignments)
        {
            if (!assignment.IsCollectionAdd && !assignment.IsDictionaryMerge)
            {
                continue;
            }

            var memberName = ExtractMemberName(assignment.PropertyName);
            if (!string.IsNullOrWhiteSpace(memberName))
            {
                members.Add(memberName);
            }
        }

        switch (rootNode.ChildAttachmentMode)
        {
            case ResolvedChildAttachmentMode.ChildrenCollection:
                members.Add("Children");
                break;
            case ResolvedChildAttachmentMode.ItemsCollection:
                members.Add("Items");
                break;
        }

        return ToImmutableArray(members);
    }

    private static ImmutableArray<string> BuildRootHotReloadClrPropertyMembers(
        ResolvedObjectNode rootNode,
        IReadOnlyDictionary<string, string> namedFieldMap)
    {
        var members = new SortedSet<string>(StringComparer.Ordinal);

        foreach (var assignment in rootNode.PropertyAssignments)
        {
            if (!string.IsNullOrWhiteSpace(BuildAvaloniaPropertyExpression(assignment)))
            {
                continue;
            }

            var memberName = ExtractMemberName(assignment.PropertyName);
            if (!string.IsNullOrWhiteSpace(memberName))
            {
                members.Add(memberName);
            }
        }

        foreach (var assignment in rootNode.PropertyElementAssignments)
        {
            if (assignment.IsCollectionAdd ||
                assignment.IsDictionaryMerge ||
                assignment.ObjectValues.Length == 0)
            {
                continue;
            }

            if (!string.IsNullOrWhiteSpace(BuildAvaloniaPropertyExpression(assignment)))
            {
                continue;
            }

            var memberName = ExtractMemberName(assignment.PropertyName);
            if (!string.IsNullOrWhiteSpace(memberName))
            {
                members.Add(memberName);
            }
        }

        foreach (var fieldName in namedFieldMap.Values)
        {
            if (!string.IsNullOrWhiteSpace(fieldName))
            {
                members.Add(fieldName);
            }
        }

        return ToImmutableArray(members);
    }

    private static ImmutableArray<string> BuildRootHotReloadAvaloniaPropertyMembers(ResolvedObjectNode rootNode)
    {
        var members = new SortedSet<string>(StringComparer.Ordinal);

        foreach (var assignment in rootNode.PropertyAssignments)
        {
            var avaloniaPropertyExpression = BuildAvaloniaPropertyExpression(assignment);
            if (avaloniaPropertyExpression is null || avaloniaPropertyExpression.Trim().Length == 0)
            {
                continue;
            }

            members.Add(avaloniaPropertyExpression);
        }

        foreach (var assignment in rootNode.PropertyElementAssignments)
        {
            var avaloniaPropertyExpression = BuildAvaloniaPropertyExpression(assignment);
            if (assignment.IsCollectionAdd ||
                assignment.IsDictionaryMerge ||
                assignment.ObjectValues.Length == 0 ||
                avaloniaPropertyExpression is null ||
                avaloniaPropertyExpression.Trim().Length == 0)
            {
                continue;
            }

            members.Add(avaloniaPropertyExpression);
        }

        return ToImmutableArray(members);
    }

    private static ImmutableArray<ResolvedEventSubscription> BuildRootHotReloadEventSubscriptions(ResolvedObjectNode rootNode)
    {
        if (rootNode.EventSubscriptions.IsDefaultOrEmpty)
        {
            return ImmutableArray<ResolvedEventSubscription>.Empty;
        }

        var builder = ImmutableArray.CreateBuilder<ResolvedEventSubscription>(rootNode.EventSubscriptions.Length);
        foreach (var eventSubscription in rootNode.EventSubscriptions)
        {
            if (string.IsNullOrWhiteSpace(eventSubscription.HandlerMethodName))
            {
                continue;
            }

            if (eventSubscription.Kind == ResolvedEventSubscriptionKind.ClrEvent &&
                string.IsNullOrWhiteSpace(eventSubscription.EventName))
            {
                continue;
            }

            if (eventSubscription.Kind == ResolvedEventSubscriptionKind.RoutedEvent &&
                (string.IsNullOrWhiteSpace(eventSubscription.RoutedEventOwnerTypeName) ||
                 string.IsNullOrWhiteSpace(eventSubscription.RoutedEventFieldName) ||
                 string.IsNullOrWhiteSpace(eventSubscription.RoutedEventHandlerTypeName)))
            {
                continue;
            }

            builder.Add(eventSubscription);
        }

        return builder.Count == 0
            ? ImmutableArray<ResolvedEventSubscription>.Empty
            : builder.ToImmutable();
    }

    private static ImmutableArray<ResolvedEventBindingDefinition> BuildEventBindingDefinitions(ResolvedObjectNode rootNode)
    {
        var builder = ImmutableArray.CreateBuilder<ResolvedEventBindingDefinition>();
        var seen = new HashSet<string>(StringComparer.Ordinal);
        CollectEventBindingDefinitions(rootNode, builder, seen);
        return builder.Count == 0
            ? ImmutableArray<ResolvedEventBindingDefinition>.Empty
            : builder.ToImmutable();
    }

    private static IReadOnlyDictionary<string, string> BuildStableEventBindingMethodNameMap(
        ImmutableArray<ResolvedEventBindingDefinition> eventBindingDefinitions)
    {
        var map = new Dictionary<string, string>(StringComparer.Ordinal);
        var signatureNames = new Dictionary<string, string>(StringComparer.Ordinal);
        var usedNames = new HashSet<string>(StringComparer.Ordinal);
        for (var index = 0; index < eventBindingDefinitions.Length; index++)
        {
            var definition = eventBindingDefinitions[index];
            if (string.IsNullOrWhiteSpace(definition.GeneratedMethodName))
            {
                continue;
            }

            var signature = BuildStableEventBindingSignature(definition);
            if (!signatureNames.TryGetValue(signature, out var stableName))
            {
                stableName = "__AXSG_EventBinding_" + ComputeStableEventBindingSignatureHash(signature);
                if (!usedNames.Add(stableName))
                {
                    var collisionIndex = 1;
                    var collisionName = stableName + "_" + collisionIndex;
                    while (!usedNames.Add(collisionName))
                    {
                        collisionIndex++;
                        collisionName = stableName + "_" + collisionIndex;
                    }

                    stableName = collisionName;
                }

                signatureNames[signature] = stableName;
            }

            map[definition.GeneratedMethodName] = stableName;
        }

        return map.Count == 0
            ? EmptyGeneratedMethodNameMap
            : map;
    }

    private static string BuildStableEventBindingSignature(ResolvedEventBindingDefinition definition)
    {
        var builder = new StringBuilder();
        AppendSignaturePart(builder, definition.DelegateTypeName);
        AppendSignaturePart(builder, definition.TargetKind.ToString());
        AppendSignaturePart(builder, definition.SourceMode.ToString());
        AppendSignaturePart(builder, definition.TargetPath);
        AppendSignaturePart(builder, definition.ParameterPath);
        AppendSignaturePart(builder, definition.ParameterValueExpression);
        AppendSignaturePart(builder, definition.DataContextTypeName);
        AppendSignaturePart(builder, definition.RootTypeName);
        AppendSignaturePart(builder, definition.CompiledDataContextTargetPath);
        AppendSignaturePart(builder, definition.CompiledRootTargetPath);
        AppendSignaturePart(builder, definition.CompiledDataContextLambdaExpression);
        AppendSignaturePart(builder, definition.CompiledRootLambdaExpression);
        AppendSignaturePart(builder, definition.CompiledDataContextParameterPath);
        AppendSignaturePart(builder, definition.CompiledRootParameterPath);
        AppendSignaturePart(builder, definition.PassEventArgs ? "1" : "0");

        for (var parameterIndex = 0; parameterIndex < definition.Parameters.Length; parameterIndex++)
        {
            var parameter = definition.Parameters[parameterIndex];
            AppendSignaturePart(builder, parameter.TypeName);
            AppendSignaturePart(builder, parameter.Name);
        }

        if (definition.CompiledDataContextMethodCall is not null)
        {
            AppendSignaturePart(builder, definition.CompiledDataContextMethodCall.TargetPath);
            AppendSignaturePart(builder, definition.CompiledDataContextMethodCall.MethodName);
            for (var argumentIndex = 0; argumentIndex < definition.CompiledDataContextMethodCall.Arguments.Length; argumentIndex++)
            {
                var argument = definition.CompiledDataContextMethodCall.Arguments[argumentIndex];
                AppendSignaturePart(builder, argument.Kind.ToString());
                AppendSignaturePart(builder, argument.TypeName);
            }
        }

        if (definition.CompiledRootMethodCall is not null)
        {
            AppendSignaturePart(builder, definition.CompiledRootMethodCall.TargetPath);
            AppendSignaturePart(builder, definition.CompiledRootMethodCall.MethodName);
            for (var argumentIndex = 0; argumentIndex < definition.CompiledRootMethodCall.Arguments.Length; argumentIndex++)
            {
                var argument = definition.CompiledRootMethodCall.Arguments[argumentIndex];
                AppendSignaturePart(builder, argument.Kind.ToString());
                AppendSignaturePart(builder, argument.TypeName);
            }
        }

        return builder.ToString();
    }

    private static void AppendSignaturePart(StringBuilder builder, string? value)
    {
        if (builder.Length > 0)
        {
            builder.Append('\u001F');
        }

        if (!string.IsNullOrEmpty(value))
        {
            builder.Append(value);
        }
    }

    private static string ComputeStableEventBindingSignatureHash(string signature)
    {
        unchecked
        {
            const ulong offset = 14695981039346656037UL;
            const ulong prime = 1099511628211UL;
            var hash = offset;
            for (var index = 0; index < signature.Length; index++)
            {
                hash ^= signature[index];
                hash *= prime;
            }

            return hash.ToString("X16", CultureInfo.InvariantCulture);
        }
    }

    private static string ResolveEmittedEventBindingMethodName(
        string? generatedMethodName,
        IReadOnlyDictionary<string, string> emittedEventBindingMethodNames)
    {
        if (generatedMethodName is null)
        {
            return string.Empty;
        }

        var methodName = generatedMethodName;
        if (string.IsNullOrWhiteSpace(methodName))
        {
            return string.Empty;
        }

        if (emittedEventBindingMethodNames.TryGetValue(methodName, out var stableMethodName) &&
            !string.IsNullOrWhiteSpace(stableMethodName))
        {
            return stableMethodName;
        }

        return methodName;
    }

    private static void CollectEventBindingDefinitions(
        ResolvedObjectNode node,
        ImmutableArray<ResolvedEventBindingDefinition>.Builder builder,
        HashSet<string> seen)
    {
        if (!node.EventSubscriptions.IsDefaultOrEmpty)
        {
            foreach (var eventSubscription in node.EventSubscriptions)
            {
                var definition = eventSubscription.EventBindingDefinition;
                if (definition is null ||
                    string.IsNullOrWhiteSpace(definition.GeneratedMethodName) ||
                    !seen.Add(definition.GeneratedMethodName))
                {
                    continue;
                }

                builder.Add(definition);
            }
        }

        if (node.Children.IsDefaultOrEmpty)
        {
            return;
        }

        foreach (var child in node.Children)
        {
            CollectEventBindingDefinitions(child, builder, seen);
        }
    }

    private static void EmitEventBindingMethods(
        StringBuilder sourceBuilder,
        ImmutableArray<ResolvedEventBindingDefinition> eventBindingDefinitions,
        IReadOnlyDictionary<string, string> emittedEventBindingMethodNames)
    {
        if (!eventBindingDefinitions.IsDefaultOrEmpty)
        {
            sourceBuilder.AppendLine("        private static object? __TryGetEventBindingDataContext(object? value)");
            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine("            return value is global::Avalonia.Controls.Control control");
            sourceBuilder.AppendLine("                ? control.DataContext");
            sourceBuilder.AppendLine("                : null;");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
        }

        foreach (var definition in eventBindingDefinitions)
        {
            var methodName = ResolveEmittedEventBindingMethodName(
                definition.GeneratedMethodName,
                emittedEventBindingMethodNames);

            if (string.IsNullOrWhiteSpace(methodName))
            {
                continue;
            }

            var senderExpression = definition.Parameters.Length > 0
                ? definition.Parameters[0].Name
                : "null";
            var eventArgsExpression = definition.Parameters.Length > 1
                ? definition.Parameters[1].Name
                : "null";

            sourceBuilder.AppendLine(
                $"        private void {methodName}({BuildEventBindingParameterList(definition.Parameters)})");
            sourceBuilder.AppendLine("        {");
            if (definition.TargetKind == ResolvedEventBindingTargetKind.Command)
            {
                var dataContextCommandExpression = string.Empty;
                var dataContextParameterExpression = "null";
                var canEmitDataContextCommand = definition.SourceMode != ResolvedEventBindingSourceMode.Root &&
                                                !string.IsNullOrWhiteSpace(definition.DataContextTypeName) &&
                                                !string.IsNullOrWhiteSpace(definition.CompiledDataContextTargetPath) &&
                                                TryBuildEventBindingMemberAccessExpression(
                                                    "__axsgDataContextTyped",
                                                    definition.CompiledDataContextTargetPath!,
                                                    out dataContextCommandExpression) &&
                                                TryBuildEventBindingParameterExpression(
                                                    definition,
                                                    "__axsgDataContextTyped",
                                                    definition.CompiledDataContextParameterPath,
                                                    eventArgsExpression,
                                                    out dataContextParameterExpression);

                var rootCommandExpression = string.Empty;
                var rootParameterExpression = "null";
                var canEmitRootCommand = definition.SourceMode != ResolvedEventBindingSourceMode.DataContext &&
                                         !string.IsNullOrWhiteSpace(definition.RootTypeName) &&
                                         !string.IsNullOrWhiteSpace(definition.CompiledRootTargetPath) &&
                                         TryBuildEventBindingMemberAccessExpression(
                                             "__axsgRootTyped",
                                             definition.CompiledRootTargetPath!,
                                             out rootCommandExpression) &&
                                         TryBuildEventBindingParameterExpression(
                                             definition,
                                             "__axsgRootTyped",
                                             definition.CompiledRootParameterPath,
                                             eventArgsExpression,
                                             out rootParameterExpression);

                sourceBuilder.AppendLine("            try");
                sourceBuilder.AppendLine("            {");
                if (canEmitDataContextCommand)
                {
                    sourceBuilder.AppendLine(
                        $"                var __axsgDataContext = __TryGetEventBindingDataContext({senderExpression}) ?? __TryGetEventBindingDataContext(this);");
                    sourceBuilder.AppendLine(
                        $"                if (__axsgDataContext is {definition.DataContextTypeName} __axsgDataContextTyped)");
                    sourceBuilder.AppendLine("                {");
                    sourceBuilder.AppendLine(
                        $"                    var __axsgCommandCandidate = (object?)({dataContextCommandExpression});");
                    sourceBuilder.AppendLine("                    if (__axsgCommandCandidate is global::System.Windows.Input.ICommand __axsgCommand)");
                    sourceBuilder.AppendLine("                    {");
                    sourceBuilder.AppendLine(
                        $"                        var __axsgParameter = {dataContextParameterExpression};");
                    sourceBuilder.AppendLine("                        if (__axsgCommand.CanExecute(__axsgParameter))");
                    sourceBuilder.AppendLine("                        {");
                    sourceBuilder.AppendLine("                            __axsgCommand.Execute(__axsgParameter);");
                    sourceBuilder.AppendLine("                        }");
                    sourceBuilder.AppendLine("                        return;");
                    sourceBuilder.AppendLine("                    }");
                    sourceBuilder.AppendLine("                }");
                }

                if (canEmitRootCommand)
                {
                    sourceBuilder.AppendLine(
                        $"                if (this is {definition.RootTypeName} __axsgRootTyped)");
                    sourceBuilder.AppendLine("                {");
                    sourceBuilder.AppendLine(
                        $"                    var __axsgCommandCandidate = (object?)({rootCommandExpression});");
                    sourceBuilder.AppendLine("                    if (__axsgCommandCandidate is global::System.Windows.Input.ICommand __axsgCommand)");
                    sourceBuilder.AppendLine("                    {");
                    sourceBuilder.AppendLine(
                        $"                        var __axsgParameter = {rootParameterExpression};");
                    sourceBuilder.AppendLine("                        if (__axsgCommand.CanExecute(__axsgParameter))");
                    sourceBuilder.AppendLine("                        {");
                    sourceBuilder.AppendLine("                            __axsgCommand.Execute(__axsgParameter);");
                    sourceBuilder.AppendLine("                        }");
                    sourceBuilder.AppendLine("                        return;");
                    sourceBuilder.AppendLine("                    }");
                    sourceBuilder.AppendLine("                }");
                }

                sourceBuilder.AppendLine("            }");
                sourceBuilder.AppendLine("            catch");
                sourceBuilder.AppendLine("            {");
                sourceBuilder.AppendLine("            }");
            }
            else if (definition.TargetKind == ResolvedEventBindingTargetKind.Method)
            {
                var dataContextMethodInvocationExpression = string.Empty;
                var dataContextMethodParameterExpression = "null";
                var dataContextMethodNeedsParameter = false;
                var canBuildDataContextMethodInvocation = false;
                if (definition.SourceMode != ResolvedEventBindingSourceMode.Root &&
                    definition.CompiledDataContextMethodCall is not null)
                {
                    canBuildDataContextMethodInvocation = TryBuildEventBindingMethodInvocationExpression(
                        "__axsgDataContextTyped",
                        definition.CompiledDataContextMethodCall,
                        senderExpression,
                        eventArgsExpression,
                        out dataContextMethodInvocationExpression,
                        out dataContextMethodNeedsParameter);
                }

                var canEmitDataContextMethod = canBuildDataContextMethodInvocation &&
                                               !string.IsNullOrWhiteSpace(definition.DataContextTypeName) &&
                                               (!dataContextMethodNeedsParameter ||
                                                TryBuildEventBindingParameterExpression(
                                                    definition,
                                                    "__axsgDataContextTyped",
                                                    definition.CompiledDataContextParameterPath,
                                                    eventArgsExpression,
                                                    out dataContextMethodParameterExpression));

                var rootMethodInvocationExpression = string.Empty;
                var rootMethodParameterExpression = "null";
                var rootMethodNeedsParameter = false;
                var canBuildRootMethodInvocation = false;
                if (definition.SourceMode != ResolvedEventBindingSourceMode.DataContext &&
                    definition.CompiledRootMethodCall is not null)
                {
                    canBuildRootMethodInvocation = TryBuildEventBindingMethodInvocationExpression(
                        "__axsgRootTyped",
                        definition.CompiledRootMethodCall,
                        senderExpression,
                        eventArgsExpression,
                        out rootMethodInvocationExpression,
                        out rootMethodNeedsParameter);
                }

                var canEmitRootMethod = canBuildRootMethodInvocation &&
                                        !string.IsNullOrWhiteSpace(definition.RootTypeName) &&
                                        (!rootMethodNeedsParameter ||
                                         TryBuildEventBindingParameterExpression(
                                             definition,
                                             "__axsgRootTyped",
                                             definition.CompiledRootParameterPath,
                                             eventArgsExpression,
                                             out rootMethodParameterExpression));

                sourceBuilder.AppendLine("            try");
                sourceBuilder.AppendLine("            {");
                if (canEmitDataContextMethod)
                {
                    sourceBuilder.AppendLine(
                        $"                var __axsgDataContext = __TryGetEventBindingDataContext({senderExpression}) ?? __TryGetEventBindingDataContext(this);");
                    sourceBuilder.AppendLine(
                        $"                if (__axsgDataContext is {definition.DataContextTypeName} __axsgDataContextTyped)");
                    sourceBuilder.AppendLine("                {");
                    if (dataContextMethodNeedsParameter)
                    {
                        sourceBuilder.AppendLine(
                            $"                    var __axsgParameter = {dataContextMethodParameterExpression};");
                    }

                    sourceBuilder.AppendLine($"                    {dataContextMethodInvocationExpression};");
                    sourceBuilder.AppendLine("                    return;");
                    sourceBuilder.AppendLine("                }");
                }

                if (canEmitRootMethod)
                {
                    sourceBuilder.AppendLine(
                        $"                if (this is {definition.RootTypeName} __axsgRootTyped)");
                    sourceBuilder.AppendLine("                {");
                    if (rootMethodNeedsParameter)
                    {
                        sourceBuilder.AppendLine(
                            $"                    var __axsgParameter = {rootMethodParameterExpression};");
                    }

                    sourceBuilder.AppendLine($"                    {rootMethodInvocationExpression};");
                    sourceBuilder.AppendLine("                    return;");
                    sourceBuilder.AppendLine("                }");
                }

                sourceBuilder.AppendLine("            }");
                sourceBuilder.AppendLine("            catch");
                sourceBuilder.AppendLine("            {");
                sourceBuilder.AppendLine("            }");
            }
            else
            {
                var canEmitDataContextLambda = definition.SourceMode != ResolvedEventBindingSourceMode.Root &&
                                               !string.IsNullOrWhiteSpace(definition.DataContextTypeName) &&
                                               !string.IsNullOrWhiteSpace(definition.CompiledDataContextLambdaExpression);
                var canEmitRootLambda = definition.SourceMode != ResolvedEventBindingSourceMode.DataContext &&
                                        !string.IsNullOrWhiteSpace(definition.RootTypeName) &&
                                        !string.IsNullOrWhiteSpace(definition.CompiledRootLambdaExpression);

                sourceBuilder.AppendLine("            try");
                sourceBuilder.AppendLine("            {");
                if (canEmitDataContextLambda)
                {
                    sourceBuilder.AppendLine(
                        $"                var __axsgDataContext = __TryGetEventBindingDataContext({senderExpression}) ?? __TryGetEventBindingDataContext(this);");
                    sourceBuilder.AppendLine(
                        $"                if (__axsgDataContext is {definition.DataContextTypeName} __axsgDataContextTyped)");
                    sourceBuilder.AppendLine("                {");
                    EmitCompiledEventLambdaInvocation(
                        sourceBuilder,
                        "                    ",
                        definition,
                        definition.CompiledDataContextLambdaExpression!,
                        definition.DataContextTypeName!,
                        "__axsgDataContextTyped",
                        "this",
                        senderExpression);
                    sourceBuilder.AppendLine("                }");
                }

                if (canEmitRootLambda)
                {
                    sourceBuilder.AppendLine(
                        $"                if (this is {definition.RootTypeName} __axsgRootTyped)");
                    sourceBuilder.AppendLine("                {");
                    EmitCompiledEventLambdaInvocation(
                        sourceBuilder,
                        "                    ",
                        definition,
                        definition.CompiledRootLambdaExpression!,
                        "global::System.Object",
                        "__axsgRootTyped",
                        "__axsgRootTyped",
                        senderExpression);
                    sourceBuilder.AppendLine("                }");
                }

                sourceBuilder.AppendLine("            }");
                sourceBuilder.AppendLine("            catch");
                sourceBuilder.AppendLine("            {");
                sourceBuilder.AppendLine("            }");
            }

            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
        }
    }

    private static string BuildEventBindingParameterList(ImmutableArray<ResolvedEventBindingParameter> parameters)
    {
        if (parameters.IsDefaultOrEmpty)
        {
            return string.Empty;
        }

        var builder = new StringBuilder();
        for (var index = 0; index < parameters.Length; index++)
        {
            if (index > 0)
            {
                builder.Append(", ");
            }

            var parameter = parameters[index];
            var typeName = string.IsNullOrWhiteSpace(parameter.TypeName)
                ? "object?"
                : parameter.TypeName;
            var name = string.IsNullOrWhiteSpace(parameter.Name)
                ? "__arg" + index.ToString()
                : parameter.Name;
            builder.Append(typeName).Append(' ').Append(name);
        }

        return builder.ToString();
    }

    private static string BuildEventBindingInvocationArgumentList(ImmutableArray<ResolvedEventBindingParameter> parameters)
    {
        if (parameters.IsDefaultOrEmpty)
        {
            return string.Empty;
        }

        var builder = new StringBuilder();
        for (var index = 0; index < parameters.Length; index++)
        {
            if (index > 0)
            {
                builder.Append(", ");
            }

            builder.Append(parameters[index].Name);
        }

        return builder.ToString();
    }

    private static void EmitCompiledEventLambdaInvocation(
        StringBuilder sourceBuilder,
        string indent,
        ResolvedEventBindingDefinition definition,
        string lambdaExpression,
        string sourceTypeName,
        string sourceExpression,
        string rootExpression,
        string senderExpression)
    {
        if (!definition.UsesInlineCodeContext)
        {
            sourceBuilder.AppendLine(indent + "var __axsgLambdaSource = " + sourceExpression + ";");
            sourceBuilder.AppendLine(indent + definition.DelegateTypeName + " __axsgHandler = " + lambdaExpression + ";");
            sourceBuilder.AppendLine(indent + "__axsgHandler(" + BuildEventBindingInvocationArgumentList(definition.Parameters) + ");");
            sourceBuilder.AppendLine(indent + "return;");
            return;
        }

        var targetTypeName = string.IsNullOrWhiteSpace(definition.LambdaContextTargetTypeName)
            ? "global::System.Object"
            : definition.LambdaContextTargetTypeName!;

        EmitInlineEventLambdaInvocationBlock(
            sourceBuilder,
            indent,
            definition,
            lambdaExpression,
            sourceTypeName,
            sourceExpression,
            rootExpression,
            targetTypeName,
            senderExpression,
            "__axsgTargetTyped");

        sourceBuilder.AppendLine(indent + "if ((object)this is " + targetTypeName + " __axsgSelfTargetTyped)");
        sourceBuilder.AppendLine(indent + "{");
        EmitInlineEventLambdaContextAndInvoke(
            sourceBuilder,
            indent + "    ",
            definition,
            lambdaExpression,
            sourceTypeName,
            sourceExpression,
            rootExpression,
            targetTypeName,
            "__axsgSelfTargetTyped");
        sourceBuilder.AppendLine(indent + "    return;");
        sourceBuilder.AppendLine(indent + "}");
    }

