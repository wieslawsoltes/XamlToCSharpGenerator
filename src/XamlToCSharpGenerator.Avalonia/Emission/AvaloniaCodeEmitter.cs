    private static bool TryBuildSpecialClrSetterInvocation(
        string variableName,
        string propertyName,
        string? clrPropertyOwnerTypeName,
        string? clrPropertyTypeName,
        string valueExpression,
        out string statement)
    {
        statement = string.Empty;
        if (string.IsNullOrWhiteSpace(clrPropertyOwnerTypeName) ||
            !string.IsNullOrWhiteSpace(clrPropertyTypeName) ||
            string.IsNullOrWhiteSpace(propertyName))
        {
            return false;
        }

        if (propertyName.StartsWith("SetClass:", StringComparison.Ordinal))
        {
            var className = propertyName.Substring("SetClass:".Length);
            if (className.Length == 0)
            {
                return false;
            }

            statement = clrPropertyOwnerTypeName +
                        ".ApplyClassValue(" +
                        variableName +
                        ", \"" +
                        Escape(className) +
                        "\", " +
                        valueExpression +
                        ");";
            return true;
        }

        if (!propertyName.StartsWith("Set", StringComparison.Ordinal) ||
            !IsClrIdentifier(propertyName))
        {
            return false;
        }

        statement = clrPropertyOwnerTypeName +
                    "." +
                    propertyName +
                    "(" +
                    variableName +
                    ", " +
                    valueExpression +
                    ");";
        return true;
    }

    private static bool IsClrIdentifier(string token)
    {
        if (string.IsNullOrWhiteSpace(token) ||
            (!char.IsLetter(token[0]) && token[0] != '_'))
        {
            return false;
        }

        for (var index = 1; index < token.Length; index++)
        {
            var character = token[index];
            if (!char.IsLetterOrDigit(character) && character != '_')
            {
                return false;
            }
        }

        return true;
    }

    private static string BuildClrTypedValueExpression(string? clrPropertyTypeName, string valueExpression)
    {
        if (string.IsNullOrWhiteSpace(clrPropertyTypeName))
        {
            return valueExpression;
        }

        var normalizedTypeName = clrPropertyTypeName.Trim();
        return normalizedTypeName switch
        {
            "global::System.Object" or
            "global::System.Object?" or
            "object" or
            "object?" => valueExpression,
            _ => "(" + normalizedTypeName + ")(" + valueExpression + ")"
        };
    }

    private static bool IsBindingValueKind(ResolvedValueKind valueKind)
    {
        return valueKind is ResolvedValueKind.Binding
            or ResolvedValueKind.TemplateBinding
            or ResolvedValueKind.DynamicResourceBinding;
    }

    private static bool HasExplicitContentValue(ResolvedObjectNode node, string contentPropertyName)
    {
        foreach (var assignment in node.PropertyAssignments)
        {
            if (IsContentPropertyName(assignment.PropertyName, contentPropertyName))
            {
                return true;
            }
        }

        foreach (var assignment in node.PropertyElementAssignments)
        {
            if (IsContentPropertyName(assignment.PropertyName, contentPropertyName))
            {
                return true;
            }
        }

        return false;
    }

    private static bool IsContentPropertyName(string propertyName, string contentPropertyName = "Content")
    {
        if (propertyName.Equals(contentPropertyName, StringComparison.OrdinalIgnoreCase))
        {
            return true;
        }

        var separatorIndex = propertyName.LastIndexOf('.');
        if (separatorIndex >= 0 &&
            separatorIndex < propertyName.Length - 1)
        {
            return propertyName.Substring(separatorIndex + 1)
                .Equals(contentPropertyName, StringComparison.OrdinalIgnoreCase);
        }

        return false;
    }

    private static string BuildHotDesignDocumentRoleExpression(ResolvedViewModel viewModel)
    {
        var roleToken = viewModel.HotDesignArtifactKind switch
        {
            ResolvedHotDesignArtifactKind.Template => "Template",
            ResolvedHotDesignArtifactKind.ResourceDictionary => "Resources",
            ResolvedHotDesignArtifactKind.ControlTheme => "Theme",
            _ when !viewModel.Document.IsClassBacked => "Include",
            _ => "Root"
        };

        return "global::XamlToCSharpGenerator.Runtime.SourceGenHotDesignDocumentRole." + roleToken;
    }

    private static string BuildHotDesignArtifactKindExpression(ResolvedViewModel viewModel)
    {
        return "global::XamlToCSharpGenerator.Runtime.SourceGenHotDesignArtifactKind." + viewModel.HotDesignArtifactKind;
    }

    private static string BuildHotDesignScopeHintsExpression(ResolvedViewModel viewModel)
    {
        var hints = viewModel.HotDesignScopeHints;
        if (hints.IsDefaultOrEmpty)
        {
            return "null";
        }

        if (hints.Length == 1)
        {
            return $"new string[] {{ \"{Escape(hints[0])}\" }}";
        }

        return $"new string[] {{ \"{Escape(hints[0])}\", \"{Escape(hints[1])}\" }}";
    }

    private static string BuildHintName(ResolvedViewModel viewModel)
    {
        var sourceIdentity = viewModel.BuildUri;
        if (string.IsNullOrWhiteSpace(sourceIdentity))
        {
            sourceIdentity = (viewModel.Document.ClassFullName ?? viewModel.Document.ClassName) + "|" + viewModel.Document.TargetPath;
        }

        var hash = ComputeStableHashHex(sourceIdentity);
        var baseName = viewModel.Document.ClassFullName ?? (viewModel.Document.ClassNamespace + "." + viewModel.Document.ClassName);
        var sanitizedBaseName = SanitizeHintSegment(baseName);
        return sanitizedBaseName + "." + hash + ".XamlSourceGen.g.cs";
    }

    private static string SanitizeHintSegment(string value)
    {
        if (string.IsNullOrWhiteSpace(value))
        {
            return "GeneratedXaml";
        }

        var chars = value.ToCharArray();
        for (var index = 0; index < chars.Length; index++)
        {
            var c = chars[index];
            if (!char.IsLetterOrDigit(c) && c != '_' && c != '.')
            {
                chars[index] = '_';
            }
        }

        return new string(chars);
    }

    private static string ComputeStableHashHex(string text)
    {
        unchecked
        {
            var hash = 2166136261u;
            foreach (var c in text)
            {
                hash ^= c;
                hash *= 16777619u;
            }

            return hash.ToString("x8");
        }
    }
