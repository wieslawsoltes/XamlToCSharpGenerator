            sourceBuilder.AppendLine("        {");
            sourceBuilder.AppendLine(
                $"            return global::XamlToCSharpGenerator.Runtime.SourceGenStaticResourceResolver.Resolve(anchor, key, \"{escapedUri}\");");
            sourceBuilder.AppendLine("        }");
            sourceBuilder.AppendLine();
        }

